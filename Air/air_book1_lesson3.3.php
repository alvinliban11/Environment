    <h2>LESSON 3.3 CHEMICAL AND PHYSICAL PROPERTIES</h2>
<p><b>Temperature</b></p>

<p>Whether in the gaseous, liquid, or solid state, all molecules possess some degree of
kinetic energy, i.e., they are in constant motion—vibrating, rotating, or translating.</p>



<p>The kinetic energies of individual molecules cannot be measured, but the combined
effect of these energies in a very large number of molecules can. This measurable quantity is known as temperature; it is a macroscopic concept only and as such does not exist
on the molecular level.<br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
Temperature can be measured in many ways; the most common method makes use
of the expansion of mercury (usually encased inside a glass capillary tube) with increasing temperature. However, thermocouples or thermistors can also be employed.<br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
The two most commonly used temperature scales are the Celsius (or Centigrade)
and Fahrenheit scales. The Celsius is based on the boiling and freezing points of
water at 1 atmosphere pressure; to the former, a value of 1008C is assigned, and to
the latter, a value of 08C. On the older Fahrenheit scale, these temperatures correspond
to 212 and 328F, respectively. Equations (3.4) and (3.5) provide the conversion from one
scale to the other.</p>

<img src="img/3.3.png">

<p>Experiments with gases at low to moderate pressures (up to a few atmospheres) have
shown that, if the pressure is kept constant, the volume of a gas and its temperature are
linearly related (Charles’ law) and that a decrease of 0.3663% or 1
273   of the initial
volume is experienced for every temperature drop of 18C. These experiments were
not extended to very low temperatures, but if the linear relationship were extrapolated,
the volume of the gas would theoretically be zero at a temperature of approximately
22738C or 24608F. This temperature has become known as absolute zero and is the
basis for the definition of two absolute temperature scales. (An absolute scale is one
which does not allow negative quantities.) These absolute temperature scales are the
Kelvin (K) and Rankine (8R) scales; the former is defined by shifting the Celsius
scale by 2738C so that 0 K is equal to 22738C; Equation (3.6) shows this relationship:</p>

<img src="img/3.3.1.png">

<p><b>Pressure</b></p>
<p>In the gaseous state, the molecules possess a high degree of translational kinetic energy,
which means that they are able to move quite freely throughout the body of the gas. If the
gas is in a container of some type, the molecules are constantly bombarding the walls of
the container. The macroscopic effect of this bombardment by a tremendous number ofmolecules—enough to make the effect measurable—is called pressure. The natural units
of pressure are force per unit area. In the example of the gas in a container, the unit area
is a portion of the inside solid surface of the container wall while the force, measured
perpendicularly to the unit area, is the result of the molecules hitting the unit area and
swing up momentum during the sudden change of direction.</p>
<p>There are a number of different methods used to express a pressure measurement.
Some of them are natural units, i.e., based on a force per unit area, e.g., pound
(force) per square inch (abbreviated lbf/in2 or psi) or dyne per square centimeter
(dyn/cm2
). Others are based on a fluid height, such as inches of water (in H2O) or millimeters of mercury (mm Hg); units such as these are convenient when the pressure is
indicated by a difference between two levels of a liquid as in a manometer or barometer.
Barometric pressure and atmospheric pressure are synonymous and measure the
ambient air pressure. Standard barometric pressure is the average atmospheric pressure
at sea level, 458 north latitude at 328F. It is used to define another unit of pressure
called the atmosphere (atm). Standard barometric pressure is 1 atm and is equivalent
to 14.696 psi and 29.921 in Hg. As one might expect, barometric pressure varies with
weather and altitude.</p>
<p>Measurements of pressure by most gauges indicate the difference in pressure either
above or below that of the atmosphere surrounding the gauge. Gauge pressure is the
pressure indicated by such a device. If the pressure in the system measured by the
gauge is greater than the pressure prevailing in the atmosphere the gauge pressure is
expressed positively; if lower than atmospheric pressure the gauge pressure is a negative
quantity; the term vacuum designates a negative gauge pressure. Gauge pressures are
often identified by the letter g after the pressure unit; for example, psig (pounds per
square inch gauge) is a gauge pressure in psi units.
Since gauge pressure is the pressure relative to the prevailing atmospheric pressure,
the sum of the two gives the absolute pressure, indicated by the letter a after the unit
[e.g., psia (pounds per square inch absolute)]:</p>

<img src="img/3.3.2.png">

<p>The absolute pressure scale is absolute in the same sense that the absolute temperature scale is absolute, i.e., a pressure of zero psia is the lowest possible pressure
theoretically achievable—a perfect vacuum.</p>

