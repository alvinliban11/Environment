<h3>PROBLEM</h3>

<p><b>4.1 The Three Ts
</b></p>

<p>The three Ts of combustion are commonly understood to mean (select one)<br>
(a) Time, temperature, and type of fuel<br>
(b) Temperature, type of fuel, and turbulence<br>
(c) Time, temperature, and turbulence<br>
(d) Type of fuel, turbulence, and time<br>
Solution: As noted in Section 4.1, the “three Ts” refer to time, temperature, and
turbulence. The correct answer is therefore (c).
</p>

<p><b>4.2 Typical Direct Flame Incinerator Operating Range

</b></p>


<p>What is a common temperature operating range for a direct flame incinerator?<br>
(a) 400–5008F<br>
(b) 700–9008F<br>
(c) 1300–15008F<br>
(d) 2000–24008F<br>
Solution: Although the operating temperatures vary from application to
application and from process to process, a typical value would fall within the
1300–15008F range. The correct answer is therefore (c).</p>


<p><b>4.3 Typical Catalytic Afterburner Operating Temperature</b></p>


<p>A rule of thumb for typical operating temperature range of a catalytic afterburner<br>
(a) 200–4008F<br>
(b) 700–9008F(c) 1200–14008F<br>
(d) 1800–20008F<br>
Solution: Although the operating temperatures vary with each application and
each process, a typical value would fall within the 700–9008F range. The
correct answer is therefore (b).</p>



<p><b>4.4 Approximate Direct Flame Incinerator Residence Time
</b></p>



<p>What is an approximate residence time for gaseous material being combusted in
a typical direct flame incinerator?<br>
(a) 0.02 s<br>
(b) 10 s<br>
(c) 0.3 s<br>
(d) 0.4 min<br>
Solution: As with the answer to Problem 4.3, solutions will vary,
but an approximate typical residence time is 0.3s. The correct answer is
therefore (c).</p>


<p><b>4.5 Operating Temperature Comparison
</b></p>

<p>How do operating temperature ranges compare for catalytic and direct flame
incinerators?<br>
(a) Catalytic incinerators operate at higher combustion temperatures<br>
(b) Direct flame incinerators operate at lower temperatures<br>
(c) Temperature requirements are identical<br>
(d) Catalytic incinerators operate at lower combustion temperatures<br>
Solution: Catalysts increase reaction rates. Since reaction rates increase with
temperature, catalytic incinerators can operate at lower temperatures with
similar pollutant destruction performance. The correct answer is therefore (d).</p>



<p><b>4.6 Effect of Fine Particulates on Catalyst Life

</b></p>


<p>In a catalytic combustion system, fine particulate matter in the process gas stream
will cause the lifetime of the catalyst to<br>
(a) Increase<br>
(b) Decrease<br>
(c) Remain unaffected<br>
(d) None of the above<br>
Solution: Fine particulates can become embedded in the pores of the catalyst,
reducing the active area. This then reduces the performance (and lifetime) of
the catalyst. The correct answer is therefore (b).</p>


<p><b>4.7 Lower Explosive Limit (LEL)
</b></p>

<p>An exhaust stream with a combustible concentration lower than the LEL will<br>
(a) Have an insufficient amount of oxygen to autoignite<br>
(b) Be a fire hazard<br>
(c) Have an insufficient amount of combustibles to autoignite<br>
(d) Meet the emission limitation for lead<br>
Solution: For a gas stream to be combustible, the concentration of the organic
must be above the LEL or below the UEL. Since the concentration is below
the LEL, the gas will not autoignite. The correct answer is therefore (c).</p>







