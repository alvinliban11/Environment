    <h2>LESSON 3.3 CHEMICAL AND PHYSICAL PROPERTIES</h2>
<p><b>Viscosity
</b></p>

<p>Viscosity is a property associated with a fluid’s resistance to flow; more precisely, this
property accounts for energy losses which result from shear stresses occurring
between different portions of the fluid that are moving at different velocities. The absolute viscosity (m) has units of mass per length . time; the fundamental unit is the poise,
which is defined as 1 g/cm . s. This unit is inconveniently large for many practical purposes and viscosities are frequently given in centipoises (0.01 poise), which is abbreviated cP. The viscosity of pure water at 68.68F is 1.00 cP. In English units, absolute
viscosity is expressed either as pounds (mass) per foot-second (lb/ft . s) or pounds
per foot-hour (lb/ft . hr). The absolute viscosity depends primarily on temperature
and to a lesser degree on pressure.</p>



<p>The kinematic viscosity (n) is the absolute viscosity divided by the density of the
fluid and is useful in certain fluid flow problems; the units for this quantity are length
squared per time, e.g., square foot per second (ft2
/s) or square meters per hour (m2
/
hr). A kinematic viscosity of 1 cm2
/s is called a stoke. For pure water at 708F, n ¼
0.983 cS (centistokes). Because fluid viscosity changes rapidly with temperature, a
numerical value of viscosity has no significance unless the temperature is specified.</p>



<p>Liquid viscosity is usually measured by the amount of time it takes for a given
volume of liquid to flow through an orifice. The Saybolt universal viscometer is the
most widely used device in the United States for the determination of the viscosity of
fuel oils and liquid wastes.
The viscosities of air at atmospheric pressure and water as functions of temperature
are presented in Tables 3.1 and 3.2 respectively. Viscosities of other substances are available in the literature.</p>



<p><b>Heat Capacity</b></p>
<p>The heat capacity of a substance is defined as the quantity of heat required to raise the
temperature of that substance by 18. The term specific heat is frequently used in place of
specific heat capacity. This is not strictly correct, because specific heat has been traditionally defined as the ratio of the heat capacity of a substance to the heat capacity</p>

