<p>of water. However, since the specific heat of water is approximately 1 cal/g . 8C or 1 Btu/
lb . 8F, the term specific heat is often interchanged with heat capacity. For gases, the
addition of heat to cause the 18 temperature rise may be accomplished either at constant
pressure or at constant volume. Since the amounts of heat necessary are different for the
two cases, subscripts are used to identify which heat capacity is being used—cp for constant pressure and cv for constant volume. For liquids and solids, this distinction does not
have to be made since there is little difference between the two. Values of heat capacities
are available in Chapter 4 and in the literature.</p>

