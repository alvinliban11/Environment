    <h2>LESSON 4.1 INTRODUCTION</h2><br>


<p>Most incinerators operate at higher temperatures than the ignition temperature, which is a
minimum temperature. Thermal destruction of most organic compounds occurs between
590 and 6508C (1100 and 12008F). However, most hazardous waste incinerators are
operated at 1800 to 22008F to ensure near-complete conversion of the waste.
</p>


<p class="indent">Time and temperature affect combustion in much the same manner as temperature and
pressure affect the volume of a gas. When one variable is increased, the other may be
decreased with the same end result. With a higher temperature, a shorter residence time
can achieve the same degree of oxidation. The reverse is also true; a higher residence
time allows the use of a lower temperature. In describing incinerator operation, these
two terms are always mentioned together. This effect is described in graphical form in
Figure 4.1. The choice between higher temperature or longer residence time is based on
economic considerations. Increasing residence time involves using a larger combustion
chamber resulting in a higher capital cost. Raising the operating temperature increases
fuel usage which also adds to the operating costs, and fuel costs are the major operating
expense for most incinerators. Within certain limits, lowering the temperature and adding
volume to increase residence time can be a cost-effective alternative method of operation.</p>

