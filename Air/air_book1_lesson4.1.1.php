<p class="indent">Proper mixing is important in combustion processes for two reasons: (1) for complete combustion to occur every part of the waste and fuel must come in contact with
air (oxygen)—if not, unreacted waste and fuel will be exhausted from the stack; and
(2) not all of the fuel or waste stream is able to be in direct contact with the burner
flame. In most incinerators, a portion of the waste stream may bypass the flame and
be mixed at some point downstream of the burner with the hot products of combustion.
If the two streams are not completely mixed, a portion of the waste stream will not react
at the required temperature and incomplete combustion will occur. A number of methods
are available to improve mixing the air and waste (combustion) streams. Some of these
include the use of refractory baffles, swirl fired burners, and baffle plates. The problem of
obtaining complete mixing is not easily solved. Unless properly designed, many of these
mixing devices may create “dead spots” and reduce operating performance. Merely
inserting obstructions to increase turbulence is often not the answer. The process of
mixing waste stream and the flame to obtain a uniform temperature for decomposition
of wastes is often the most difficult part in the design of the incinerator.</p>




<p class="indent">Oxygen is necessary for combustion to occur. To achieve complete combustion of a
compound, a sufficient supply of oxygen must be present to convert all of the carbon to
CO2. This quantity of oxygen is referred to as the stoichiometric or theoretical amount.
The stoichiometric amount of oxygen is determined from a balanced chemical equation
summarizing the oxidation reactions. If an insufficient amount of oxygen is supplied, the
mixture is referred to as “rich”; there is not enough oxygen to combine with all the fuel
and waste so that incomplete combustion occurs. If more than the stoichiometric amount
of oxygen is supplied, the mixture is referred to as “lean.” The added oxygen then plays
no part in the oxidation reaction and passes through the incinerator. Oxygen for a combustion process is almost always supplied by using air. Since air is essentially 79% nitrogen
and 21% oxygen by volume, a larger volume of air is required than if pure oxygen were
used. Stoichiometric calculations are treated in the “Problems” section of this chapter.</p>


<p class="indent">In most applications, more than the stoichiometric amount of air is used to ensure
complete combustion. This extra volume is referred to as excess air. If ideal mixing
were achievable, no excess air would be necessary. However, most combustion
devices are not capable of achieving ideal mixing of the fuel and air/waste streams.
The amount of excess air is sometimes held to a minimum in order to reduce heat
losses; excess air takes no part in the reaction but does absorb some of the heat produced.
To raise the excess air to the combustion temperature, additional fuel must be used to
make up for any loss of heat. Operating at a high volume flow rate of excess air can
thus be very costly in terms of the added fuel required.</p>




