    <h2>LESSON 4.1 INTRODUCTION</h2><br>


<p>From a macroscopic perspective, the following sequence of steps is involved in the
catalytic conversion of reactants to products:
</p>

<p class="indent">1. Transfer of reactants to and products from the outer catalyst surface
</p>
<p class="indent">2. Diffusion of reactants and products within the pore of the catalyst
</p>
<p class="indent">3. Active adsorption of reactants and the desorption of the products on the active
pore centers of the catalyst</p>
<p class="indent">4. Reaction(s) on active centers on the catalyst surface
</p>


<p>At the same time, energy effects arising due to chemical reaction can result in the
following:
</p>


<p class="indent">1. Heat transfer to or from active centers to the catalyst particle surface

</p>
<p class="indent">2. Heat transfer to and from reactants and products within the catalyst particle

</p>
<p class="indent">3. Heat transfer to and from moving streams in the reactor
</p>
<p class="indent">4. Heat transfer from one catalyst particle to another within the reactor

</p>
<p class="indent">5. Heat transfer to or from the walls of the reactor


</p>

<p>The basic problem in the design of a heterogeneous reactor, as in the case of a
catalytic combustion unit, is to determine the quantity of catalyst and/or reactor size
required for a given conversion and flow rate. In order to obtain this, information on
the rate equation(s) and their parameter(s) must be made available. A rigorous approach
to the evaluation of these reaction velocity constants has yet to be accomplished. At this
time, industry still relies on the procedures set forth earlier.</p>


<p class="indent">Although not treated in any detail in this text, flares provide another means of
control. If the concentration of the organics in air equals or exceeds the lower flammability limit, a flare unit may be employed. Elevated, ground-level, and open-pit flares may be used. However, ground-level flares have been outlawed in most states; open-pit
burning is used only under special conditions. Elevated flares are usually employed to
ensure sufficient dilution and subsequent dispersion from adjacent structures and potential receptors of the energy and end products of combustion. Flares find their primary
application in the petroleum and petrochemical industries. The suggested design procedures are available in the literature.
</p>


<p class="indent">In operation, the gas containing the organics is continuously fed to and discharged
from a stack, with the combustion occurring near the top of the stack and characterized
by a flame at the end of the stack. The discharge temperature is in the 1500–30008F
range. The combustion flame is located at a point where the velocity of flow equals the
velocity of flame propagation, provided the mixture is at or above the ignition temperature.
Good mixing and a H/C (hydrogen/carbon) ratio in excess of 0.3 in the process gas
stream can help insure proper combustion. A blue flame, appearing colorless against a
blue-sky background, indicates good operation; a yellow-orange flame with a trail of
black smoke indicates poor operation. The addition of steam ( jets) at the top of the
stack can help remove operational problems resulting from incomplete combustion.
Steam rates in the 0.1–0.5 lb steam per pound of process gas range are often employed.
Operating stack velocities are in excess of the flame propagation rate, and usually
in excess of 200ft/s. The design of this unit includes flame arresters in the stack to
help remove some of the flashback and explosion possibilities. The diameter of the
stack is obtained from flow (velocity) considerations and the height from atmospheric
dispersion calculations.
</p>
<p class="indent">In general, flare performance depends on such factors as flare gas exit velocity,
emission stream heating value, residence time in the combustion zone, waste gas/
oxygen mixing, and flame temperature. Steam-assisted smokeless flares are the most
frequently used. In these units, process off-gases enter the flare through the collection
header. When water or organic droplets are present, passing the off-gases through a
knockout drum may be necessary since these droplets can create problems; water droplets can extinguish the flame and organic droplets can result in burning particles. Once
the off-gases enter the flare stack, the aforementioned flame flashback can occur if the
emission stream flow rate is too low. Flashback may be prevented, however, by
passing the gas through a gas barrier, a water seal, or a stack seal. Purge gas is another
option. At the flare tip, the emission stream is ignited by pilot burners. If conditions in
the flame zone are optimum (oxygen availability, adequate residence time, etc.), the volatile organic compounds (VOCs) in the emission stream may be completely burned with
nearly 100% efficiency. In some cases, it may be necessary to add supplementary fuel
(natural gas) to the emission stream to achieve destruction efficiencies of 98% and
greater if the net heating value of the emission stream is less than 300 Btu/scf.
</p>





