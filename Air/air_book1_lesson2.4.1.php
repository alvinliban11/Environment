    <h2>LESSON 2.4: THE CLEAN AIR ACT</h2>



<p>The Clean Air Act addresses the urban air pollution problems of ozone (smog),
carbon monoxide (CO), and particulate matter (PM). Specifically, it clarifies how
areas are designated and redesignated “attainment.” It also allows the EPA to define
the boundaries of “nonattainment” areas—geographic areas whose air quality does
not meet federal air quality standards designed to protect public health. The law also
establishes provisions defining when and how the federal government can impose sanctions on areas of the country that have not met certain conditions.<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
The Clean Air Act established nonattainment area classifications ranked according
to the severity of the area’s air pollution problem for the pollutant ozone. These classifications are marginal, moderate, serious, severe, and extreme. The EPA assigns each
nonattainment areas one of these categories, thus triggering varying requirements that
area must comply with in order to meet the ozone standard.<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
As mentioned, nonattainment areas have to implement different control measures,
depending on their classification. Marginal areas, for example, are the closest to
meeting the standard. They are required to conduct an inventory of their ozonecausing emissions and institute a permit program. Nonattainment areas with more
serious air quality problems must implement various control measures. The worse the
air quality, the more controls these areas will have to implement.<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
The Clean Air Act also established similar programs for areas that do not meet the federal
health standards for carbon monoxide and particulate matter. Areas exceeding the standards
for theses pollutants are divided into “moderate” and “serious” classifications. Depending on
the degree to which they exceed the carbon monoxide standard, areas are then required to
implement programs introducing oxygenated fuels and/or enhanced emission inspection
programs, among other measures. Depending on their classification, areas exceeding the particulate matter standard have to implement either reasonably available control measures
(RACMs) or best available control measures (BACMs), among other requirements.<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
To summarize, the NAAQS represents a maximum concentration or “threshold
level” of a pollutant in the air above which humans or the environment may experience
some adverse effects. The actual threshold levels are based on years of epidemiological,
health, and environmental effects research conducted by the EPA. There are two types of
NAAQS: primary standards, which are set at levels that are designed to protect the public
health, and secondary standards, which are designed to protect the public welfare (such
as vegetation, livestock, building materials, and other elements in the environment). The
NAAQS also differentiate between effects from short-term exposure and longer-term
exposure to air pollutants; there are short-term NAAQS, based on 1-hr, 3-hr, 8-hr
averages, or 24-hr concentrations, and long-term NAAQS, based on quarterly or
annual concentrations.<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
If monitoring indicates that the concentration of a pollutant exceeds the NAAQS in
any area of the country, that area (as noted earlier) is labeled a nonattainment area for
that pollutant, meaning that the area is not meeting the ambient standard. Conversely,
any area in which the concentration of a criteria pollutant is below the NAAQS is
labeled an attainment area, indicating that the NAAQS is being met. The attainment/
nonattainment designation is made on a pollutant-by-pollutant basis. Therefore, the
air quality in an area of the country may be designated attainment for some pollutants
and nonattainment for other pollutants at the same time. For example, many cities are designated nonattainment for ozone, but are in attainment for the other criteria
pollutants. <br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The NAAQS provide target levels of concentrations of pollutants in the atmosphere,
but do not set pollutant emission limitations for individual pollution sources. There are
four key federal regulations that govern emissions from individual sources, each of
which is described below.</p>


<p class="indent">1. Prevention of significant deterioration (PSD) permitting program</p>
<p class="indent">2. Nonattainment new source review (NA-NSR) permitting program</p>
<p class="indent">3. New source performance standards (NSPS)</p>
<p class="indent">4. Maximum achievable control technology (MACT) for hazardous air pollutants
(HAPs)</p>






