</b></p>
<p>A flue gas [molecular weight (MW) = 30, dry basis] is being discharged from a
scrubber at 1808F (dry bulb) and 1258F (wet bulb). The gas flow rate on a dry
basis is 10,000 lb/hr. The absolute humidity at the dry-blub temperature of
1808F and wet-bulb temperature of 1258F is 0.0805 lb H2O/lb dry air.<br>
(a) What is the mass flow rate of the wet gas?<br>
(b) What is the actual volumetric flow rate of the wet gas?<br><br>
Solution: Curves showing the relative humidity (ratio of the mass of the water
vapor in the air to the maximum mass of the water vapor the air can hold at
that temperature, i.e., if the air were saturated) of humid air appear on the
psychrometric chart (see Figure 3.1). The curve for 100% relative humidity is
also referred to as the saturation curve. The abscissa of the humidity chart is
air temperature, also known as the dry-bulb temperature (TDB). As discussed
in Section 3.2, the wet-bulb temperature (TWB) is another measure of humidity;
it is the temperature at which a thermometer with a wet wick wrapped around the
bulb stabilizes. As water evaporates from the wick to the ambient air, the bulb is
cooled; the rate of cooling depends on how humid the air is. No evaporation
occurs if the air is saturated with water; hence, TWB and TDB are then the
same. The lower the humidity, the greater the difference between these two temperatures. On a psychrometric chart, constant wet-bulb temperature lines are</p>

