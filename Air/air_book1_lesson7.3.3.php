<p class="indent">Ordinarily, determining the settling velocity of a particle of known diameter would
require a trial-and-error calculation since the particle’s Reynolds number is unknown and
one cannot select the proper describing drag force equation. This iterative calculation can
be circumvented by rearrangement of the drag force equations and solving for the
settling velocity directly.
</p>


