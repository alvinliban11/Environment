<p class="indent">If a particle is initially at rest in a stationary gas and is then set in motion by the application of a constant external force or forces, the resulting motion occurs in two stages. The
first period involves acceleration, during which time the particle velocity increases from zero
to some maximum velocity. The second stage occurs when the particle achieves this
maximum velocity and remains constant. During the second stage, the particle is not accelerating. The LHS (left-hand side) of those above equations containing the term dv/dt are,
therefore, zero. The final, constant, and maximum velocity attained is defined as the terminal
settling velocity of the particle. Most particles can be shown to reach their terminal settling
velocity almost instantaneously (L. Theodore: personal notes, 1966).
<br>
Consider the equations examined above under terminal settling conditions. Since
</p>


