<p class="indent">Another important consideration involves the Cunningham correction factor, CCF
[C. E. Cunningham, Proc. Roy. Soc. London Sec. A 83: 357 (1910)]. At very low
values of the Reynolds number, when particles approach sizes comparable to the
mean free path of the fluid molecules, the medium can no longer be regarded as continuous. For this condition, particles can fall between the molecules at a faster rate than predicted by the aerodynamic theories that led to the previous standard drag coefficients. To
allow for this “slip,” Cunningham introduced a multiplying correction factor to Stokes’
law. This can alter the equations used to describe flow in the laminar regime. This effect
