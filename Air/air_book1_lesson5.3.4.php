<p><b>5.21 Packed versus Plate Column


</b></p>

<p>Briefly describe the differences between a packed column and a plate column.<br>
Solution:<br> Of the various types of gas absorption devices mentioned, packed
columns and plate columns are the most commonly used in industrial practice.
Although packed columns are used more often in air pollution control, both
have their special areas of usefulness, and the relative advantages and disadvantages of each are worth considering. In general:
</p>

<p>1. The pressure drop of the gas passing through the packed column is smaller.<br>
2. The plate column can treat an extremely low liquid feed and permits a higher
gas feed than the packed column. It can also be designed to handle liquid
rates that would ordinarily flood the packed column.<br>
3. If the liquid deposits a sediment, the plate column is more advisable. By
fitting the column with manholes, the plate column can be cleaned of accumulated sediment that would clog many packing materials and warrant
necessary costly removal and refilling of the column. Packed columns are
also susceptible to plugging if the gas contains particulate contaminants.<br>
4. In mass transfer processes accompanied by considerable heat effects,
cooling or heating the liquid is accomplished more easily in the plate
column. A system of pipes immersed in the liquid can be placed between
the plates and heat can be removed or supplied through the pipe wall
directly to the area in which the process is taking place. The solution of
the same problem for a packed column leads to the division of this
process into a number of sections, with the cooling or heating of the
liquid taking place between these sections.<br>
5. The total weight of the plate column is usually less than that for the packed
column designed for the same capacity.<br>
6. A well-installed plate column avoids serious channeling difficulties, ensuring
good, continuous contact between the gas and liquid throughout the column.<br>
7. In highly corrosive atmospheres, the packed column is simpler and cheaper
to construct.<br>
8. The liquid holdup in the packed column is considerably less than in the plate
column.<br>
9. Temperature changes are apt to do more damage to a packed column than to
a plate column.<br>
10. Plate columns are advantageous for absorption processes with an accompanying chemical reaction (particularly when it is not very rapid). The process
is favored by a long residence time of the liquid in the column and by easier
control of the reaction.<br>
11. Packed column are preferred for liquids with high foaming tendencies.<br>
12. The relative merits of the plate column and packed column for a specified
purpose are properly determined only by comparison of the actual cost
figures resulting from a detailed design analysis for each type. Most conditions being equal, packed columns in the smaller sizes (diameters up to
approximately 2–3 ft) are on the average less expensive. In the larger
sizes, plate columns tend to be the more economical.</p>

