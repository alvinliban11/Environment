 <h2>LESSON 6.3 OPERATION AND MAINTENANCE, AND IMPROVING
PERFORMANCE
</h2><br>





<p>Utilizing adsorption as an air pollution control measure on sources emitting volatile
organic hydrocarbons has proved to be extremely effective if a proper design procedure
is applied and rigid operating procedures are established and followed. In terms of operation and maintenances, it is always desirable to check over the process being controlled
to ensure that normal operation is being experienced. Of course, the blower must be
running with the fan turning in the correct direction. Three-phase motors that are
running backward may be corrected by changing two of the phase wires to the motor.
Initial startups will probably require a system balance of the exhaust ducts. If multiple
processes are being exhausted into the same system, adjustments of individual slide
dampers may be required to obtain the correct airflow in each branch duct. Airflow
switches are inexpensive and should be placed in each branch to sense airflow and
allow the process to operate when the airflow is adequate. Airflows below 100 ft/min
through the carbon beds will provide adequate retention time for solvents in the air
stream to be adsorbed on the activated carbon. Excessive flows will reduce carbon efficiencies and allow volatiles to escape into the atmosphere. Excessive airflows are also
detrimental to process operations where unnecessary solvents are evaporated and lost
from process tanks and delivered to the carbon beds, posing additional loads on the
system.</p>

<p>Additional details are available from L. Theodore, “Engineering Calculations:
Adsorber Sizing Made Easy,” Chem. Eng. Progress, pp. 16–17 (March 2005).
Optimizing performance of air pollution control equipment such as carbon adsorbers involves consideration and monitoring of the following factors:</p>

<p>1. Operation of process controls to minimize solvent emissions<br>
2. Quality of solvent/air inlet stream<br>
3. Characteristics of the inlet stream, such as concentration, temperature, and flow<br>
4. Duration of the adsorption cycle<br>
5. Quality and quantity of available steam for regeneration<br>
6. Duration of steam–stripping cycle<br>
7. Saturation and retentivity of the carbon<br>
8. Quantity and quality of cooling water<br>
9. Effectiveness of the water/solvent separator<br>
10. Quality of reclaimed solvent<br>
11. Quality of the wastewater<br>
12. Quality of the exhaust stream from the adsorber bed</p>


<p>More recent development with adsorbers has centered primarily on improvement of
adsorbents. Activity in the nanotechnology area has produced adsorbents such as carbon
nanotubes, which have outperformed adsorbents employed in the past. See text by Kunz
and Theodore (R. Kunz and L. Theodore, Nanotechnology: Environmental Implications
and Solutions, John Wiley & Sons, Hoboken, NJ, 2006) for additional details.</p>








