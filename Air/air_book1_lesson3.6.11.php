<p>This equation is based on adiabatic conditions, i.e., the entire heat load is transferred from the flowing gas. The unit is assumed to be perfectly insulated so that
no heat is transferred to the surroundings. However, this is not the case in a
real-world application. As with mass balances, an enthalpy balance may be performed within any properly defined boundary, whether real or imaginary. For
example, an enthalpy balance can be applied across the entire unit or process.
The enthalpy of the feed stream(s) is equated with the enthalpy of the product
stream(s) plus the heat loss from the process. All the enthalpy terms must be
based on the same reference temperature. Finally, the enthalpy has two key properties that should be kept in mind:<br><br>
1. Enthalpy is a point function, i.e., the enthalpy change from one state (say,
2008F, 1 atm) to another state (say 4008F, 1 atm) is a function only of the
two states and not the path of the process associated with the change.<br>
2. Absolute values of enthalpy are not important. The enthalpy of water at 608F,
1 atm, as recorded in some steam tables is 0 Btu/lbmol. This choice of zero is
arbitrary. However, another table may indicate a different value. Both are
correct! Note that changing the temperature of water from 60 to 1008F
results in the same change in enthalpy using either table.<br><br>
Enthalpy changes may be expressed with units (English) of Btu, Btu/lb,
Btu/lbmol, Btu/scf, or Btu/time depending on the available data and calculation
required.</p>



