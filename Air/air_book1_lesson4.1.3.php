    <h2>LESSON 4.1 INTRODUCTION</h2><br>


<p>The combustion process in the first stage is an extremely rapid and highly irreversible
chemical reaction. The oxygen supplied by the (primary) air may be in excess or obtained
directly from the process gas stream (secondary air). The carbon content of the fuel burns
almost quantitatively to carbon dioxide and the hydrogen to water. As noted above, combustion reactors should be operated with the least amount of excess air compatible with
the need for complete combustion and/or fuel requirements. The mixing of the air and the
fuel in the burner section of the reactor also determines the completeness of combustion.

</p>


<p class="indent">One can show that any fuel completely burned without excess air produces a
discharge gas containing the maximum CO2 possible for that fuel. Thus, with no
excess air and perfect mixing, combustion will be complete with the resulting gas containing maximum CO2 and little to no O2, CO, and H2. Since the reaction is extremely
rapid, the combustion of fuel occurs in a rather narrow zone in the reactor, perhaps
within a foot of reactor length at entrance conditions. The average residence time thus
approaches zero. The energy liberated on combustion of the fuel is then used to heat
the combustion temperature. Under these conditions, the rate of the combustion reaction
is dictated by the fuel rate; kinetics effects need not be considered. The calculations
reduce to one of stoichiometry, and overall mass and energy balances. Some of the procedures set forth in Chapter 3 can be applied. However, the mixing of the fuel combustion gases and the process gas stream does not occur instantaneously. A finite residence time (or reactor length) is required to achieve “complete” system mixing that will yield a
uniform temperature profile through the cross section of the reactor. This is a critical
design and operational consideration, for failure to achieve the above can result in
incomplete combustion, high(er) fuel requirements, or both. In fact, variations in performance can in many cases be either directly or indirectly attributed to this mixing
process. Care must be exercised in this part of the design and operation since flame
quenching can result if the mixing process is too severe. Once mixing is complete
and the temperature uniform, the so-called residence requirement is applicable to the
combustion of the pollutant in the process gas stream. Thus, the residence time specified
in the codes and regulations applies to both the mixing process, where the process gas
stream and fuel combustion products achieve a uniform temperature, and the combustion
process for the pollutant(s), which is assumed to occur at this elevated temperature.
</p>

<p class="indent">In the second stage of the process, the heated gases from the burner pass through the
reactor where the pollutants (organics) are reacted (oxidized) to harmless end products.
Although the pollutants also serve as a source of fuel, enthalpy of reaction (heat) effects
are often small enough to be neglected so that the calculation of conversion reduces to a
kinetic problem. However, the reaction mechanism is quite complex. It is difficult to
obtain and interpret experimental data for combustion reactions. In fact, it is common
practice to treat a mixture of hydrocarbons in terms of a single hydrocarbon component.
Since many chemical (combustion) reacting systems of interest to the environmental
engineer can be closely approximated by first-order or pseudo-first-order reactions
(L. Theodore: personal notes, 1976), industry usually employs a simple first-order,
irreversible reaction mechanism in the design calculation. (It is well known that even
zero- or second-order reactions can often be satisfactorily represented by first-order
reactions.) This is a reasonable assumption since the oxygen concentration can approach
a few parts per million. The reaction velocity constant is an empirically determined parameter that is applicable over the desired range of operating conditions. Although this
approach is displaced from earlier presentations, the procedure has merit and is
thoroughly justifiable if the resulting equation works.</p>


<p>Catalytic reactors are an alternative to thermal reactors. If a solid catalyst is added to
the reactor, the reaction is said to be heterogeneous. For simple reactions, the effect of
the presence of a catalyst is to
</p>


<p class="indent">1. Increase the rate of reaction
</p>
<p class="indent">2. Permit the reaction to occur at a lower temperature
</p>
<p class="indent">3. Permit the reaction to occur at a more favorable pressure
</p>
<p class="indent">4. Reduce the reactor volume
</p>
<p class="indent">5. Increase the conversion of a product(s) and/or yield of a reactant(s) relative to
the other(s)</p>

<p  >In a typical catalytic reactor, the gas stream is delivered to the reactor continuously by a
fan at velocity in the 10–30 ft/s range but at a lower temperature—usually in the 650–
8008F range—than a thermal unit. A length-to-diameter ratio less than 0.5 is usually
employed. The gases, which may or may not be preheated, pass through the catalyst bed where the reaction occurs. The combustion products, which are again made up of
water vapor, carbon dioxide, inerts, and unreacted vapors, are continuously discharged
from the outlet at a higher temperature. Energy savings can again be achieved with heat
recovery from the exit stream.</p>


<p class="indent">Metals in the platinum family are recognized for their ability to promote combustion
at low temperatures. Other catalysts include various oxides of copper, chromium,
vanadium, nickel, and cobalt. These catalysts are subject to poisoning, particularly
from halogens, halogen and sulfur compounds, zinc, arsenic, lead, mercury, and particulates. High temperatures can also reduce catalyst activity. It is therefore important that
catalyst surfaces be clean and active to ensure optimum performance. Catalysts can
usually be regenerated with superheated steam.</p>




<p class="indent">Catalysts may be porous pellets, usually cylindrical or spherical in shape, ranging
from 1
16 to 1
2 inch in diameter. Small sizes are recommended, but the pressure drop
through the reactor increases. Other shapes include honeycombs, ribbons, wire mesh,
etc. Since catalysis is a surface phenomena, a physical property of these particles is
that the internal pore surface is near infinitely greater than the outside surface. The
reader is referred to the literature for more information on catalyst preparation, properties,
comparisons, costs, and impurities.</p>





