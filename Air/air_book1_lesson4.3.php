    <h2>LESSON 4.3 OPERATION AND MAINTENANCE, AND
IMPROVING PERFORMANCE
</h2><br>


<p>Normal operation of an incinerator should be quite simple. A controller should be
incorporated into the design to maintain the outlet temperature at a fixed value by
varying the auxiliary fuel input. Combustion air (assuming limited air in the fume
stream) is usually controlled by the average amount of fumes to be incinerated. This
adjustment is normally set manually if the fume flow rate and fume heat content are
fairly constant. If the fume heat content or flow rate are likely to be highly variable, a
more sophisticated control system may be appropriate—one that analyzes combustion efficiency at the outlet as well as fume flow and outlet temperature, and varies both
auxiliary fuel input and combustion air accordingly. But, normal operation of even a
complicated control system usually requires nothing more than an occasional monitoring
of the instruments.
</p>



<p class="indent">Most incinerators are custom-designed within certain basic parameters. Therefore,
they are likely to be accompanied by a very complete instruction manual that should
include the manufacturer’s basic maintenance instructions from all the subsuppliers,
i.e., the incinerator manufacturer will have purchased equipment, such as the refractory,
valves, and controls, from other suppliers. The operating and maintenance instructions
for this equipment will be quite extensive and complete because it has been written
by the original manufacturer, who is concerned only with particular items. The instruction manual is therefore a very useful document from a maintenance viewpoint and
should be followed explicitly. (The instruction manual may not be so useful from a
system operations viewpoint because fume incinerator systems are usually customdesigned and system problems cannot always be anticipated, which often results in
some field modifications to the operating instructions.)</p>



<p class="indent">There are some general maintenance guidelines that can be discussed. The refractory should be inspected on a regular basis. Cracks may develop, especially in brick
joints, and thermal shock damage (spalling) should be repaired with a suitable plastic
of the same thermal properties. The burner should be inspected at regular intervals for
signs of warpage or corrosion. Moving parts should be lubricated with graphite or a
similar high-temperature lubricant. Lubricants that carbonize should not be used
under any circumstances. Also dirt, mortar, carbon, or other foreign matter should be
cleaned from the burner area. The pressure seals around any parts projecting through
the burner or incinerator shell should be inspected. Usually, these are asbestos rope
packing glands and should be fairly tight after the adjusting/retaining screws have been
loosened. These seals should only be lubricated with flake or powder graphite. This
should never be mixed with oil, as the oil will carbonize. The gas jets should be free of
corrosion and should be cleared of any deposits on burners using gas as an auxiliary fuel.</p>

<p class="indent">The outer shell of the incinerator should be inspected, especially when a new lining
has been installed, for signs of thermal shock, i.e., welds, especially at the outlet, should
be checked for hairline cracks, which are the first signs of poor thermal design</p>

<p class="indent">The auxiliary fuel piping train should be inspected in accordance with the
manufacturer’s instructions. Electrically operated valves and interlock switches should
also be inspected frequently for conditions that might cause “shorting” (short-circuiting),
e.g., dirty contacts, moisture leaks, deteriorating insulation, etc. Air supply lines and
filters (to air-operated valves) should be inspected for dirt or blockages. The valves
themselves are usually provided with air signal and air supply pressure gauges, and
these should be checked occasionally for accuracy. If there are shutoff dampers in the
ductwork to or from the incinerator, their seals should be checked frequently.</p>

<p class="indent">Maintenance procedures for catalytic incinerators should include catalyst cleaning
every 3 months to a year. Cleaning is usually accomplished by blowing clean compressed air through the catalyst element, by vacuuming, or by washing with water or
a mild detergent not containing phosphates. Iron oxide deposits can be removed by
soaking with a mild organic acid followed by a water rinse.</p>
<p class="indent">Improving the performance of a thermal incinerator basically involves the
optimization of fume combustion. Ideally, no more combustion air should be used
than is required for complete combustion of the fumes and the auxiliary fuel. The
auxiliary fuel should be used only in amounts required to maintain the design
furnace temperature.</p>
<p class="indent">An incinerator operating efficiently should normally have only 1–2% O2 and 0–1%
combustibles in the outlet gases. Monitors are available that can indicate these parameters
to the operator as well as provide automatic control of the incinerator when required.</p>
<p class="indent">The following questions often require answers. If odors are involved, has their concentration been reduced or eliminated? Has there been adequate reduction in the emissions of all
types of reactive hydrocarbons? Has there been a reduction in any possible explosion or
public health hazard that might exist in the manufacturing process? If so, the air pollution
control system has been well designed and is being operated and maintained properly.
Recent developments with combustion equipment has centered primarily on the
development of improved catalysts. Activity in the nanotechnology area has produced
catalysts that have outperformed catalysts employed in the past. See text by Kunz and
Theodore (R. Kunz and L. Theodore, Nanotechnology: Environmental Implications
and Solutions, John Wiley & Sons, Hoboken, NJ, 2006) for additional details</p>





