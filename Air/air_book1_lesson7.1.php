<h2>LESSON 7.1 INTRODUCTION
</h2>



<p>The author has employed several definitions for particulates: a small, discrete mass of
solid or liquid matter; a fine liquid or a solid particle that is found in the air or emissions;
any solid or liquid matter that is dispersed in a gas; or, insoluble solid matter dispersed in
a liquid so as to produce a heterogeneous mixture. Further, particulate matter 10 (PM10)
is defined as particulate matter with a diameter less than or equal to 10 micrometers (mm)
while particulate matter 2.5 (PM2.5) is particulate matter with a diameter less than or
equal to 2.5 micrometers (mm).

</p>

<p>Particulates may be classified into two broad categories: (1) natural and (2) humanmade (engineered or synthetic). Natural sources include (but are not limited to):
</p>

<p class="indent">1. Windblown dust</p>
<p class="indent">2. Volcanic ash and gases</p>
<p class="indent">2. Volcanic ash and gases</p>
<p class="indent">4. Pollens and other aeroallergens</p>

<p>These sources contribute to background values over which control activities can have
little, if any, effect. Human-made sources cover a wide spectrum of chemical and physical activities and are contributors to urban air pollution. Particulates in the United States
pour out from over 200 million vehicles, from the refuse of nearly 300 million people,
the generation of billions of kilowatts of electricity, and the production of innumerable
products demanded by everyday living.<br>
Particulates may also be classified by their origin.</p>

<p class="indent">1. Primary—emitted directly from a process</p>
<p class="indent">2. Secondary—subsequently formed as a result of a chemical reaction</p>

<p>Particulates may be classified as solid or liquid matter whose effective diameter (to
be defined shortly) is larger than a molecule but smaller than approximately 1000 mm.
Particulates dispersed in a gaseous medium are collectively termed an aerosol. The
terms “smoke,” “fog,” “haze,” and “dust” are commonly used to describe particular
types of aerosols, depending on the size, shape, and characteristic behavior of the
dispersed particles. Aerosols are rather difficult to classify on a scientific basis in
terms of their fundamental properties such as settling rate under the influence of external
forces, optical activity, ability to absorb electric charge, particle size and structure,
surface-to-volume ratio, reaction activity, physiological action, and so on. In general,
particle size and settling rate have been the most characteristic properties emphasized
by engineers and scientists. For example, particles larger than 100 mm may be excluded
from the category of dispersions because they settle too rapidly. On the other hand,
particles on the order of 1 mm or less settle so slowly that, for all practical purposes,
they are regarded as permanent suspensions. Despite possible advantages of scientific
classification schemes, the use of popular descriptive terms such as smoke, dust, and
mist, which are traditionally and essentially based on the mode of formation, appears
to be a satisfactory and convenient method of classification. In addition, this
approach is so well established and understood that it undoubtedly would be difficult
to change</p>

<p>When a liquid or solid substance is emitted to the air as particulate matter, its properties and effects may be changed. As a substance is broken up into smaller and smaller
particles, more of its surface area is exposed to the air. Under these circumstances, the
substance—whatever its chemical composition—tends to physically or chemically
combine with other particulates or gases in the atmosphere. The resulting combinations
are frequently unpredictable. Very small aerosol particles (measuring 0.001–0.1 mm, or 1–100 nm) can act as condensation nuclei to facilitate the condensation of water vapor,
thus promoting the formation of fog and ground mist. Particles less than 2 or 3 mm in
size—about half (by weight) of the particles suspended in urban air—can penetrate
into the mucus, and attract and convey harmful chemicals such as sulfur dioxide. By
virtue of the increased surface area of the small aerosol particles, and as a result of
the adsorption of gas molecules or other such properties that are able to facilitate chemical reactions, aerosols tend to exhibit greatly enhanced surface activity, a phenomenon
well documented in the nanotechnology literature.
</p>
<p>The range of sizes of particles formed in a process is largely dependent on the types
of the particle formation mechanisms present. It is sometimes possible to estimate the
general size range simply by recognizing which of these are important in the process
being evaluated. The most important particle formation mechanisms in air pollution
sources include the following.</p>


<p class="indent">1. Physical attrition/mechanical dispersion
</p>
<p class="indent">2. Combustion particle burnout</p>
<p class="indent">3. Homogeneous nucleation</p>
<p class="indent">4. Heterogeneous nucleation</p>
<p class="indent">5. Droplet evaporation</p>

<p>Particle size is the single most important characteristic that affects the behavior of a
particle. The range in sizes of particles observed in practice is remarkable. Some of the
droplets collected in the demisters of wet scrubbers and the solid particles collected
in large-diameter cyclones are as large as raindrops. However, some of the particles
created in high-temperature incinerators and metallurgical processes can consist of a
few molecules clustered together. These particles cannot be seen by sensitive light
microscopes because they are extremely small in size; these sizes approach those of individual gas molecules (which range from 0.2 to 1.0 nm). In fact, particles composed of a
few molecules clustered together can exist in a stable form. Some of these industrial processes generate particles in the range of 10–100 nm. However, particles in this size
range can grow and agglomerate to yield particles in the 100þ-nm range.</p>

