<h3>PROBLEM</h3>


<p><b>6.1 Principal Adsorbent
</b></p>

<p>The adsorbent most often used to control organic vapors is (select one)<br>
(a) Molecular sieves<br>
(b) Activated carbon<br>
(c) Silica gel<br>
(d) Zeolites<br>
Solution: All four have been used for adsorption purposes, with (d) being a class
of (a). However, it is activated carbon that is almost always used for organics.
The correct answer is therefore (b).</p>


<p><b>6.2 Commercial Adsorbent
</b></p>

<p>Which of the following materials is not a commercial adsorbent?<br>
(a) Silica gel<br>
(b) Activated carbon<br>
(c) Methane<br>
(d) Molecular sieve<br>
Solution: Answers (a), (b), and (d) are commercial adsorbents. The correct
answer is therefore (c).</p>

<p><b>6.3 Adsorption Process
</b></p>

<p>The adsorption process is almost always<br>
(a) Exothermic<br>
(b) Endothermic<br>
(c) Neutral<br>
(d) Hydroscopic<br>
Solution: The process is always exothermic since energy is liberated on
adsorption during either physical or chemical adsorption. The correct answer
is therefore (a).</p>

<p><b>6.4 Effect of Temperature

</b></p>

<p>During gas adsorption, as the temperature of the system increases, the amount of
material adsorbed<br>
(a) Increases<br>
(b) Decreases<br>
(c) Remains constant<br>
(d) Is doubled<br>
Solution: Increasing the temperature during the adsorption process displaces the
adsorbate from the adsorbent. Therefore, the amount adsorbed decreases. The
correct answer is therefore (b).</p>

<p><b>6.5 Increasing the Amount Adsorbed
</b></p>

<p>For adsorption processes, the amount of pollutant retained by the collecting
media increases as<br>
(a) The temperature of the system increases<br>
(b) The liquid-to-gas flow ratio is increased<br>
(c) The molecular weight of the pollutant decreases<br>
(d) The pressure of the system increases<br>
Solution: During the regenerative step, the pressure is often decreased to remove
(displace) the pollutant from the collecting media. The opposite is true when the
pressure is increased. The correct answer is therefore (d).</p>


<p><b>6.6 Chemical Versus Physical Adsorption

</b></p>

<p>When compared to the heat of adsorption for physical adsorption, that for chemical adsorption is<br>
(a) Always greater<br>
(b) Always less<br>
(c) About equal<br>
(d) Dependent on the carbon pore size<br>
Solution: The heat liberated during physical adsorption usually may be assumed
to be approximately equal to the enthalpy (heat) of condensation. The heat liberated during chemical adsorption usually may be assumed to be approximately
equal to the enthalpy (heat) of reaction. Further, the heat of reaction is usually
several orders of magnitude greater than the heat of condensation. The correct
answer is therefore (a).</p>


