<p class="indent">For larger flow rates, horizontal flow adsorbers are used. Structurally, they are more
suited to handle larger gas volumes. In horizontal flow units, the bed length is horizontal
as is the direction of the incoming stream. The stream flows across the bed and then
down through the bed. This system is illustrated in Figure 6.5. Adsorbers of this type
are manufactured as a package system capable of handling flow rates of up to 10,000
cfm. Larger units must be engineered and fabricated for a specific application.
</p>




<p class="indent">With a fixed-bed adsorber, it is necessary to periodically remove an adsorber from
service for regeneration, drying, and cooling. This process can be done automatically if
the bed is arranged to move in a prescribed way. Adsorbers where the bed moves from
the polluted fluid to the regeneration fluid to the drying bed and cooling fluid are called
moving-bed adsorbers. Although there are several possible arrangements, the most
common type of moving-bed adsorber has a cylindrical bed that slowly rotates about
its axis with an angular velocity while the frame and partitions are stationary. There
are three sections of this moving-bed adsorber: an adsorption section, a regeneration
section, and a drying–cooling section. As the bed revolves past the radial partitions,
an element of the bed will pass from the adsorption section to the regeneration
section, another element will pass from the regeneration section to the drying and
cooling section, and a third element will pass into the adsorption section. In this way,
the bed becomes continuously saturated, regenerated, and then dried and cooled in preparation for another adsorption cycle.
A fluidized-bed adsorption system operates in the same manner as a tray tower
absorber (see Chapter 5). Instead of liquid flowing down the column from tray to</p>
