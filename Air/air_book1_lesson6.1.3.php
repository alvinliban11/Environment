 <h2>LESSON 6.1 INTRODUCTION</h2><br>





<p> After a predetermined length of time, referred to as the cycle time, the solvent-laden
air stream is directed to the second adsorber by a series of valves. Often, steam is then
injected into the first bed to remove the adsorbed vapors. The steam and desorbed vapors
are then usually sent to a recovery system. If the solvents are immiscible in water, they
can be separated by condensing the exhaust and decanting off the solvent. If the solvents
are miscible in water, distillation may be required.</p>


<p class="indent">Before the first adsorber is returned to service, cooling and drying of the carbon is
usually provided. This will ensure against immediate breakthrough occurring from the
“hot, wet” carbon bed. This can be accomplished by venting the solvent-laden air
stream through the hot, wet adsorber, and then to the on-line adsorber to maintain a
high removal efficiency.</p>


<p class="indent">Regenerable fixed carbon beds are usually between 1 and 6 ft (0.3 and 1.8 m)
thick. The maximum adsorbent depth is primarily based on pressure drop considerations.
Superficial gas velocities through the adsorber range from 20 to 100 ft/min (6.0–
30.0 m/min); the maximum permissible velocity is approximately 90 ft/min. Pressure
drops normally range from 3 to 15 in H2O (from 750 to 3730 Pa) depending on the
gas velocity, bed depth, and carbon particle size.<br>
