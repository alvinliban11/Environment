<p class="indent">tray, granular activated carbon is used. The solvent-laden air stream is introduced at the
middle of the tower. Then it passes up through the tower, fluidizing the activated carbon
in a series of trays. The carbon then flows down through a vessel from tray to tray until it
reaches the desorption section. Regeneration is accomplished in the bottom half of the
vessel and the activated carbon is air-conveyed back to the top of the tower. As with
the moving bed, the fluidized bed also provides continuous operation and more efficient
utilization of the adsorbent. The need for multiple vessels is eliminated, which can
greatly reduce the cost of the system. Gas velocities around 200 ft/min (1 m/s) are
needed to fluidize the bed. This allows for use of a much smaller vessel for comparable
airflow and helps to achieve uniform gas distribution. The main disadvantage with
fluidized-bed adsorption systems is the high attrition (wear) losses of the granular
activated carbon. There are also operational problems, as with virtually all systems
that involve the movement of solids.</p>




