 <h2>7.4 PARTICLE SIZING AND MEASUREMENT METHODS




</h2><br>




<p>Unfortunately, the term “measurement methods” has come to mean different things to
engineers and scientists in the instrumentation field. Traditionally, this term has referred
to sampling techniques employed for source characterization purposes. Source characterization includes either the determination of particle size distribution, concentration
and flow rate, or chemical composition and other properties, or both. The equipment
and procedures employed to measure particle data in a stream (usually air) represents
the sampling part of the overall procedure. The analysis of the data is also important,
usually providing the aforementioned information on particle size distribution,
concentration, and flow rate.</p>

<p class="indent">An accurate quantitative analysis of the discharge of particulates from a process
must be determined prior to the design and/or selection of recovery/control equipment.
If the unit is properly engineered, utilizing the emission data as input to the device, most
particulates can be successfully controlled by one or a combination of the methods to be
discussed later in this book.</p>




<p class="indent">There are many techniques available for measuring the particle size distribution of
particulates. The wide size range covered, from nanometers to millimeters, cannot
always be analyzed using a single measurement principle. Added to this are the usual
constraints of capital costs versus operating costs, speed of operation, degree of skill
required, and most important, the end-use requirement.
</p>




<p class="indent">Physical sizing is traditionally one of the most common methods available for
classifying (or sizing) particles. This has been achieved by dry or wet screening with
sieves, microscopic analysis, electric-grating techniques, and light-scattering methods.
More realistic determinations of particle behavior in any environment must consider
the size, shape, and density. The technique best able to accomplish this is aerodynamic
sizing. Only by knowing the aerodynamic size of particles is it possible to determine
how they will behave in an air stream and the kind(s) of control equipment required
to capture them
</p>

<p class="indent">Consider, for example, a ping-pong ball and a golf ball. On close examination they
will appear almost equal in size; however, if both were tossed into a moving air stream,
they would behave quite differently. Even though the size and shape are similar, the
density is quite different, and the behavior of the two objects is far from being similar.</p>

<p class="indent">Since the density is quite different, the behavior of the objects is far from being similar
aerodynamically. This is the primary fallacy in physical sizing.
A common method of specifying large particle sizes is to designate the screen mesh
that has an aperture corresponding to the particle diameter. Since various screen scales
are in use, confusion may result unless the screen scale involved is specified. The screen
mesh generally refers to the number of screen openings per unit of length or area. The
aperture for a given mesh will depend on the wire employed. The Tyler and the US
Standard Screen Scales (Table 7.2) are the most widely used in the United States.
The screens are generally constructed of wire mesh cloth, with the diameters of
the wire and the spacing of the wires being specified. These screens form the bottoms
of metal pans about 8 inches in diameter and 2 inches high, whose sides are so fashioned
that the bottom of one sieve nests snugly on the top of the next.</p>


<p class="indent">The clear space between the individual wires of the screen is termed the screen
aperture. As indicated above, the term mesh is applied to the number of apertures per
linear inch; for example, a 10-mesh screen will have 10 openings per inch, and the
aperture will be 0.1 inch minus the diameter of the wire</p>

<p class="indent">Particle size itself is difficult to define in terms that accurately represent the types of
particles of interest. This difficulty stems from the fact that particles exist in a wide
variety of shapes, not just spheres. In the case of spherical particles, the definition of particle size is easy: it is simply the diameter. For the irregularly shaped particles, there are a
variety of ways to define the size. The most convenient of these is to directly relate it to
how the particle behaves in a fluid such as air, and the particle size definition that is most</p>


