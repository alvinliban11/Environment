</b></p>

<p>Consider the adsorber system in Figure 6.12. It is designed to operate with a
maximum discharge concentration of 50 ppm. Once the unit is installed and
running, it operates with a discharge of 60 ppm. Rather than purchase a new
unit, what options are available to bring the unit into compliance with the specified design concentration?<br>
Solution: This is the first of two open-ended problems that close out this chapter.
In a very real sense this is a takeoff of a similar problem in Chapter 5
(“Absorbers”). The following suggestions and options (if possible) are
recommended for adsorbers:<br><br>
1. Reduce the inlet temperature.<br>
2. Increase the system pressure.<br>
3. Change to finer-mesh carbon with a higher CAP, but check the effects on
both the pressure drop and the fan.<br>
4. Increase the amount of carbon in the bed, but check the effects on both the
pressure drop and fan.<br>
5. Employ a higher-temperature steam to reduce the HEEL.<br>
6. Make sure that the top of the bed is level.<br>
7. Process modification: reduce gas flow rate.<br>
8. Process modification: reduce inlet concentration of pollutant.</p>
