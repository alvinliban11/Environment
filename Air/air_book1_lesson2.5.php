<p>The Clean Air Act contains provisions for a broad array of authorities to make the law
readily enforceable. EPA has authority to
<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1. Issue administrative penalty orders up to $200,000 and field citations up to
$5000
<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2. Obtain civil judicial penalties
<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;3. Secure criminal penalties for knowing violations and for knowing and negligent
endangerment
<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;4. Require sources to certify compliance
<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;5. Issue administrative subpoenas for compliance data
<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;6. Issue compliance orders with compliance schedules of up to one year<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
Citizen suit provisions are also included to allow citizens to seek penalties against violators, with penalties going to a United States Treasury fund for use by the EPA for compliance and enforcement activities.

The following EPA actions represent recent regulations promulgated to implement
the requirements of the Clean Air Act:
<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1. Clean Air Interstate Rule published on May 12, 2005 (70 FR 25161) amends
requirements for State Implementation Plans and for provisions for Acid Rain
Program.
<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2. Mercury Rules published on May 18, 2005 (20 FR 28605) amends New Source
Performance Standards for electric utility steam generating units and some provisions of the Acid Rain Program.
<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;3. Non-road Diesel Rule published on May 11, 2004 (69 FR 38957) amends provisions for mobile sources and for highway vehicles and engines.
<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;4. Ozone Rules identified those areas that are designated as not attaining the
ambient air quality standards for ozone.
<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;5. Fine Particle Rules identified those areas that are designated as not attaining the
ambient air quality standards for particulate matter.</p>





