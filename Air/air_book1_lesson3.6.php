    <h2>LESSON 3.6 CONSERVATION LAWS</h2>

<p><b>Conservation of Mass
</b></p>

<p>The conservation law for mass can be applied to any process or system. The general form
of this law is given in Equation (3.34):</p>

<img src="img/mass.png">

<p>Equation (3.34) may be applied to the total mass involved or to a particular species, on
either a mole or mass basis. The conservation law for mass can be applied to steady-state
or unsteady-state processes and to batch or continuous systems. To isolate a system for
study, one separates it from the surroundings by a boundary or envelope. This boundary
may be real (e.g., the walls of a vessel) or imaginary. Mass crossing the boundary and
entering the system is part of the mass-in term in Equation (3.34), whereas that leaving
the system is part of the mass-out term. Equation (3.34) may be written for any compound whose quantity is not changed by chemical reaction, or for any chemical
element, regardless of whether it has participated in a chemical reaction. It may be
written for one piece of equipment, around several pieces of equipment, or around an
entire process. It may be used to calculate an unknown quantity directly, to check the validity of experimental data, or to express one or more of the independent relationships
among the unknown quantities in a particular problem.</p>



<p>A steady-state process is one in which there is no change in conditions (temperature,
pressure, etc.) or rates of flow with time at any given point in the system. The accumulation term in Equation (3.34) is then zero. If there is no chemical reaction, the generation
term is also zero. All other processes are classified as unsteady-state. In a batch process,
the container holds the product or products. In a continuous process, reactants are fed in
an unending flow to a piece of equipment or to several pieces in series, and products are
continuously removed from one or more points. A continuous process may or may not be
steady-state. As indicated previously, Equation (3.34) may be applied to the total mass of
each stream (referred to as an overall or total material balance) or to the individual components of the streams (referred to as componential or component material balance).
Often, the primary task in preparing a material balance is to develop the quantitative
relationships among the streams.</p>

<p><b>Conservation of Energy</b></p>

<p>A presentation of the conservation law for energy would be incomplete without a brief
review of some introductory thermodynamic principles. Thermodynamics is defined as
the science that deals with the relationships between the various forms of energy.</p>

<p>A system may possess energy as a result of its temperature, velocity, position, molecular structure, surface, and so on. The energies corresponding to these conditions are
internal, kinetic, potential, chemical, surface, and so on, respectively. Engineering thermodynamics is founded on three basic laws. Energy, like mass, is conserved.
Application of the conservation law for energy gives rise to the first law of thermodynamics. (The reader is directed to the literature for details on the second and third law.)
