<p><b>Partial Pressure and Partial Volume</b></p>


<p>In engineering practice, mixtures of gases are more often encountered than single or pure
gases. The ideal gas law is based on the number of molecules present in the gas volume;
the kind of molecules is not a significant factor, only the number. This law applies
equally well to mixtures and pure gases alike. Dalton and Amagat both applied the
ideal gas law to mixtures of gases. Since pressure is caused by gas molecules colliding
with the walls of the container, it seems reasonable that the total pressure of a gas mixture is made up of pressure contributions due to each of the component gases. These
pressure contributions are called partial pressures. Dalton defined the partial pressure of
a component as the pressure that would be exerted if the same mass of the component
gas occupied the same total volume alone at the same temperature as the mixture. The
sum of these partial pressures equals the total pressure:</p>

