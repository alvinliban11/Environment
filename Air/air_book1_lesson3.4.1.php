<p>Volumetric flow rates are often given not at the actual conditions of pressure and
temperature but at arbitrarily chosen standard conditions (STP, standard temperature
and pressure). To distinguish between flow rates based on the two conditions, the
letters a and s are often used as part of the unit. The units acfm and scfm stand for
actual cubic feet per minute and standard cubic feet per minute, respectively. The
ideal gas law can be used to convert from standard to actual conditions, but since
there are many standard conditions in use, the STP being employed must be known
or specified. Standard conditions most often used are shown in Table 3.4.</p>

<p>The reader is cautioned on the incorrect use of acfm and/or scfm. The use of standard conditions is a convenience; when predicting the performance of or designing
equipment, the actual conditions must be employed. Designs based on standard conditions can lead to disastrous results, with the unit usually underdesigned. For
