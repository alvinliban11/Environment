 <h2>LESSON 6.2 DESIGN AND PERFORMANCE EQUATIONS
</h2><br>





<p> Most available data on adsorption systems are determined at equilibrium conditions.
Adsorption equilibrium is the set of conditions at which the number of molecules arriving on the surface of the adsorbent equals the number of molecules that are leaving. The
adsorbent bed is then said to be “saturated with vapors” and can remove no more vapors
from the exhaust stream. Thus, equilibrium determines the maximum amount of vapor
that may be adsorbed at a given set of operating conditions. Although a number of variables affect adsorption, the two most important ones in determining equilibrium for a
given system are temperature and pressure. Three types of equilibrium graphs are
used to describe adsorption systems: isotherm at constant temperature, isobar at constant
pressure, and isostere at constant amount of vapors adsorbed.</p>


<p class="indent">The most common and useful adsorption equilibrium data is the adsorption isotherm. The isotherm is a plot of the adsorbent capacity vs. the partial pressure of the
adsorbate at a constant temperature, T. Adsorbent capacity is usually given in weight
percent expressed as grams of adsorbate per 100 g of adsorbent. Figure 6.6 shows a
typical example of an adsorption isotherm for carbon tetrachloride on activated
carbon. Graphs of this type are used to estimate the size of adsorption systems, as
described later. Attempts have been made to develop generalized equations that can
predict adsorption equilibrium from physical data. This is very difficult because adsorption isotherms take many shapes depending on the forces involved. Isotherms may be
concave upward, concave downward, or “S”-shaped. To date, most of the theories
agree with data only for specific adsorbate systems and are valid over limited concentration ranges.</p>


<p class="indent">Two additional adsorption equilibrium relationships are the isostere and the
isobar. The isostere is a plot of the natural log of the partial pressure p vs. 1/T at a constant amount of vapor adsorbed. Adsorption isostere lines are usually straight for most
adsorbate–adsorbent systems. The isostere is important in that the slope of the isostere
(approximately) corresponds to the heat of adsorption. The isobar is a plot of the amount
of vapors adsorbed vs. T at a constant partial pressure. However, in the design of a</p>

<img src="img/as6.png">


<p class="indent">pollution control system, the adsorption isotherm is by far the most commonly used
equilibrium relationship.
The movement of vapors through an adsorbent bed is often referred to as a dynamic
process. The term dynamic refers to motion, both in the movement of gas through the
adsorbent bed and the change in vapor concentration(s) as it moves through the bed.
There are a variety of configurations in which the contaminant air stream and adsorbent
are brought into contact. The most common configuration is to pass the air stream down
through a fixed volume or bed of adsorbent. Figure 6.7 illustrates how adsorption (mass
transfer) occurs as vapors pass down through the bed.
</p>

<img src="img/as7.png">


<p class="indent">The gas stream containing the pollutant at an initial concentration C0 is passed down
through a (deep) bed of adsorbent material that is free of any contaminant. Most of the
contaminant is readily adsorbed by the top portion of the bed. The small amount of contaminant that is left is easily adsorbed in the remaining section of the bed. The effluent
from the bottom of the bed is essentially pollutant-free, denoted at C1.</p>

<p class="indent">After a period of time, the top layer of the adsorbent bed becomes saturated with
contaminant. The majority of adsorption (approximately 95%) now occurs in a
narrow portion of the bed directly below this saturation section. The narrow zone of
adsorption is referred to as the mass transfer zone (MTZ). As additional contaminant
vapors pass through the bed, the saturated section of the bed becomes larger and the
MTZ moves further down the length of the adsorbent. The actual length of the MTZ
remains fairly constant as it travels through the adsorbent bed. Additional absorption
occurs as the vapors pass through the “unused” portion of the bed. The effluent concentration at C2 is essentially still zero, since there is still an unsaturated section of the bed.</p>

<p class="indent">Finally, when the lower portion of the MTZ reaches the bottom of the bed, the
concentration of contaminant in the effluent suddenly begins to rise. This is referred
to as the breakthrough point (or breakpoint)—where untreated vapors are being
exhausted from the adsorber. If the contaminated air stream is not switched to a fresh
bed, the concentration of contaminant in the outlet will quickly rise until it approaches
the initial concentration, illustrated at point C4. It should be noted that in most air
pollution control systems even trace amounts of contaminants in the effluent stream
are undesirable.</p>

<p class="indent"> To achieve continuous operation, adsorbers must be either replaced or cycled from
adsorption to desorption before breakthrough occurs. In desorption or regeneration, the
contaminant vapors are removed from the used bed in preparation for the next cycle.
Most commercial adsorption systems are the regenerable type.
In regard to regenerable adsorption systems, three important terms are used to
describe the equilibrium capacity (CAP) of the adsorbent bed. Other capacity terms
are measured in mass of vapor per mass of adsorbent. First, the breakthrough capacity
(BRK) is defined as the capacity of the bed at which unreacted vapors begin to be
exhausted. The saturation capacity (SAT) is the maximum amount of vapor that can
be adsorbed per unit mass of carbon (this is the capacity read from the adsorption isotherm) and thus is equal to CAP. The working capacity or working charge (WC) is the
actual amount adsorbed in the adsorber. Thus, the working capacity is a certain fraction
of the saturation capacity. Working capacities can range from 0.1 to 0.7 times the saturation capacity. (Note: A smaller capacity increases the amount of carbon required.) This
fraction is set by the designer for individual systems by often balancing the cost of
carbon and adsorber operation vs. preventing breakthrough while allowing for an
adequate cycle time.</p>





