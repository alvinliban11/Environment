    <h2>LESSON 3.5 PHASE EQUILIBRIUM</h2>



<p>In air pollution control equipment applications (particularly in absorption), the most
important equilibrium phase relationship is that between liquid and vapor. Henry’s
law theoretically describes liquid–gas behavior and under certain conditions is applicable in practice</p>

<p>Raoults’s law states that the partial pressure of a component over a solution is the
product of the vapor pressure of that component and the mole fraction of that component. Raoult’s law is expressed as</p>

<img src="img/pa.png">

<p>Raoult’s law applies over the entire concentration range from 0 to 1.0, but is applicable
only if the solution behaves in an ideal manner. Unfortunately few solutions behave
ideally. A special case of Raoult’s law is Henry’s law.
Henry’s law is an empirical relationship used for representing data on many
systems:</p>

<img src="img/pi.png">

<p>This is a more convenient form of Henry’s law to work with. The constant mi (or Hi) has
been determined experimentally for a large number of compounds and is usually valid at
low concentrations.</p>

<p>Another phase equilibrium application involves the psychrometric or humidity
chart. A humidity chart is used to determine the properties of moist air and to calculate
moisture content in air. The ordinate of the chart is the absolute humidity H, which is
defined as the mass of water vapor per mass of bone-dry air. (Some charts base the ordinate on moles instead of mass.) On the basis of this definition, Equation (3.33) gives
H in terms of moles and also in terms of partial pressure:</p>

<img src="img/h.png">

<p>The wet-bulb temperature (TWB) is another measure of humidity; it is the temperature at which a thermometer with a wet wick wrapped around the bulb stabilizes. As
water evaporates from the wick to the ambient air, the bulb is cooled; the rate of
cooling depends on how humid the air is. No evaporation occurs if the air is saturated
with water; hence, TWB and TDB (dry bulb) are the same. Furthermore, the lower the
humidity, the greater the difference between these two temperatures.</p>

<p>The humid volume is the volume of wet air per mass of dry air and is linearly related
to the humidity. The product of the humid volume and the absolute humidity gives the
volume of the moist air. The humid enthalpy (also called humid heat) is the enthalpy of
the moist air on bone-dry air basis. The term enthalpy is a measure of the energy content
of the mixture and is defined later in this chapter. Steam tables, charts, and diagrams may
also be required in air pollution control equipment calculations. Abbreviated and more
comprehensive information is available in the literature.
</p>





