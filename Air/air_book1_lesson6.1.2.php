<h4>Regenerable Adsorption Systems
</h4>


<p class="indent">A large regenerable adsorption system can be categorized as a fixed, moving, or fluidized
bed. The name refers to the manner in which the vapor stream and adsorbent are brought
into contact. The choice of a particular system depends on the pollutants to be controlled
and the recovery requirements. The most common adsorption system for controlling air
pollutants is the fixed carbon bed. These systems are used to control a variety of organic vapors and are usually regenerated by direct steaming of the bed. The organic compounds may be recovered by condensing the exhaust from the regeneration step and
separating out the water and solvent.</p>


<p class="indent">Fixed-bed adsorption systems generally involve multiple beds. One or more beds treat
the process exhaust while the other beds are either being regenerated or cooled. A typical
three-bed adsorption system is shown in Figure 6.4. The solvent-laden air stream is first pretreated to remove any solid or liquid particles that could blind the carbon bed and decrease
efficiency. The solvent-laden air stream then usually passes down through the fixed carbon
bed. Upward flow through the bed is usually avoided (unless flow rates are low ,500 cfm to
eliminate the risk of entraining carbon particles in the exhaust stream).
