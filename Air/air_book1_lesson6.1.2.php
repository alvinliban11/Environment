 <h2>LESSON 6.1 INTRODUCTION</h2><br>





<p> Another phenomenon, capillary condensation, usually occurs only in the micropores. Capillary condensation occurs when multilayers of adsorbed contaminant
molecules build up on both sides of the pore wall, totally packing the pore and condensing on it. The amount of contaminant that is removed increases since additional
molecules condense on the surface previously occupied by the liquid molecules.
However, desorption is often not as complete if capillary condensation occurs,
since the forces that hold a liquid together are much stronger than the physical
adsorption forces.</p>

<p class="indent">Air pollution control applications involve contaminant vapors at low partial
pressures. Therefore, the micropore structure of an adsorbent plays an important role
in determining the overall efficiency. Another reason for the wide use of activated
carbon is that 90–95% of its surface area is in the micropore size range.
Adsorption control systems can be classified as either regenerable or nonregenerable. Nonregenerable systems are normally used to control exhaust streams with low pollutant concentrations, below 1.0 ppm. Generally, these pollutants are highly odorous or
toxic to some degree. When these systems become saturated and start emitting pollutants
(breakthrough), the bed is taken off stream and replaced with a fresh bed. The used
carbon can then be sent back to the manufacturer for reactivation. Regenerable
systems are used for higher pollutant concentrations such as in solvent recovery operations. Once the bed reaches the breakthrough point in a regenerable system, the pollutant vapors are directed to a second bed while the first has the vapors desorbed. Details
on both classes of system follows.</p>

<h4>Nonregenerable Adsorption Systems</h4>

<p class="indent">Nonregnerable adsorption systems are manufactured in a variety of configurations. Bed
areas are sized to control the airflow through them at 20–60 ft/min (6.0–18 m/min).
They usually consist of a thin adsorbent bed depth, ranging in thickness from 0.5 to
4.0 inches (1.25–10.0 cm). These thin beds have a low pressure drop, normally below
0.025 in H2O (62 Pa) depending on the bed thickness, gas velocity, and particle size
of the adsorbent. Service time for these units can range from 6 months for heavy contaminant concentrations to up to 2 years for trace concentrations or intermittent operations. They are used mainly as air purification devices for small airflows in the
office, laboratory exhaust, and other small exhaust streams.</p>

<p class="indent">The shapes of these thin-bed adsorbers are flat, cylindrical, or pleated. The granules of
activated carbon are retained by porous support material, usually perforated sheetmetal.
The adsorber system usually consists of a number of retainers or panels placed in
one frame. The panels are similar to home air filters except that instead of containing
steel wool they contain activated carbon as the filter. The pleated cell is one continuous
retainer of activated carbon, rather than individual panels. The cylindrical canisters (see
Figure 6.2) are usually small units designed to handle low flow rates of approximately 25
cfm (0.12 m/s). Cylindrical canisters are made of the same materials as the panel and
pleated absorbers except their shape is round rather than square. Panel and pleated
beds are dimensionally about the same size, normally 2 ft2 (0.6 m2
), with the carbon
