    <h2>LESSON 2.4: THE CLEAN AIR ACT</h2>


<h4>Air Toxics</h4>

<p>Toxic air pollutants are those pollutants that are hazardous to human health or the environment. These pollutants are typically carcinogens, mutagens, and reproductive toxins.<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
The toxic air pollution problem is widespread. Information generated in 1987 from
the Superfund “right to know” rule (SARA Section 313), indicated that more than 2.7
billion pounds (lb) of toxic air pollutants were emitted annually in the United States.
The EPA studies indicated that exposure to such quantities of toxic air pollutants may
result in 1000–3000 cancer deaths each year.<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Section 112 of the Clean Air Act includes a list of 189 substances that are identified
as hazardous air pollutants. As noted earlier, a list of categories of sources that emit thesepollutants was prepared. [The list, of course, included the categories (1) major sources, or
sources emitting 10 tons per year of any single hazardous air pollutants; and, (2) area
sources (smaller sources, such as dry cleaners and autobody refinishing).] In turn,
EPA promulgated emission standards, referred to as the aforementioned maximum
achievable control technology (MACT) standards, for each listed source category.
These standards were based on the best demonstrated control technology or practices utilized by sources that make up each source category. Within 8 years of promulgation of a
MACT standard, EPA must evaluate the level of risk that remains (residual risk), due to
exposure to emissions from a source category, and determine whether the residual risk is
acceptable. If the residual risks are determined to be unacceptable, additional standards
are required.
</p>

<h4>Acid Depositon Control</h4>


<p>Acid rain occurs when sulfur dioxide and nitrogen oxide emissions are transformed in
the atmosphere and return to Earth in rain, fog, or snow. Approximately 20 million
tons of sulfur dioxide are emitted annually in the United States, mostly from the
burning of fossil fuels by electric utilities. Acid rain damages lakes, harms forests and
buildings, contributes to reduced visibility, and is suspected of damaging health.<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
It was hoped that the Clean Air Act would bring about a permanent 10 million ton
reduction in sulfur dioxide (SO2) emissions from 1980 levels. To achieve this, the EPA
allocated allowances in two phases, permitting utilities to emit one ton of sulfur dioxide.
The first phase, which became effective January 1, 1995, required 110 power plants to
reduce thier emissions to a level equivalent to the product of an emissions rate of 2.5 lb
of SO2/MM Btu (British thermal unit)  an average of their 1985–1987 fuel use.
Emissions data indicate that 1995 SO2 emissions at these units nationwide were
reduced by almost 40% below the required level.<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
The second phase, which became effective January 1, 2000, required approximately
2000 utilities to reduce their emissions to a level equivalent to the product of an emissions rate of 1.2 lb of SO2/MM Btu  the average of their 1985–1987 fuel use. In both
phases, affected sources were required to install systems that continuously monitor emission in order to track progress and assure compliance.
The Clean Air Act allowed utilities to trade allowances within their systems and/or
buy or sell allowances to and from other affected sources. Each source must have had
sufficient allowances to cover its annual emissions. If not, the source was subject to a
$2000/ton excess emissions fee and a requirement to offset the excess emissions in
the following year.<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
The Clean Air Act also included specific requirements for reducing emissions of
nitrogen oxides.</p>


<h4>Operating Permits</h4>

<p>The Act requires the implementation of an operating permit program modeled after
the National Pollution Discharge Elimination System (NPDES) of the Clean WaterAct. The purpose of the operating permits program is to ensure compliance with all
applicable requirements of the Clean Air Act. Air pollution sources, subject to the
program, must obtain an operating permit; states must develop and implement an operating permit program consistent with the Act’s requirements; and, EPA must issue
permit program regulations, review each state’s proposed program, and oversee the
state’s effort to implement any approved program. The EPA must also develop and
implement a federal permit program when a state fails to adopt and implement its
own program.<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
In many ways, this program is the most important procedural reform contained in the
1990 Amendments to the Clean Air Act. It enhanced air quality control in a variety of
ways and updated the Clean Air Act, making it more consistent with other environmental statutes. The Clean Water Act, the Resource Conservation and Recovery Act and the Federal
Insecticide, Fungicide, and Rodenticide Act all require permits.</p>

<h4>Stratospheric Ozone Protection</h4>
<p>The Clean Air Act requires the phase-out of substances that deplete the ozone layer. The
law required a complete phase-out of chlorofluorocarbons (CFCs) and halons, with stringent interim reductions on a schedule similar to that specified in the Montreal Protocol–
CFCs, halons, and carbon tetrachloride by 2000 and methyl chloroform by 2002.<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Class
II chemicals (HCFCs) will be phased out by 2030.
The law required nonessential products releasing Class I chemicals to the banned.
This ban went into effect for aerosols and noninsulating foams using Class II chemicals
in 1994. Exemptions are included for flammability and safety.</p>




