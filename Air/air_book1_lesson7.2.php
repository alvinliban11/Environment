 <h2>7.2 PARTICLE COLLECTION MECHANISMS


</h2><br>




<p>The overall collection/removal process for particulates in a fluid essentially consists of
four steps (L. Theodore: personal notes, 1976).
</p>



<p class="indent">1. An external force (or forces) must be applied that enables the particle to develop a
velocity that will displace and/or direct it to a collection or retrieval section or area.

<p class="indent">2. The particle should be retained at this area with strong enough forces so that it is
not reentrained.</p>
<p class="indent">3. As collected/recovered particles accumulate, they are subsequently removed.
</p>
<p class="indent">4. The ultimate disposition of the particles completes the process.</p>
</p>




<p>Note that all of these collection mechanism forces are strongly dependent on particle
size. Each mechanism is briefly described below.
As indicated above, all particulate devices collect or recover particles by a variety of
mechanisms involving an applied force—the simplest of which is gravity. Large particles moving slowly (relatively speaking) in a gas stream can settle out and be collected
or recovered. This mechanism is responsible for particle capture in the simplest of all
devices—the settling chamber. The distance (d) that the particle travels while settling
(traveling) with velocity (v) in time (t) is given by</p>

<img src="img/sd63.png">

<p class="indent">Details of gravity forces can be found in Chapter 8.
Centrifugal force is another collection mechanism used for particle capture. The
shape or curvature of the collector causes the gas stream to rotate in a spiral motion.
Large particles move toward the outside of the wall by virtue of their momentum.
The particles lose kinetic energy there and are separated from the gas stream. Particles
are then acted on by gravitational forces and are collected. Thus, centrifugal and gravitational forces are both responsible for particle collection in a cyclone. See Chapter 9
for more details.
</p>



<p class="indent">2Inertial impaction occurs when an object (e.g., a fiber or liquid droplet), placed in
the path of a particulate-laden gas stream, causes the gas to diverge and flow around it.
Larger particles, however, tend to continue in a straight path because of their inertia; they
may impinge on the obstacle and be collected (as in Figure 7.1). Since the trajectories of
particle centers can be calculated, it is possible to theoretically determine the probability
of collision.
</p>

<img src="img/sd64.png">



<p class="indent">3Direct interception also depends on inertia and is merely a secondary form of impaction. Note that the trajectory of a particle’s center can be calculated; however, even though
the center may bypass the target object, a collision might occur since the particle has finite
size (Figure 7.2). A collision occurs due to direct interception if the particle’s center misses
the target object by some dimension less than the particle’s radius. Direct interception is,
therefore, not a separate principle, but only an extension of inertial impaction.

</p>


<img src="img/sd65.png">



<p class="indent">Inertial impaction is analogous to a small car riding down an interstate highway at
65 mph (mi/hr) and approaching a merge lane where a slow-moving large truck has
entered. If the car is not able to get into the passing lane to go around the merged
truck, there could be an “impaction” incident. The faster the car is going, the more probable the impaction. The larger the car, the more difficulty it will have going around the
truck. Conversely, if the truck enters the interstate road at the same speed as the car, the
likelihood of impaction is reduced.
</p>


<p class="indent">Another primary particle collection mechanism involves electrostatic forces. The
particles can be naturally charged, or, as in most cases involving electrostatic attraction,
be charged by subjecting the particle to a strong electric field. The charged particles
migrate to an oppositely charged collection surface. This is the collection mechanism
responsible for particle capture in both electrostatic precipitators and charged droplet
scrubbers (see Appendix). In an electrostatic precipitator, particle collection occurs as
a result of electrostatic forces only. The electrostatic force FE experienced by a
charged particle in an electric field is given by</p>


<img src="img/sd66.png">


<p class="indent">
Thermophoresis and diffusiophoresis are two relatively weak forces that can affect
particle collection. Thermophoresis is particle movement caused by thermal differences
on two sides of the particle. The gas molecule kinetic energies on the hot side of the
particle are higher. Therefore, collisions with the particle on this side transfer more
energy than the molecular collisions on the cold side. Accordingly, the particle is
deflected toward the cold area. Diffusiophoresis is caused by an imbalance in the
kinetic energies being transmitted to the particles by the surrounding molecules.
When there is a strong difference in the concentration of molecules between two sides
of the particle, there is a difference in the number of molecular collisions. The particle
moves toward the area of lower concentration.</p>





<p class="indent">Very small particles deflect slightly when they are struck by gas molecules. The
deflection is caused by the transfer of kinetic energy from the rapidly moving gas molecule to the small particle. As the particle size, mass, and density increase, the extent of
the particle movement decreases. Thus, deflection begins to be effective as a capture
mechanism for particles less than approximately 1.0 mm, and it is significant for particles
less than 0.5 mm. Because of the bombardment by fluid molecules, particles suspended
in a fluid will be subjected to a random and chaotic molecular motion known as
Brownian motion. This movement arises in addition to any net motion in a given direction due to the action of other external forces such as gravity. Thus, this effect becomes
the predominant collection mechanism for particles in the 0.1–1.0 mm range. Additional
details are provided in the next section.</p>

<p>Other factors affecting collection mechanisms include:<br>
1. Nonspherical particles<br>
2. Wall effects<br>
3. Multiparticle effects<br>
4. Multidimensional flow</p>


<p>Details are available in the literature (L. Theodore, Nanotechnology: Basic Calculations
for Engineers and Scientists, John Wiley & Sons, Hoboken, NJ, 2007).</p>

