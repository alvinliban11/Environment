 <h2>LESSON 6.1 INTRODUCTION</h2><br>





<p>Because of its nonpolar surface, activated carbon is used to control emission of
organic solvents, odors, toxic gases, and gasoline vapors. Carbons used in gas phase
adsorption systems are manufactured in granular form, usually ranging from 4  6 to
4  20 mesh in size. (A 4  6 mesh is one that will pass the carbon through a
4-wire-per-inch Tyler mesh screen, but will be captured by a 6-wire-per-inch screen.)
The bulk density of the packed bed can range from 0.5 to 0.08 g/cm3 (from 30 to 5
lb/ft3
) depending on the internal porosity of the carbon. (The bulk density was defined earlier as the mass of carbon divided by the volume occupied by both the carbon
and void spaces between the carbon particles.) The surface area of the carbon can range
from 600 to 1600 m2
/g (2.9  106 to 7.8  106 ft2
/lb); this is equivalent to having the
surface the area of two to five football fields in one gram of carbon.</p>

<p class="indent"><b>Silica Gel.</b> Silica gels are made from sodium silicate. Sodium silicate is mixed
with sulfuric acid, resulting in a jelly-like precipitate from which the “gel” name
comes. The precipitate is then dried and roasted. Depending on the processes used in
manufacturing the gel, different grades of varying activity can be produced. Silica
gels have surface areas of approximately 3.7  106 ft2
/lb (750 m2
/g). Silica gels are
used primarily to remove moisture from exhaust streams, but are ineffective at temperatures above 5008F (2608C).</p>

<p class="indent"><b>Molecular Sieves. </b>Unlike the other adsorbents, which are amorphous (not crystalline) in nature, molecular sieves have a crystalline structure. The pores, therefore, are
relatively uniform in diameter. Molecular sieves can be used to capture or separate gases
on the basis of molecular size and shape. An example of this are refining processes,
which sometimes use molecular sieves to separate straight-chained paraffins from
branched and cyclic compounds. However, the main use of molecular sieves is in the
removal of moisture from exhaust streams. The surface area of molecular sieves range
from 2:9  106 to 3:4  106 ft2
=lb (from 600 to 700 m2
=g).</p>

<p class="indent"><b>Aluminum Oxide (Activated Alumina).</b> Aluminum oxides are manufactured
by thermally activated alumina or bauxite. This is accomplished by heating the
alumina in an inert atmosphere to produce a porous aluminum oxide pellet.
Aluminum oxides are not commonly used in air pollution applications. They are used
primarily for drying of gases, especially under high pressure, and as support material
in catalytic reactions. A prime example is the impregnating of the alumina with platinum
or palladium for use in catalytic incineration. The surface area of activated alumina can
range from 0.98  106 to 1.5  106 ft2
/lb (from 200 to 300 m2
/g).</p>

<h4>Pore Size Distribution
</h4>

<p class="indent">The physical properties of the adsorbent affect the adsorption capacity, rate, and pressure
drop across the adsorber bed. Table 6.1 summarizes these properties for the above adsorbents. Since adsorption occurs at the gas–solid interface, the surface area available to the
vapor molecules determines the effectiveness of the adsorbent. Generally, the
larger the surface area, the higher the adsorbent’s capacity. However, the surface area
must be available in certain pore sizes if it is to be effective as a vapor adsorber. The
pores in activated carbon are generally classified as micropores, transitional pores, or
macropores.</p>

<p class="indent">Micropores are openings whose radii are 200 nm (20 A˚ ) or less. Pores larger than
2000 nm (200 A˚ ) are macropores. Transitional pores are those with radii between 200
and 2000 nm (between 0.2 and 2.0 mm).</p>

<img src="img/as.png">

<p class="indent">Most gaseous air pollutant molecules are in the 40–90 nm size range. If a large
portion of an adsorbent’s surface area is in pores smaller than 40 nm, many contaminant
molecules will be unable to reach these active sites. Figure 6.1 illustrates molecule movement in the pores. In addition, the larger pore sizes (macro- and transitional) contribute
little to molecule capture. The vapor pressure of the contaminant in these larger areas is
too low to be effectively removed. These larger pores serve mainly as passageways to the
smaller micropore areas where the adsorption forces are strongest. Adsorption forces are
strongest in pores that are not more than approximately twice the size of the contaminant
molecule. These strong adsorption forces result from the overlapping attraction of the
closely spaced walls.
</p>

