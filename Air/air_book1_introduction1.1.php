<p class="indent">Many substances that oxidize slowly in a given state can oxidize extremely rapidly
or possibly even explode when dispersed as fine particles in air. Dust explosions, for
example, are often caused by the unstable burning or oxidation of combustible particles,
brought about by their relatively large specific surfaces. Adsorption and catalytic
phenomena can also be extremely important in analyzing and understanding particulate
pollution problems. For example, the conversion of sulfur dioxide to corrosive sulfuric
acid assisted by the catalytic action of iron oxide particles, demonstrates the catalytic
nature of certain types of particles in the atmosphere.<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
The technology of control (as it applies to this book) consists of all the sciences and
techniques that can be brought to bear on the problem via air pollution control equipment. These include the analysis and research that enter into determinations of technological and economic feasibility, planning, and standard-setting, as well as the
application of specific hardware, fuels, and materials of construction. Technology also
includes the process of evaluating and upgrading the effectiveness of air pollution
control practices.<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; At the heart of the control strategy process is the selection of the best air pollution
control measures from among those available. To eliminate or reduce emissions from
a polluting operation, four major courses of action are open:</p>



<p class="indent">1. Eliminate the operation</p>
<p class="indent">2. Regulate the location of the operation</p>
<p class="indent">3. Modify the operation</p>
<p class="indent">4. Reduce or eliminate discharges from the operation by applying control devices
and systems</p>

<p class="indent">The ability to achieve an acceptable atmosphere in a community often requires a combination of these measures aimed at all or a major fraction of the contaminant sources
within any control jurisdiction.<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
Control technology is self-defeating if it creates undesirable side effects in meeting
(limited) air pollution control objectives. Air pollution control should be considered in
terms of both the total technological system and ecological consequences. The former
considers the technology that can be brought to bear on not only individual pieces of
equipment but also the entire technological system. Consideration of ecological side
effects must also take into account, e.g., the problem of disposal of possibly unmanageable accumulations of contaminants by other means. These may be concentrated in the
collection process, such as groundwater pollution resulting from landfill practices or
pollution of streams from the discharges of air pollution control systems.<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
Gaseous and particulate pollutants discharged into the atmosphere can be controlled. The five generic devices available for particulate control include gravity settlers,
cyclones (centrifugal separators), electrostatic precipitators, wet scrubbers, and baghouses (fabric filtration). The four generic devices for gases include absorbers,
adsorbers, and enumerators. These control devices are discussed in individual chapters
later in the text.<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; There are a number of factors to be considered prior to selecting a particular piece of
air pollution control hardware. In general, they can be grouped in three categories:
environmental, engineering, and economic. These three categories are discussed below.</p>
<p class="indent">1. Environmental</p>
<p class="indent1">a. Equipment location</p>
<p class="indent1">b. Available space</p>
<p class="indent1">c. Ambient conditions</p>
<p class="indent1">d. Availability of adequate utilities (power, compressed air, water, etc.) and
ancillary systems facilities (waste treatment and disposal, etc.)</p>
<p class="indent1">e. Maximum allowable emission (air pollution codes)</p>
<p class="indent1">f. Aesthetic considerations (visible steam or water vapor plume, etc.)</p>
<p class="indent1">g. Contribution of air pollution control system to wastewater and land pollution</p>
<p class="indent1">h. Contribution of air pollution control system to plant noise level</p>

<p class="indent">2. Engineering</p>
<p class="indent1">a. Contaminant characteristics [physical and chemical properties, concentration,
particulate shape and size distribution (in the case of particulates), chemical
reactivity, corrosivity, abrasiveness, toxicity, etc.]</p>
<p class="indent1">b. Gas stream characteristics (volumetric flow rate, temperature, pressure,
humidity, composition, viscosity, density, reactivity, combustibility, corrosivity, toxicity, etc.)</p>
<p class="indent1">c. Design and performance characteristics of the particular control system [size
and/or weight fractional efficiency curves (in the case of particulates), mass
transfer and/or contaminant destruction capability (in the case of gases or
vapors), pressure drop, reliability and dependability, turndown capability,
power requirements, utility requirements, temperature limitations, maintenance
requirements, flexibility of complying with more stringent air pollution
codes, etc.]</p>

<p class="indent">3. Economic</p>
<p class="indent1">a.  Capital cost (equipment, installation, engineering, etc.)</p>
<p class="indent1">b. Operating cost (utilities, maintenance, etc.)</p>
<p class="indent1">c. Expected equipment lifetime and salvage value</p>







