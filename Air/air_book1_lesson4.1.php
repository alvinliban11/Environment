    <h2>LESSON 4.1 INTRODUCTION</h2><br>


<p>Combustion is often used to control the emissions of organic compounds from process
industries. At a sufficiently high temperature and adequate residence time, any hydrocarbon can be oxidized to carbon dioxide and water by the combustion process.
</p>


<p class="indent">Combustion systems are often relatively simple devices capable of achieving very
high removal destruction efficiencies. They consist of burners, which ignite the fuel
and organic, and a chamber, which provides appropriate residence (or detention)
time for the oxidation process. Because of the high cost and decreasing supply of
fuels, combustion systems may be designed to include some type of heat recovery.
Combustion is also used for the more serious emission problems that require high
destruction efficiencies, such as the emission of toxic or hazardous gases. There are,
however, some problems that may occur when using combustion. Incomplete combustion of many organic compounds results in the formation of aldehydes and
organic acids, which may create an additional pollution problem. Oxidizing organic
compounds containing sulfur or halogens produce unwanted pollutants such as sulfur
dioxide, hydrochloric acid, hydrofluoric acid, or phosgene. If present, these pollutants would require a scrubber (see Chapter 11) to remove them prior to release into
the atmosphere</p>

<p class="indent">Several basic combustion systems are in use. Although these devices are physically
similar, the conditions under which they operate may be different. Choosing the proper
device depends on many factors, including the type of hazardous contaminants in the
waste stream, concentration of combustibles in the steam, process flow rate, control
requirements, and an economic evaluation.
</p>


<p class="indent">Combustion is a chemical process occurring from the rapid combination of oxygen
with various elements or chemical compounds resulting in the release of heat. The
process of combustion is also referred to as oxidation or incineration. Most fuels used
for combustion along with the waste are composed essentially of carbon and hydrogen,
but can include other elements such as sulfur, nitrogen, and chlorine. Although combustion
seems to be a very simple process that is well understood, in reality it is not. The exact
manner in which a fuel or waste is oxidized occurs in a series of complex, free radical
chain reactions. The precise set of reactions by which combustion occurs is termed the
mechanism of combustion. By analyzing the mechanism of combustion, the rate at which
the reaction proceeds and the variables affecting the rate can be estimated. For most combustion devices, the rate of reaction proceeds extremely fast compared to the mechanical
operation of the device. Maintaining efficient and complete combustion is somewhat
of an art rather than a science. Therefore, this section focuses on the factors that
influence the completeness of combustion, rather than analyzing the mechanism involved.</p>


<p class="indent">To achieve complete combustion once the air (oxygen), waste, and fuel have been
brought into contact, the following conditions must be provided: a temperature high
enough to ignite the waste/fuel mixture, turbulent mixing of the air and waste/fuel;
and, sufficient residence time for the reaction to occur. These three conditions are
referred to as the “three Ts of combustion.” Time, temperature, and turbulence govern
the speed and completeness of reaction. They are not independent variables since
changing one can affect the other two.</p>




<p class="indent">The rate at which a combustible compound is oxidized is greatly affected by temperature. The higher the temperature, the faster the oxidation reaction will proceed. The
chemical reactions involved in the combustion of a fuel and oxygen can occur even at
room temperature, but very slowly. For this reason, a pile of oily rags can be a fire
hazard. Small amounts of heat are liberated by the slow oxidation of the oils. This, in
turn, raises the temperature of the rags and increases the oxidation rate, liberating more
heat. Eventually, a full-fledged fire can break out</p>


<p class="indent">For combustion processes, ignition is accomplished by adding heat to speed up the
oxidation process. Heat is needed to combust any mixture of air and fuel until the
ignition temperature of the mixture is reached. By gradually heating a mixture of fuel
and air, the rate of reaction and energy released will gradually increase until the reaction
no longer depends on the outside heat source. In effect, more heat is being generated than
is lost to the surroundings. The ignition temperature must be reached or exceeded to
ensure complete combustion. To maintain combustion of a waste, the amount of
energy released by the combusted waste must be sufficient to heat up the incoming
waste (and air) up to its ignition temperature; otherwise, a fuel must be added. The
ignition temperature of various fuels and compounds can be found in combustion handbooks. These temperatures are dependent on combustion conditions and therefore
should be used only as a guide. Ignition depends on</p>

<p class="indent1">1. Concentration of combustibles in the waste stream</p>
<p class="indent1">2. Inlet temperature of the waste stream
</p>
<p class="indent1">3. Rate of heat loss from combustion chamber
</p>
<p class="indent1">4. Residence time and flow pattern of the waste stream
</p>
<p class="indent1">5. Combustion chamber geometry and materials of construction
</p>


