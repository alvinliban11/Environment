<p>After a plate layout has been assumed, it is necessary to check the plate for its
liquid-handling capacity. If the liquid-to-gas ratio is high and the column diameter
large, the check will indicate whether the column will show a tendency towards flooding
or gas maldistribution on the plate. If this is the case, then the liquid rate is the
controlling factor in estimating column diameter and a satisfactory assumption for design purposes is a plate-handling capacity of 30 gal/min of liquid per foot of
diameter. However, a well-designed single-pass, crossflow plate can ordinarily be
expected to handle up to 60 gal/min of liquid per foot of diameter without excessive
liquid gradient on the plate. It should also be noted that low gas rates can lead to
weeping, a condition where the liquid flows down through the holes in the plate
rather than across the plate.</p>




<p class="indent">The column height is determined from the product of the number of actual plates
(theoretical plates divided by the overall plate efficiency) and the plate spacing
chosen. The theoretical plate (or “stage,” as it is sometimes called) is the theoretical
unit of separation in plate column calculations. It is defined as a plate in which two
dissimilar phases are brought into intimate contact with each other until equilibrium is
reached and then are mechanically or otherwise separated. During the contact, various
diffusing components of the mixture redistribute themselves between the phases. In
an equilibrium stage the two phases are well mixed for a time sufficient to allow establishment of equilibrium between the phases leaving the stage. At equilibrium, no further
net change of composition of the phases is possible for a given set of operating conditions. The number of theoretical plates can be determined graphically from the operating diagram composed of an operating line and equilibrium curve.<br><br>
For cases when both the operating line and the equilibrium curve may be considered
straight (dilute solutions), the number of theoretical plates may be determined directly
without recourse to graphical techniques. This will frequently be the case for relatively
dilute gases (as usually encountered in air pollution control) and liquid solutions where,
more often than not, Henry’s law is usually applicable. Since the quantity of gas
absorbed is small, the total flows of liquid and gas entering and leaving the column
remain essentially constant. Hence, the operating line will be substantially straight.
