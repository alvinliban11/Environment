<h3>PROBLEM</h3>


<p><b>6.8 Representing Equilibrium Data
</b></p>

<p>The representation of adsorption data at a constant temperature that indicates the
amount adsorbed versus the partial pressure of the adsorbate is called an<br>
(a) Isobar<br>
(b) Isostere<br>
(c) Isotherm<br>
(d) Isokinetic<br>
Solution: Adsorption equilibrium data at a constant temperature are referred to
collectively as an isotherm. The correct answer is therefore (c).</p>


<p><b>6.9 Process Options
</b></p>

<p>Of the following vapors, which one would be most readily adsorbed using activated carbon?<br>
(a) H2O at 140F<br>
(b) CH4 at 70F<br>
(c) C4H10 at 140F<br>
(d) C4H10 at 70F<br>
Solution: The general rule of thumb is that organics are more easily adsorbed on
activated carbon. Furthermore, the higher the molecular weight, the more easily it
can be captured (because of the larger molecular diameter). Finally, increasing the
temperature decreases the adsorption capability. The correct answer is therefore (d).</p>

<p><b>6.10 Saturation Capacity

</b></p>

<p>TThe saturation capacity of an adsorbent can be obtained by<br>
(a) Subtracting the heel from the breakthrough capacity<br>
(b) Adding the breakthrough and working capacity<br>
(c) Reading a graph of the applicable isotherm<br>
(d) Subtracting the heel from the working capacity<br>
Solution: Saturation capacity represents the equilibrium capacity of the adsorbent for a particular system and specific operating conditions. It therefore represents the equilibrium value. The correct answer is therefore (c).</p>

<p><b>6.11 Mass Transfer Zone


</b></p>

<p>In adsorption, if the mass transfer zone (MTZ) is larger than the bed depth, then<br>
(a) The bed will become blinded with water<br>
(b) The pressure drop will be too great<br>
(c) The system is well designed<br>
(d) Untreated vapors will be exhausted<br>
Solution: The mass transfer zone represents the length of the bed across which
the concentration gradient exists. Therefore, all the vapors will not be captured
since the bed at the discharge location contains residual vapors. Note that
“untreated vapors” refers to discharge under normal operating conditions. The
correct answer is therefore (d).</p>

<p><b>6.12 Thin Beds

</b></p>

<p>A thin-bed adsorption system is employed mainly for<br>
(a) High power consumption per cfm<br>
(b) Low concentration of adsorbate in the gas<br>
(c) High pressure drop<br>
(d) High collection efficiency<br>
Solution: Answers (a), (c), and (d) are incorrect. A thin bed cannot be used if the
concentration is high, as this would result in the bed becoming saturated in a
short period of time. However, a low concentration is acceptable. The correct
answer is therefore (b).</p>


<p><b>6.13 Advantages Versus Disadvantages


</b></p>

<p>Describe three advantages and three disadvantages of using an adsorber for
gaseous control.<br>
Solution: Three advantages are:<br>
1. Moderate capital costs<br>
2. Moderate operating costs<br>
3. Ability to regenerate at high recovery efficiencies<br>
Three disadvantages are:<br>
1. Pollutant is not permanently destroyed<br>
2. Carbon has to be periodically replaced<br>
3. Plugging and/or short-circuiting can occur</p>


<p><b>6.14 Design and Process Factors


</b></p>

<p>As noted earlier, activated carbon adsorption equipment is commonly used for
the control of gaseous pollutants. Aside from the size of such a piece of equipment, list and briefly discuss at least five design or process factors that affect its
effectiveness in controlling gaseous pollutants.<br>
Solution:<br>
1. Temperature<br>
2. Pressure<br>
3. Molecular weight<br>
4. Partial pressure of other gases<br>
5. Reactivity, e.g., decomposition and polymerization<br>
6. Size and/or surface area of adsorbent<br>
7. Design considerations, including height, velocity, pressure drop, concentration (in and out), and regeneration frequency</p>






