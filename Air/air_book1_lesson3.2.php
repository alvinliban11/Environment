    <h2>3.2 MEASUREMENT FUNDAMENTALS</h2>

<p><b>Units and Dimensions</b></p>
<p>The units used in this text are consistent with those adopted by the engineering session in
the United States. For engineering work, SI (Syste`me International) and English units are most often employed; in the U.S., the English engineering units are generally used,
although efforts are still in place or underway to obtain universal adoption of SI units for
all engineering and science applications. The SI units have the advantage of being based
on the decimal system, which allows for more convenient conversion of units within the
system. However, the English engineering units will primarily be used here. Conversion
factors between SI and English units and additional details on the SI system are provided
in the Appendix.</p>

<p><b>Conversion of Units</b></p>

<p>Converting a measurement from one unit to another can conveniently be accomplished
by using unit conversion factors; these factors are obtained from a simple equation that
relates the two units numerically. For example, for
12 inches (in) ¼ 1 foot (ft) (3:1)
the following conversion factor can be obtained:
12 in=1 ft ¼ 1 (3:2)
Since this factor is equal to unity, multiplying some quantity (e.g., 18 ft) by this factor
cannot alter its value. Hence
18 ft (12 in=1 ft) ¼ 216 in (3:3)</p>

<p>Note that in Equation (3.3), the original units of feet on the left-hand side cancel out
leaving only the desired units of inches.<br><br>
Physical equations must be dimensionally consistent. For the equality to hold, each
term in the equation must have the same dimensions. This condition can be and should
be checked when solving engineering problems. Throughout this book, great care is
exercised in maintaining the dimensional formulas of all terms and the dimensional
homogeneity of each equation. Equations will generally be developed in terms of
specific units rather than general dimensions (e.g., feet, rather than length). This
approach should help the reader to more easily attach physical significance to the
equations presented.</p>

<p><b>Significant Figures and Scientific Notation</b></p>

<p>Significant figures provide an indication of the precision with which a quantity is
measured or known. The last digit represents, in a qualitative sense, some degree of
doubt. For example, a measurement of 8.32 inches implies that the actual quantity is
somewhere between 8.315 and 8.325 inches. This applies to calculated and measured
quantities; quantities that are known exactly (e.g., pure integers) have an infinite
number of significant figures.</p>

<p>The significant digits of a number are the digits from the first nonzero digit on the
left to either (1) the last digit (whether it is nonzero or zero) on the right if there is a
decimal point, or (2) the last nonzero digit of the number if there is no decimal point.
For example:</p>
<p>370 has 2 significant figures<br>
370. has 3 significant figures<br>
370.0 has 4 significant figures<br>
28,070 has 4 significant figures<br>
0:037 has 2 significant figures<br>
0.0370 has 3 significant figures<br>
0.02807 has 4 significant figures</p>



