<h3>PROBLEM</h3>


<p><b>6.46 Accident and Emergency Management</b></p>

<p>On the basis of the air pollution control equipment course recently completed
under the direction of the internationally recognized authority Dr. Theodore, a
young engineer is now aware that during the operation of an adsorber, a fire
and/or explosion can develop, particularly when treating ketones. Since two
of the adsorbers at her plant site have been retrofitted to recover/control a
dilute MIBK air stream from a process vent, she is concerned about a possible
accident and/or explosion. Outline what steps or procedures she should take
or follow, as a competent and responsible environmental engineer, to help
reduce this hazard.<br>
Solution: This, too, is an open-ended question. Here are some suggestions:

</p>


<p>1. Thermocouples are a good indicator of a fire; however, they should be placed
in the gas, not in the adsorbent.<br>
2. Carbon monoxide and carbon dioxide are also good indicators of a fire; a gas
chromatograph is satisfactory.<br>
3. Ketones are particularly bad actors; be careful on startup, begin with the
regenerative step.<br>
4. If a fire develops, steam (if available) the unit to death.<br>
5. Be sure that concentration levels are below the LEL.<br>
6. Operating above the LEL can be dangerous, particularly if the concentration
drops.<br>
7. Keep ignition sources away.<br><br>
NOTE: Additional problems are available for all readers at www.wiley.com. Follow
links for this title.</p>





