  <h2>LESSON 5.3 OPERATION AND MAINTENANCE, AND IMPROVING
PERFORMANCE

</h2><br>


<p>Since packed towers are used primarily for gaseous pollutant control, this device will
be emphasized in the presentation below, although much of this material will also be
applicable to plate and other towers
</p>




<p class="indent">2. Estimate the efficiency of separation E. This may be determined at the local
(across plate), 

<p class="indent">Normal preventive maintenance requires only periodic checks of the fan, pumps,
chemical feed system, piping, duct, and liquid distributor. The normally irrigated
packing may never have to be cleaned during the life of the absorber as long as the absorber has been properly designed and operated. However, should the absorber be run dry
or the entering air stream contain unexpectedly high solids loading, a heavy formation of
solids, crystallized salts, or other foreign matter may accumulate on the packing, and this
must be removed. In most cases, removal can be accomplished by recirculating, for a
short period of time, a chemical solution into which the solids will dissolve or react.
Sometimes, chemical treatment will not remove the buildup and it may be necessary
to use high-pressure water, hot water, or atmospheric steam. The absorber manufacturer
should be consulted to verify the resistance of the internals and shell to the cleaning
process. Prior to using any chemical, hot water, or steam, in a very few instances,
such as CaF2 deposition or extremely heavy solids deposition, it may be necessary to
remove the packing medium from the absorber for cleaning.</p>


<p class="indent">The normally unirrigated entrainment separator must be periodically flushed with
sprays to prevent buildup and eventual plugging. The intervals between routine washings
must be determined by experience, as the collection of solid materials is a function of
specific operating conditions.<br>
A maintenance checklist is suggested to ensure proper operation and unexpected
problems with the absorber system. The periodic time intervals indicated for maintenance will vary depending on the specific system’s operating conditions and the equipment manufacturer’s recommendation.<br>
There are options available that should be considered when purchasing a packed
absorber (or any absorber) in order to reduce maintenance costs and allow the equipment
to be more operationally reliable between scheduled maintenance shutdowns. Any of
these options generally increase the initial cost of the equipment, and for this reason are seldom considered. However, some of these items can pay for themselves in a short
time by reducing maintenance costs.
</p>

<p class="indent">Another source of periodic maintenance relates to the strainers or filters in the
recycle piping and the recycle pumps. To prevent total system shutdown during cleaning
or maintenance of these items, dual strainers or pumps may be considered, valved separately, so that the system can be maintained in a fully operational mode during strainer
cleaning or pump maintenance checks. 
Proper instrumentation used for the purpose of providing operational data is useful in
determining areas where problems or maintenance may be required. The type of instrumentation will depend totally on the system requirements. Areas of specific interest would be
pressure drop across the absorber, both the wetted bed and entrainment separator, low liquid
recycle flow and/or pressure, liquid makeup rates to the system, and liquid temperatures.</p>
<p class="indent">The absorber can be designed to provide future additional packing height requirements should greater future gas absorption capability be required. This can be accomplished in one of two ways: the shell of the absorber can be flanged to allow a future
stub section to be inserted, or the tower can be initially designed with a void space
above the packing between the liquid distributors so that future packing heights can
be added without shell modification. Although the initial cost of this may seem high,
it could preclude the need to field-modify the absorber or to purchase a new absorber
for greater efficiency, when and if the air pollution laws are tightened.<br><br>
Recent developments with absorbers have centered primarily on improvement of</p>
<p class="indent">1. Packing types</p>
<p class="indent">2. Plate manufacture</p>
<p class="indent">3. Liquid distributor manufacture (the author favors one spray).</p>
<p >However, the reader should note that these units are still designed essentially as they
were nearly 100 years ago.</p>




