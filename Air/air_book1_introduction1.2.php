<p>Prior to the purchase of control equipment, experience has shown that the following
points should be emphasized:</p>

<p class="indent">1. Refrain from purchasing any control equipment without reviewing certified
independent test data on its performance under a similar application.<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Request
the manufacturer to provide performance information and design specifications.</p>
<p class="indent">2. In the event that sufficient performance data are unavailable, request that the
equipment supplier provide a small pilot model for evaluation under<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; existing
conditions.</p>
<p class="indent">3. Request participation of the appropriate regulatory authorities in the decisionmaking process.</p>
<p class="indent">4. Prepare a good set of specifications. Include a strong performance guarantee
from the manufacturer to ensure that the control equipment will meet all<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
applicable local, state, and federal codes/regulations at specific process
conditions</p>

<p class="indent">5. Closely review the process and economic fundamentals. Assess the possibility
for emission trade-offs (offsets) and/or applying the “bubble concept”<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; (see
Chapter 2). The bubble concept permits a plant to find the most efficient
way to control its emissions as a whole. Reductions at a source where<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; emissions
can be lessened for the least cost can offset emissions of the same pollutant
from another source in the plant.</p>
<p class="indent">6. Make a careful material balance study before authorizing an emission test or
purchasing control equipment.</p>
<p class="indent">7. Refrain from purchasing any equipment until firm installation cost estimates
have been added to the equipment cost. Escalating installation costs are <br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;the
rule rather than the exception.</p>
<p class="indent">8. Give operation and maintenance costs high priority on the list of equipment
selection factors.</p>
<p class="indent">9. Refrain from purchasing any equipment until a solid commitment from the
vendor(s) is obtained. Make every effort to ensure that the new system will<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
utilize fuel, controllers, filters,</p>
<p class="indent">10. The specification should include written assurance of prompt technical
assistance from the equipment supplier. This, together with a completely <br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;understandable operating manual (with parts list, full schematics, consistent units,
and notations, etc.), is essential and is too often forgotten in the rush<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; to get
the equipment operating.</p>
<p class="indent">11. Schedules, particularly on projects being completed under a court order or
consent judgment, can be critical. In such cases, delivery guarantees <br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;should
be obtained from the manufacturers and penalties identified.</p>
<p class="indent">12. The air pollution equipment should be of fail-safe design with built-in
indicators to show when performance is deteriorating.</p>
<p class="indent">13. Withhold 10–15% of the purchase price until compliance is clearly
demonstrated.</p>
<p>The usual design, procurement, construction, and/or startup problems can be
further compounded by any one or a combination of the following:</p>

<p class="indent">1. Unfamiliarity of process engineers with air pollution engineering</p>
<p class="indent">2. New and changing air pollution codes/regulations</p>
<p class="indent">3. New suppliers, frequently with unproven equipment</p>
<p class="indent">4. Lack of industry standards in some key areas</p>

<p class="indent">5. Interpretations of control by agency field personnel</p>
<p class="indent">6. Compliance schedules that are too tight</p>
<p class="indent">7. Vague specifications</p>
<p class="indent">8. Weak guarantees for the new control equipment</p>
<p class="indent">9. Unreliable delivery schedules</p>
<p class="indent">10. Process reliability problems</p>







