<p align="left">
    <strong>P Soup: The Global Phosphorus Cycle</strong>
</p>
<p align="left">
    by Elena Bennett and Steve Carpenter
</p>
<p>
    Think of global environmental change, and you’ll probably think most
    immediately of such sweeping atmospheric phenomena as global warming or
    ozone depletion. Many of the other environmental disruptions we’re familiar
    with— toxic dumps, decimated forests, eroded fields—seem largely confined
    to particular localities.
</p>
<p>
    Yet there are some environmental changes that, while appearing to be
    locally confined,are in fact manifestations of worrisome global patterns.
    Look at the algae forming on a local farm pond, for example, and you’re
    seeing the result of a process—the phosphorus cycle—that extends far beyond
    that farm.
</p>
<p>
    Algae thrives (literally “blooms”) on runoff of waste fertilizer or other
    materials containing phosphorus. While human-caused changes in the closely
    related nitrogen cycle have been widely publicized, impacts on the
    phosphorus cycle are less well known. Our research suggests, however, that
    the movement of phosphorus is indeed a global phenomenon—and that that
    patch of algae you see in the pond at your feet may be affected by changes
    in the soil hundreds or thousands of miles away.
</p>
<p>
    Both nitrogen and phosphorus are essential nutrients for plants and are
    therefore present in most fertilizers in addition to being present in
    agricultural and municipal waste products. As a result, the movement of
    large amounts of fertilizers around the planet can also mean the movement
    of excessive nutrients from one place to another. Typically, some of the
    fertilizer used on a farm does not stay there but moves downhill where it
    can get into a downstream aquatic ecosystem—a river, lake, or bay.
    Concentrations of excess nutrients in these bodies of water cause the
    patches of algae to expand prolifically. Such “eutrophication,” as the
    green blanketing of the water is called, can be a crippling process: it
    suffocates the life under the slime— killing fish, diminishing
    biodiversity, and emitting noxious odors. It reduces the value of the water
    for most human uses— whether for drinking, fishing, swimming, or even
    boating.
</p>
<p>
    Lake Mendota, in Madison, Wisconsin is a classic example of a eutrophic
    lake in an urbanizing, but primarily agricultural setting. The lake has
    exhibited many of the symptoms associated with eutrophication since
    agriculture became the primary land cover in the surrounding watershed, in
    place of the native prairie and oak savannah. Blooms of blue-green algae
    have been common here since the 1880s. Along with these blooms came
    dramatic changes in the food web, including loss of some native species and
    increased populations of non-native species such as Eurasian milfoil and
    carp. Eutrophication has greatly diminished the lake’s recreational value.
</p>
<p>
    Historically, Madison has never developed public swimming pools, because
    people were always able to swim in the area’s plentiful lakes. Now, as
    eutrophication worsens, there is increasing public pressure to develop
    pools because many of the lakes are no longer swimmable. For Lake Mendota
    alone, the cost of eutrophication has been estimated to be about $50
    million in lost recreation and property values. Even so, what happened to
    this lake is seen as a local story—of little interest to someone in North
    Carolina or South China. If the people in Madison want to deal with this
    problem, obviously it is their own local farms and sewer pipes they have to
    deal with. What’s not so obvious is that what has happened here results
    from massive changes in the flow of phosphorus around the globe.
</p>
<p align="left">
    <strong>HUMAN IMPACT ON THE GLOBAL PHOSPHORUS CYCLE</strong>
</p>
<p>
    Long before humans arrived on the scene, phosphorus was moving around the
    planet in a natural cycle that probably took millions of years to complete.
    A phosphorus molecule might be trapped in rock, then released by erosion to
    start its gravitydriven journey to the ocean. Along the way, it might be
    taken up by plants and then animals, then returned to the soil or water via
    dead vegetation or urine, to continue its slow trek downhill until it
    finally reached the ocean. Once in the ocean—probably after taking more
    detours through plants and animals along the way— it would sink into the
    sediment. In time, geological processes would turn this sediment to
    terrestrial rock—reincorporating the phosphorus molecule. The cycle would
    begin again.
</p>
<p>
    With the advent of human agriculture and urbanization, the natural cycle
    was in some respects shortcircuited. Technological advances, especially in
    the past 50 years, enabled us to mine phosphorus on a large scale, make
    fertilizer and other products from it, and transport these products around
    the world, dramatically accelerating the now not-so-natural phosphorus
    cycle. Globally, we estimate that the annual accumulation of phosphorus in
    the Earth’s freshwater and terrestrial ecosystems has almost quadrupled,
    from around 3.5 terragrams per year before humans began mining and farming
    on a large scale, to around 13 terragrams per year now.
</p>
<p>
    To understand the cycle as it was before human interventions began, it is
    useful to think of the cycle as a flow from the earth’s crust back to
    earth’s crust, through four main compartments. First, phosphorus-containing
    rock is weathered—worn by wind, rain, freezing and thawing, etc.—until it
    becomes soil. With the soil, it moves into lakes and rivers, which
    transport it to the ocean. Some of it is dissolved in water, and some
    adsorbed to soil particles that are carried by erosion down to the sea and,
    ultimately, to the ocean bottom. There it awaits the tectonic movement that
    will lift up the rock and make it part of the land again. The cycle is
described by Aldo Leopold in his essay, “Odyssey,” in    <em>A Sand County Almanac</em>: “X had marked time in the limestone ledge
    since the Paleozoic seas covered the land. Time, to an atom locked in a
    rock, does not pass. The break came when a bur-oak root nosed down a crack
    and began prying and sucking. In the flash of a century the rock decayed,
    and X was pulled out and up into the world of living things. He helped
    build a flower, which became an acorn, which fattened a deer, which fed an
    Indian, all in a single year. . . .” The narrative continues through a
    journey of many adventures involving a bluestem, a plover, some phlox, a
    fox, a buffalo, a spiderwort, a prairie fire, another fox, another Indian,
    a beaver, a bayou, and a riverbank. Then, “One spring an oxbow caved the
    bank and after one short week of freshet [a rise or overflow of a stream] X
    lay again in his ancient prison, the sea.”
</p>
<p>
    Within each of the four compartments of this cycle—the Earth’s crust, the
    soil, aquatic systems and the oceans—there are more rapid cycles of
    phosphorus through the biosphere. In the soil phase, the phosphorus does
    not all just stay put in the ground.
</p>
