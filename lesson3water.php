    that around the world, water subsidizes agricultural systems. Because we do
    not account for the cost of water pollution when we add up the cost of
    agricultural production, food production seems cheaper than it actually is.
    In economic terms, the cost of water pollution caused by agricultural
    production is an externality to the agricultural production system. If this
    worked in the past because clean fresh water seemed infinite, it no longer
    makes sense to take fresh water supply for granted. Since both food and
    water are necessary for human survival, it might appear that we are facing
    a difficult choice between producing food and protecting aquatic resources.
    However, just as we cannot afford to ignore water pollution, we cannot
    afford to solve the problem simply by reducing agricultural production.
</p>
<p>
