        <p align="left">
            <strong>INVASIVE SPECIES</strong>
        </p>
        <p>
            Invasive species—non-native species whose introduction harms or is
            likely to harm the environment, economy, or human health—present
            one of the most significant threats to biodiversity and healthy
            ecosystems. Once introduced, they have the potential to establish
            themselves alongside, or in place of, existing species. They can
            compete with native species for prey and habitat, facilitate the
            spread of diseases, introduce new genetic material, and even alter
            landscapes. Invasive species can impede endangered species
            conservation and restoration efforts. In the marine environment,
            some compete with commercially significant fish species for food
            and habitat, or they clog nets and eat bait. On land and in the
            sea, invasive species are responsible for about 137 billion dollars
            in lost revenue and management costs in the U.S. each year.
        </p>
        <p>
            Invasive species are hard to identify and eradicate before they
            take hold in an ecosystem, which can occur remarkably quickly. For
            example, every 14 weeks, a new invasive species is discovered in
            the San Francisco Bay.
        </p>
        <p>
            Ballast water is the primary vector for the release of invasive
            species into marine waters. Ballast water—and all the living
            creatures contained within it—is pumped into and out of oceangoing
            vessels for stabilization. Often it is taken up in one port and
            discharged in another. Every day, some 7,000 species are
            transported around the world via ballast water.
        </p>
        <p>
            Another important vector is aquaculture. Species such as Atlantic
            salmon, grown on the western coasts of the U.S. and Canada, act as
            invasive species if they escape or are released unintentionally
            from aquaculture facilities into the surrounding waters. Once in
            the wild, they can compete with native species for food, shelter,
            and other resources, as well as spread disease. In some cases,
            species raised for aquaculture may interbreed with native species,
            potentially threatening the viability of native stocks.
        </p>
        <p>
            Other vectors include the home aquarium industry, ship hulls, oil
            platforms, and marine debris. Invasive species arrive in seaweed
            used to pack live bait and via the pet trade industry. They also
            reach U.S. waters as live food imports. The Internet has
            significantly aided the introduction of new species. Today,
            consumers need only a credit card, access to a computer, and a
            delivery address to purchase marine life for food, for use as bait,
            or as pets.
        </p>
        <p>
            In an increasingly global economy, all this mobility represents a
            serious threat to the health of living marine resources.
        </p>
        <p>
            Our laws are not equipped to deal with these threats. Biological
            pollution by invasive species is the focus of the National Invasive
            Species Act of 1996 (NISA). However, under the NISA structure,
            invasive species are managed on a caseby-case, crisis-by-crisis
            basis, and the national focus is on terrestrial invasive species.
        </p>
        <p>
            To the extent that NISA addresses marine species, it does so almost
            exclusively in the context of ballast-water discharges, despite the
            existence of many other vectors. Ballast-water exchange (BWE) is a
            procedure in which ships in the open ocean dump ballast water taken
            aboard in foreign ports. Its purpose is to lessen the chance of
            introducing coastal invasive species into potentially hospitable
            habitats in destination ports. However, BWE does not always
            dislodge species and it does not apply to coastwise travel, which
            can also allow species to be transported to new environments.
            Additionally, BWE is not mandatory under NISA. Although the U.S.
            Coast Guard is required to check ship logs to determine whether an
            exchange occurred, it is not required to check the ballast tanks.
            Current guidelines encourage ship operators to report voluntary
            exchange, but compliance with this minimal requirement is weak.
        </p>
        <p>
            There is little law focusing on other vectors of invasive species.
            For example, there is no uniform regime in place to track live
            imports either entering or traveling around the country. There is
            no systematic process for determining which management approach is
            best when a species is found, no central source of information for
            researching species, and no dedicated source of funding to control
invasive species. For species like the destructive seaweed,            <em>Caulerpa taxifolia</em>, which grows as much as three inches
            [7.6 cm] a day, any delay in response could have severe
            environmental and economic
        </p>
        <p>
            ramifications.
        </p>
        <p>
            Currently, agencies at different levels of government report
            commodities using a different nomenclature and verification system.
            With such inconsistency, neighboring states could simultaneously be
            working to promote and eradicate the same species, and one agency’s
            food list could be another agency’s most wanted list of invaders.
            The lack of regulatory clarity was brought home by the discovery of
            the invasive snakehead fish in a Maryland pond. Federal regulations
            did not prohibit the importation or interstate transportation of
            this Asian fish and state law provided only a mild penalty for
            release of the fish, for which the statute of limitations had
            expired. Furthermore, state managers had no clear legal authority
            to eradicate the population that had established itself. This type
            of confusion results in invasive species—literally— slipping
            through the regulatory cracks and getting into the environment
            without anyone noticing.
        </p>
        <p align="left">
            <strong>SOUNDS</strong>
        </p>
        <p>
            The use of anthropogenic sound as a tool in the ocean has become
            enormously valuable for scientists,engineers,fishermen, and the
            military. It allows fishermen to locate schools of fish and to keep
            predators from raiding or becoming entangled in their nets. The use
            of sound also helps mariners detect icebergs and other
            obstructions, biologists study behavior changes in marine species,
            oceanographers map the bottom of the ocean floor, geologists find
            oil and gas, climatologists research global climate change, and the
            U.S. Navy detect submarines.
        </p>
        <p>
            Many marine species, including marine mammals, turtles, and fish,
            also rely on sound. They use vocalizations and their ability to
            hear to detect predators, prey, and each other. In the oceans, as
            on land, sound is essential for communication.
        </p>
        <p>
            Anthropogenic sound in the ocean is on the rise, mainly due to
            increased vessel traffic. Coastal development is bringing more
            pleasure craft, and globalization and international trade require
            more commercial vessels. In addition, the navies of the United
            States and other nations are increasingly using active sonar
            systems to patrol coastal waters for enemy submarines. Meanwhile,
            oil and gas operations on the outer continental shelf are expected
            to spread into deeper waters. Climate change, too, may have a
            significant effect on sound levels in the ocean. Not only does
            sound travel faster in warmer water, but also rising temperatures
            and melting ice at the poles may open new shipping channels in
            areas that have previously experienced little vessel traffic.
        </p>
        <p>
            Sound sources differ in both their intensity and frequency, and
            thus can have varied effects on species. Sounds in the same
            frequency ranges used by marine species can mask acoustic
            communication among animals and interfere with detection of prey
            and predators. High-intensity sounds can cause pain and, in some
            circumstances, tissue and organ damage. If the pressure resulting
            from the sound is intense enough, the animal can experience
            internal bleeding and subsequent death.
        </p>
        <p>
            A mass stranding of whales in 2000 heightened concerns about the
            effects of sound in the oceans. In March of that year, at least 17
            whales were stranded on beaches in the northern Bahama Islands.
            Most of the animals were alive when they stranded and eight of them
            were returned to the sea. The other nine animals died; pathology
            reports revealed bruising and internal organ damage. The stranding
            occurred about the time that ten U.S. Navy vessels were operating
            their midfrequency sonar systems nearby. Investigations conducted
            cooperatively by the Navy and the National Marine Fisheries Service
            suggested that the sonar transmissions were a critical factor in
            the strandings.
        </p>
        <p>
            Low-intensity sounds can disrupt behavior and cause hearing loss,
            ultimately affecting longevity, growth, and reproduction. Frequent
            or chronic exposure to both high- and low-intensity sounds may
            cause stress, which human and terrestrial animal studies indicate
            can affect growth, reproduction, and ability to resist disease.
            Impulse sounds, such as those produced by explosions and seismic
            air guns, may damage or destroy plankton, including fish eggs and
            larvae, as well as damage or destroy tissues and organs in higher
            vertebrates.
        </p>
        <p>
            The Marine Mammal Protection Act (MMPA), Endangered Species Act
            (ESA), and the National Environmental Policy Act (NEPA) all provide
            legal mechanisms for addressing sound. However, the MMPA and ESA
            apply only to marine mammals and endangered species, and are only
            capable of protecting individuals from particular sound-related
            projects, such as drilling operations or sonar activities. In
            addition, the federal government has recently proposed to exempt
            certain activities from environmental review under NEPA. Because
            review under these statutes is triggered only on a case-by-case
            basis and does not effectively address cumulative impacts on marine
            ecosystems, underwater sound as a source of potentially significant
            pollution in the marine environment has not received comprehensive
            treatment. A new policy framework is needed to adequately address
            this emerging pollution concern.
        </p>
        <p align="left">
            <strong>ACTION TO REDUCE MARINE POLLUTION</strong>
        </p>
        <p>
            For too long our oceans have been dumping grounds. Within
        </p>
        <p>
            U.S. waters, ecosystems are subjected to insults from nonpoint,
            unregulated point, and nontraditional types of pollution from both
            land- and ocean-based sources. Nutrients, toxins, cruise ship
            discharges, acoustic and biological pollution, and invasive species
            all harm marine ecosystems, and the legal regimes in place do not
            match the nature of today’s pollution threats. For each of these
            pollution sources, policy changes can and should be made as quickly
            as possible.
        </p>
        <p align="left">
            <strong>SUMMARY OF RECOMMENDATIONS</strong>
        </p>
        <p align="left">
            <strong>1. </strong>
            <strong>
                Revise, strengthen, and redirect pollution laws to focus on
                nonpoint source pollution on a watershed basis.
            </strong>
        </p>
        <p>
            EPA and the states should establish water quality standards for
            nutrients, especially nitrogen, as quickly as possible. EPA and the
            states should also ensure that water quality standards are in place
            for other pollutants—such as PAHs, PCBs, and heavy metals such as
            mercury—where these are identified as problematic on a
            watershed-by-watershed basis. Congress should amend the Clean Water
            Act to require the use of best management practices to control
            polluted runoff resulting from agriculture and development.
            Congress and the executive branch should provide substantial
            financial and technical support for the adoption of such practices.
            Congress should link the receipt of agricultural and other federal
            subsidies to compliance with the Clean Water Act. Finally, Congress
            and the Environmental Protection Agency should ensure that air
            emissions of nitrogen compounds, mercury, and other pollutants are
            reduced to levels that will result in a substantial reduction of
            their impact on marine ecosystems.
        </p>
        <p align="left">
            <strong>2. </strong>
            <strong>Address unabated point sources of pollution.</strong>
        </p>
        <p>
            Concentrated animal feeding operations should be brought into
            compliance with existing provisions in the CWA. Congress should
            enact legislation that regulates wastewater discharges from cruise
            ships under the CWA by establishing uniform minimum standards for
            discharges in all state waters and prohibiting discharges within
            the U.S. Exclusive Economic Zone that do not meet effluent
            standards. Congress should amend NISA to require ballast-water
            treatment for all vessels that travel in U.S. waters, and regulate
            ballast-water discharge through a permitting system under the CWA.
            Finally, the International Maritime Organization draft convention
            on ballast-water management should be finalized and its provisions
            implemented through appropriate U.S. laws.
        </p>
        <p align="left">
            <strong>3. </strong>
            <strong>
                Create a flexible framework to address emerging and
                nontraditional sources of pollution.
            </strong>
        </p>
        <p>
            A national electronic permitting system should be created under
            NISA to facilitate communication and track imports of live species
            that may result in aquatic introductions. Each state should
            inventory existing species and their historical abundance, in
            conjunction with the development of the regional ocean governance
            plans under the National Ocean Policy Act. Congress should provide
            adequate funding for developing statewide invasive species
            management plans that include provisions for inventorying,
            monitoring, and rapid response. With regard to sound, a
            comprehensive research and monitoring program should be developed
            to determine the effects of sound sources on living marine
            resources and ecosystems. Consideration should be given to
            requiring the utilization of best available control technologies,
            where the generation of sound has potential adverse effects.
            Finally, the environmental ramifications of any sound-producing
            project should be taken into formal consideration—pursuant to NEPA
            or other applicable statutes—at the planning stages of the project,
            before significant resources, time, and money have been devoted to
            its development.
        </p>
        <p align="left">
            <strong>4. Strengthen control of toxic pollutants.</strong>
        </p>
        <p>
            The U.S. should ratify the Stockholm Convention on Persistent
            Organic Pollutants (POPs), and implement federal legislation that
            allows for additions to the list of the “dirty dozen” chemicals. In
            concert with this effort, EPA should develop and lead a
            comprehensive monitoring program to quantify levels of particular
            toxic substances in designated ocean habitats and species, and
            sufficient resources should be devoted to studying the effects of
            toxins on marine species. This monitoring program should be
            coordinated with Food and Drug Administration [FDA] and EPA seafood
            contaminant advisory efforts, so that people know where their
            seafood comes from and what it contains.
        </p>
