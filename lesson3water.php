            <strong>CHAPTER 1 SOURCES OF BEACHWATER POLLUTION</strong>
        </p>
        <p>
            Most beach closings and advisories are based on monitoring that
            detects elevated levels of bacteria indicating the presence of
            microscopic disease-causing organisms from human and animal wastes.
            These wastes typically enter coastal waters from discharges of
            untreated or partially treated wastes from sewage treatment plants
            and sanitary sewers (such as combined sewer or sanitary sewer
            overflows), septic system failures, and stormwater runoff from
            urban, suburban, and rural areas. Beach closings and advisories can
            also be responses to other specific pollution events, such as an
            overflow from an animalwaste lagoon or an oil spill. In some
            coastal and Great Lakes counties, advisories are issued as a
            precautionary measure when storms occur because past experience
            demonstrates that rain carries pollution to swimming waters.
