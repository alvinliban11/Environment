    it possible to conserve fertilizer, stabilize soils, and improve water
    quality while reducing costs. Effective policies might include the
    establishment of national or international phosphorus markets and better
    tracking of phosphorus around the world, in order to ensure that some areas
    are not being overwhelmed by excess nutrients. Phosphorus markets could
    ensure that products such as manure are moved to where they can be used.
