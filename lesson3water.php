    For most of human history, groundwater was tapped mainly in arid regions
    where surface water was in short supply. From Egypt to Iran, ancient Middle
    Eastern civilizations used periscope-like conduits to funnel spring water
    from mountain slopes to nearby towns—a technology that allowed settlement
    to spread out from the major rivers. Over the centuries, as populations and
    cropland expanded, innovative well-digging techniques evolved in China,
    India, and Europe. Water became such a valuable resource that some cultures
    developed elaborate mythologies imbuing underground water and its seekers
    with special powers. In medieval Europe, people called water witches or
    dowsers were believed to be able to detect groundwater using a forked stick
    and mystical insight.
</p>
<p>
    In the second half of the 20<sup>th </sup>century, the soaring demand for
    water turned the dowsers’ modernday counterparts into a major industry.
    Today, major aquifers are tapped on every continent, and groundwater is the
    primary source of drinking water for more than 1.5 billion people
    worldwide. The aquifer that lies beneath the Huang-Huai-Hai plain in
    eastern China alone supplies drinking water to nearly 160 million people.
    Asia as a whole relies on its groundwater for nearly one-third of its
    drinking water supply. Some of the largest cities in the developing
    world—Jakarta, Dhaka, Lima, and Mexico City, among them—depend on aquifers
    for almost all their water. And in rural areas, where centralized water
    supply systems are undeveloped, groundwater is typically the sole source of
    water.
</p>
<p>
    More than 95 percent of the rural U.S. population depends on groundwater
    for drinking. A principal reason for the explosive rise in groundwater use
    since 1950 has been a dramatic expansion in irrigated agriculture. In
    India, the leading country in total irrigated area and the world’s third
    largest grain producer, the number of shallow tubewells used to draw
    groundwater surged from 3,000 in 1960 to 6 million in 1990. While India
    doubled the amount of its land irrigated by surface water between 1950 and
    1985, it increased the area watered by aquifers 113-fold. Today, aquifers
    supply water to more than half of India’s irrigated land. The United
    States, with the third highest irrigated area in the world, uses
    groundwater for 43 percent of its irrigated farmland.
</p>
<p>
    Worldwide, irrigation is by far the biggest drain on freshwater: it
    accounts for about 70 percent of the water we draw from rivers and wells
    each year.
</p>
<p>
    Other industries have been expanding their water use even faster than
    agriculture—and generating much higher profits in the process. On average,
    a ton of water used in industry generates roughly $14,000 worth of
    output—about 70 times as much profit as the same amount of water used to
    grow grain. Thus, as the world has industrialized, substantial amounts of
    water have been shifted from farms to more lucrative factories. Industry’s
    share of total consumption has reached 19 percent and is likely to continue
    rising rapidly. The amount of water available for drinking is thus
    constrained not only by a limited resource base, but by competition with
    other, more powerful users.
</p>
<p>
    And as rivers and lakes are stretched to their limits—many of them dammed,
    dried up, or polluted—we’re growing more and more dependent on groundwater
    for all these uses. In Taiwan, for example, the share of water supplied by
    groundwater almost doubled from 21 percent in 1983 to over 40 percent in
    1991. And Bangladesh, which was once almost entirely river- and
    stream-dependent, dug over a million wells in the 1970s to substitute for
    its badly polluted surfacewater supply. Today, almost 90 percent of its
    people use only groundwater for drinking.
</p>
<p>
    Even as our dependence on groundwater increases, the availability of the
    resource is becoming more limited. On almost every continent, many major
    aquifers are being drained faster than their natural rate of recharge.
    Groundwater depletion is most severe in parts of India, China, the United
    States, North Africa, and the Middle East. Under certain geological
    conditions, groundwater overdraft can cause aquifer sediments to compact,
    permanently shrinking the aquifer’s storage capacity. This loss can be
    quite considerable, and irreversible. The amount of water storage capacity
    lost because of aquifer compaction in California’s Central Valley, for
    example, is equal to more than 40 percent of the combined storage capacity
    of all humanmade reservoirs across the state.
</p>
<p>
    As the competition among factories, farms, and households intensifies, it’s
    easy to overlook the extent to which freshwater is also required for
    essential ecological services. It is not just rainfall, but groundwater
    welling up from beneath, that replenishes rivers, lakes, and streams. In a
    study of 54 streams in different parts of the country, the U.S. Geological
    Survey (USGS) found that groundwater is the source for more than half the
    flow, on average. The 492 billion gallons (1.86 cubic kilometers) of water
    aquifers add to U.S. surface water bodies each day is nearly equal to the
    daily flow of the Mississippi. Groundwater provides the base contribution
    for the Mississippi, the Niger, the Yangtze, and many more of the world’s
    great rivers—some of which would otherwise not be flowing year-round.
    Wetlands, important habitat for birds, fish, and other wildlife, are often
    largely groundwaterfed, created in places where the water table overflows
    to the surface on a constant basis. And while providing surface bodies with
    enough water to keep them stable, aquifers also help prevent them from
    flooding: when it rains heavily, aquifers beneath rivers soak up the excess
    water, preventing the surface flow from rising too rapidly and overflowing
    onto neighboring fields and towns. In tropical Asia, where the hot season
    can last as long as 9 months, and where monsoon rains can be very intense,
    this dual hydrological service is of critical value.
</p>
<p>
    Numerous studies have tracked the extent to which our increasing demand on
    water has made it a resource critical to a degree that even gold and oil
    have never been. It’s the most valuable thing on Earth. Yet, ironically,
    it’s the thing most consistently overlooked, and most widely used as a
    final resting place for our waste. And, of course, as contamination
    spreads, the supplies of usable water get tighter still.
</p>
<p align="left">
    <strong>TRACKING THE HIDDEN CRISIS</strong>
</p>
<p>
    In 1940, during the Second World War, the U.S. Department of the Army
    acquired 70 square kilometers [27 square miles] of land around Weldon
    Spring and its neighboring towns near St. Louis, Missouri. Where farmhouses
    and barns had been, the Army established the world’s largest TNT-producing
    facility. In this sprawling warren of plants, toluene (a component of
    gasoline) was treated with nitric acid to produce more than a million tons
    of the explosive compound each day when production was at its peak.
</p>
<p>
    Part of the manufacturing process involved purifying the TNT—washing off
    unwanted “nitroaromatic” compounds left behind by the chemical reaction
    between the toluene and nitric acid. Over the years, millions of gallons of
    this red-colored muck were generated. Some of it was treated at wastewater
    plants, but much of it ran off from the leaky treatment facilities into
    ditches and ravines, and soaked into the ground. In 1945, when the Army
    left the site, soldiers burned down the contaminated buildings but left the
    red-tinged soil and the rest of the site as they were. For decades, the
    site remained abandoned and unused.
</p>
<p>
    Then, in 1980, the U.S. Environmental Protection Agency (EPA) launched its
    “Superfund” program, which required the cleaning up of several sites in the
    country that were contaminated with hazardous waste. Weldon Spring made it
    to the list of sites that were the highest priority for cleanup. The Army
    Corps of Engineers was assigned the task, but what the Corps workers found
    baffled them. They expected the soil and vegetation around the site to be
    contaminated with the nitroaromatic wastes that had been discarded there.
    When they tested the groundwater, however, they found that the chemicals
    were showing up in people’s wells, in towns several miles from the site—a
    possibility that no one had anticipated, because the original pollution had
    been completely localized. Geologists determined that there was an enormous
    plume of contamination in the water below the TNT factory—a plume that over
    the previous 35 years had flowed through fissures in the limestone rock to
    other parts of the aquifer.
</p>
<p>
    The Weldon Spring story may sound like an exceptional case of clumsy
    planning combined with a particularly vulnerable geological structure. But
    in fact there is nothing exceptional about it all. Across the United
    States, as well as in parts of Europe, Asia, and Latin America, human
    activities are sending massive quantities of chemicals and pollutants into
    groundwater. This isn’t entirely new, of course; the subterranean world has
    always been a receptacle for whatever we need to dispose of—whether our
    sewage, our garbage, or our dead. But the enormous volumes of waste we now
    send underground, and the deadly mixes of chemicals involved, have created
    problems never before imagined.
</p>
<p>
    What Weldon Spring shows is that we can’t always anticipate where the
    pollution is going to turn up in our water, or how long it will be from the
    time it was deposited until it reappears. Because groundwater typically
    moves very slowly—at a speed of less than a foot a day, in some
    cases—damage done to aquifers may not show up for decades. In many parts of
    the world, we are only just beginning to discover contamination caused by
    practices of 30 or 40 years ago.
</p>
<p>
    Some of the most egregious cases of aquifer contamination now being
    unearthed date back to Cold War era nuclear testing and weapons-making, for
    example.
</p>
<p>
    And once it gets into groundwater, the pollution usually persists: the
    enormous volume, inaccessibility, and slow rate at which groundwater moves
    make aquifers virtually impossible to purify.
</p>
<p>
    As this covert crisis unfolds, we are barely beginning to understand its
    dimensions. Few countries track the health of their aquifers—their enormous
    size and remoteness make them extremely expensive to monitor. As the new
    century begins, even hydrogeologists and health officials have only a hazy
    impression of the likely extent of groundwater damage in different parts of
    the world. . . .
</p>
<p align="left">
    <strong>THE FILTER THAT FAILED: PESTICIDES IN YOUR WATER</strong>
</p>
<p>
    Pesticides are designed to kill. The first synthetic pesticides were
    introduced in the 1940s, but it took several decades of increasingly heavy
    use before it became apparent that these chemicals were injuring non-target
    organisms—including humans. One reason for the delay was that some groups
    of pesticides, such as organochlorines, usually have little effect until
    they bioaccumulate. Their concentration in living tissue increases as they
    move up the food chain. So eventually, the top predators—birds of prey, for
    example—may end up carrying a disproportionately high burden of the toxin.
    But bioaccumulation takes time, and it may take still more time before the
    effects are discovered. In cases where reproductive systems are affected,
    the aftermath of this chemical accumulation may not show up for a
    generation.
</p>
<p>
    Even when the health concerns of some pesticides were recognized in the
    1960s, it was easily assumed that the real dangers lay in the dispersal of
    these chemicals among animals and plants—not deep underground.
</p>
<p>
    It was assumed that very little pesticide would leach below the upper
    layers of soil, and that if it did, it would be degraded before it could
    get any deeper. Soil, after all, is known to be a natural filter, which
    purifies water as it trickles through. It was thought that industrial or
    agricultural chemicals, like such natural contaminants as rock dust, or
    leaf mold, would be filtered out as the water percolated through the soil.
</p>
<p>
    But over the past 35 years, this seemingly safe assumption has proved
    mistaken. Cases of extensive pesticide contamination of groundwater have
    come to light in farming regions of the United States, Western Europe,
    Latin America, and South Asia.
</p>
<p>
    What we now know is that pesticides not only leach into aquifers, but
    sometimes remain there long after the chemical is no longer used. DDT, for
    instance, is still found in U.S. waters even though its use was banned 30
    years ago. In the San Joaquin Valley of California, the soil fumigant DBCP
    (dibromochloropropane), which was used intensively in fruit orchards before
    it was banned in 1977, still lurks in the region’s water supplies. Of 4,507
    wells sampled by the USGS between 1971 and 1988, nearly a third had DBCP
    levels that were at least 10 times higher than allowed by the current
    drinking water standard.
</p>
<p>
    In places where organochlorines are still widely used, the risks continue
    to mount. After half a century of spraying in the eastern Indian states of
    West Bengal and Bihar, for example, the Central Pollution Control Board
    found DDT in groundwater at levels as high as 4,500 micrograms per
    liter—several thousand times higher than what is considered a safe dose.
</p>
<p>
    The amount of chemical that reaches groundwater depends on the amount used
    above ground, the geology of the region, and the characteristics of the
    pesticide itself. In some parts of the midwestern United States, for
    example, although pesticides are used intensively, the impermeable soils of
    the region make it difficult for the chemicals to percolate underground.
    The fissured aquifers of southern Arizona, Florida, Maine, and southern
    California, on the other hand, are very vulnerable to pollution—and these
    too are places where pesticides are applied in large quantities.
</p>
<p>
    Pesticides are often found in combination, because most farms use a range
    of toxins to destroy different kinds of insects, fungi, and plant diseases.
    The USGS detected two or more pesticides in groundwater at nearly a quarter
    of the sites sampled in its National Water Quality Assessment between 1993
    and 1995. In the Central Columbia Plateau aquifer, which extends over the
    states of Washington and Idaho, more than two-thirds of water samples
    contained multiple pesticides.
</p>
<p>
    Scientists aren’t entirely sure what happens when these chemicals and their
    various metabolites come together. We don’t even have standards for the
    many hundred <em>individual </em>pesticides in use—the EPA has drinking
    water standards for just 33 of these compounds—to say nothing of the
    infinite variety of toxic blends now trickling into the groundwater.
</p>
<p>
    While the most direct impacts may be on the water we drink, there is also
    concern about what occurs when the pesticideladen water below farmland is
    pumped back up for irrigation. One apparent consequence is a reduction in
    crop yields. In 1990, the now-defunct U.S. Office of Technology Assessment
    reported that herbicides in shallow groundwater had the effect of “pruning”
    crop roots, thereby retarding plant growth.
</p>
<p align="left">
    <strong>From Green Revolution to Blue Baby: </strong>
</p>
<p align="left">
    <strong>The Slow Creep of Nitrogen</strong>
</p>
<p>
    Since the early 1950s, farmers all over the world have stepped up their use
    of nitrogen fertilizers. Global fertilizer use has grown ninefold in that
    time. But the larger doses of nutrients often can’t be fully utilized by
    plants. A study conducted over a 140,000 square kilometer
    [54,054-square-mile] region of Northern China, for example, found that
    crops used on average only 40 percent of the nitrogen that was applied. An
    almost identical degree of waste was found in Sri Lanka. Much of the excess
    fertilizer dissolves in irrigation water, eventually trickling through the
    soil into underlying aquifers.
</p>
<p>
    Joining the excess chemical fertilizer from farm crops is the organic waste
    generated by farm animals, and the sewage produced by cities. Livestock
    waste forms a particularly potent tributary to the stream of excess
    nutrients flowing into the environment, because of its enormous volume. In
    the United States, farm animals produce 130 times as much waste as the
    country’s people do—with the result that millions of tons of cow and pig
    feces are washed into streams and rivers, and some of the nitrogen they
    carry ends up in groundwater. To this Augean [extremely difficult and
    distasteful] burden can be added the innumerable leaks and overflows from
    urban sewage systems, the fertilizer runoff from suburban lawns, golf
    courses, and landscaping, and the nitrates leaking (along with other
    pollutants) from landfills.
</p>
<p>
    There is very little historical information available about trends in the
    pollution of aquifers. But several studies show that nitrate concentrations
    have increased as fertilizer applications and population size have grown.
    In California’s San Joaquin– Tulare Valley, for instance, nitrate levels in
    groundwater increased 2.5 times between the 1950s and 1980s—a period in
    which fertilizer inputs grew six-fold.
</p>
<p>
    Levels in Danish groundwater have nearly tripled since the 1940s. As with
    pesticides, the aftermath of this multi-sided assault of excess nutrients
    has only recently begun to become visible, in part because of the slow
    speed at which nitrate moves
</p>
<p>
    underground.
</p>
<p>
    What happens when nitrates get into drinking water? Consumed in high
    concentrations—at levels above 10 milligrams (mg) per liter, but usually on
    the order of 100 mg/liter—they can cause infant methemoglobinemia, or
    so-called blue-baby syndrome. Because of their low gastric acidity, infant
    digestive systems convert nitrate to nitrite,which blocks the
    oxygen-carrying capacity of a baby’s blood, causing suffocation and death.
</p>
<p>
    Since 1945, about 3,000 cases have been reported worldwide—nearly half of
    them in Hungary, where private wells have particularly high concentrations
    of nitrates. Ruminant livestock such as goats, sheep, and cows, are
    vulnerable to methemoglobinemia in much the same way infants are, because
    their digestive systems also quickly convert nitrate to nitrite. Nitrates
    are also implicated in digestive tract cancers, although the
    epidemiological link is still uncertain.
</p>
<p>
    In cropland, nitrate pollution of groundwater can have a paradoxical
    effect. Too much nitrate can weaken plants’ immune systems, making them
    more vulnerable to pests and disease. So when nitrate-laden groundwater is
    used to irrigate crops that are also being fertilized, the net effect may
    be to reduce, rather than to increase production. This kind of
    overfertilizing makes wheat more susceptible to wheat rust, for example,
    and it makes pear trees more vulnerable to fire blight.
</p>
<p>
    In assembling studies of groundwater from around the world, we have found
    that nitrate pollution is pervasive—but has become particularly severe in
    the places where human population—and the demand for high food
    productivity—is most concentrated.
</p>
<p>
    In the northern Chinese counties of Beijing, Tianjin, Hebei, and Shandong,
    nitrate concentrations in groundwater exceeded 50 mg/liter in more than
    half of the locations studied. (The World Health Organization [WHO]
    drinking water guideline is 10 mg/liter.) In some places, the concentration
    had risen as high as 300 mg/liter. Since then, these levels may have
    increased, as fertilizer applications have escalated since the tests were
    carried out in 1995 and will likely increase even more as China’s
    population (and demand for food) swells, and as more farmland is lost to
    urbanization, industrial development, nutrient depletion, and erosion.
</p>
<p>
    Reports from other regions show similar results. The USGS found that about
    15 percent of shallow groundwater sampled below agricultural and urban
    areas in the United States had nitrate concentrations higher than the 10
    mg/liter guideline. In Sri Lanka, 79 percent of wells sampled by the
    British Geological Survey had nitrate levels that exceeded this guideline.
</p>
<p>
    Some 56 percent of wells tested in the Yucatán peninsula in Mexico had
    levels above 45 mg/liter. And the European Topic Centre on Inland Waters
    found that in Romania and Moldova, more than 35 percent of the sites
    sampled had nitrate concentrations higher than 50 mg/liter.
</p>
<p align="left">
    <strong>From Tank of Gas to Drinking Glass: </strong>
</p>
<p align="left">
    <strong>The Pervasiveness of Petrochemicals</strong>
</p>
<p>
    Drive through any part of the United States, and you’ll probably pass more
    gas stations than schools or churches.As you pull into a station to fill
    up, it may not occur to you that you’re parked over one of the most
    pervasive threats to groundwater: an underground storage tank (UST) for
    petroleum.
</p>
<p>
    Many of these tanks were installed two or three decades ago and, having
    been left in place long past their expected lifetimes, have rusted through
    in places—allowing a steady leakage of gasoline into the ground. Because
    they’re underground, they’re expensive to dig up and repair, so the leakage
    in some cases continues for years.
</p>
<p>
    Petroleum and its associated chemicals—benzene, toluene, and gasoline
    additives such as MTBE [methyl tertiary-butyl ether]—constitute the most
    common category of groundwater contaminant found in aquifers in the United
    States.
</p>
<p>
    Many of these chemicals are also known or suspected to be cancer-causing.
    In 1998, the EPA found that over 100,000 commercially owned petroleum USTs
    were leaking, of which close to 18,000 are known to have contaminated
    groundwater. In Texas, 223 of 254 counties report leaky USTs, resulting in
    a silent disaster that, according to the EPA, “has affected, or has the
    potential to affect, virtually every major and minor aquifer in the state.”
    Household tanks, which store home heating oil, are a problem as well.
    Although the household tanks aren’t subject to the same regulations and
    inspections as commercial ones, the EPA says they are “undoubtedly
    leaking.”
</p>
<p>
    Outside the United States, the world’s ubiquitous petroleum storage tanks
    are even less monitored, but spot tests suggest that the threat of leakage
    is omnipresent in the industrialized world. In 1993, petroleum giant Shell
    reported that a third of its 1,100 gas stations in the United Kingdom were
    known to have contaminated soil and groundwater.
</p>
<p>
    Another example comes from the eastern Kazakh town of Semipalatinsk, where
    6,460 tons of kerosene have collected in an aquifer under a military
    airport, seriously threatening the region’s water supplies.
</p>
<p>
    The widespread presence of petrochemicals in groundwater constitutes a kind
    of global malignancy, the danger of which has grown unobtrusively because
    there is such a great distance between cause and effect. An underground
    tank, for example, may take years to rust; it probably won’t begin leaking
    until long after the people who bought it and installed it have left their
    jobs. Even after it begins to leak, it may take several more years before
    appreciable concentrations of chemicals appear in the aquifer—and it will
    likely be years beyond that before any health effects show up in the local
    population. By then, the trail may be decades old. So it’s quite possible
    that any cancers occurring today as a result of leaking USTs might
    originate from tanks that were installed half a century ago. At that time,
    there were gas tanks sufficient to fuel 53 million cars in the world; today
    there are enough to fuel almost 10 times that number.
</p>
<p align="left">
    <strong>FROM SEDIMENT TO SOLUTE: </strong>
</p>
<p align="left">
    <strong>THE EMERGING THREAT OF NATURAL CONTAMINANTS</strong>
</p>
<p>
    In the early 1990s, several villagers living near India’s West Bengal
    border with Bangladesh began to complain of skin sores that wouldn’t go
    away. A researcher at Calcutta’s Jadavpur University, Dipankar Chakraborti,
    recognized the lesions immediately as early symptoms of chronic arsenic
    poisoning. In later stages, the disease can lead to gangrene, skin cancer,
    damage to vital organs, and eventually, death. In the months that followed,
    Chakraborti began to get letters from doctors and hospitals in Bangladesh,
    who were seeing streams of patients with similar symptoms. By 1995, it was
    clear that the country faced a crisis of untold proportions, and that the
    source of the poisoning was water from tubewells, from which 90 percent of
    the country gets its drinking water.
</p>
<p>
    Experts estimate that today, arsenic in drinking water could threaten the
    health of 20 to 60 million Bangladeshis— up to half the country’s
    population—and another 6 to 30 million people in West Bengal As many as 1
    million wells in the region may be contaminated with the heavy metal at
    levels between 5 and 100 times the WHO drinking water guideline of 0.01
    mg/liter.
</p>
<p>
    How did the arsenic get into groundwater? Until the early 1970s, rivers and
    ponds supplied most of Bangladesh’s drinking water. Concerned about the
    risks of water-borne disease, the WHO and international aid agencies
    launched a well-drilling program to tap groundwater instead. However, the
    agencies, not aware that soils of the Ganges aquifers are naturally rich in
    arsenic, didn’t test the sediment before drilling tubewells. Because the
    effects of chronic arsenic poisoning can take up to 15 years to appear, the
    epidemic was not addressed until it was well under way.
</p>
<p>
    Scientists are still debating what chemical reactions released the arsenic
    from the mineral matrix in which it is naturally bound up. Some theories
    implicate human activities. One hypothesis is that as water was pumped out
    of the wells, atmospheric oxygen entered the aquifer, oxidizing the iron
    pyrite sediments, and causing the arsenic to dissolve. An October 1999
    article in the scientific journal <em>Nature </em>by geologists from the
    Indian Institute of Technology suggests that phosphates from fertilizer
    runoff and decaying organic matter may have played a role. The nutrient
    might have spurred the growth of soil microorganisms, which helped to
    loosen arsenic from sediments.
</p>
<p>
    Salt is another naturally occurring groundwater pollutant that is
    introduced by human activity. Normally, water in coastal aquifers empties
    into the sea. But when too much water is pumped out of these aquifers, the
    process is reversed: seawater moves inland and enters the aquifer. Because
    of its high salt content, just 2 percent of seawater mixed with freshwater
    makes the water unusable for drinking or irrigation. And once salinized, a
    freshwater aquifer can remain contaminated for a very long time. Brackish
    aquifers often have to be abandoned because treatment can be very
    expensive.
</p>
<p>
    In Manila, where water levels have fallen 50 to 80 meters [55 to 88 yards]
    because of overdraft, seawater has flowed as far as 5 kilometers [3 miles]
    into the Guadalupe aquifer that lies below the city. Saltwater has traveled
    several kilometers inland into aquifers beneath Jakarta and Madras, and in
    parts of the U.S. state of Florida. Saltwater intrusion is also a serious
    problem on islands such as the Maldives and Cyprus, which are very
    dependent on aquifers for water supply. Fluoride is another natural
    contaminant that threatens millions in parts of Asia. Aquifers in the drier
    regions of western India, northern China, and parts of Thailand and Sri
    Lanka are naturally rich in fluoride deposits. Fluoride is an essential
    nutrient for bone and dental health, but when consumed in high
    concentrations, it can lead to crippling damage to the neck and back, and
    to a range of dental problems. The WHO estimates that 70 million people in
    northern China, and 30 million in northwestern India are drinking water
    with high fluoride levels.
</p>
<p align="left">
    <strong>A CHEMICAL SOUP</strong>
</p>
<p>
    With just over a million residents, Ludhiana is the largest city in Punjab,
    India’s breadbasket state. It is also an important industrial town, known
    for its textile factories, electroplating industries, and metal foundries.
    Although the city is entirely dependent on groundwater, its wells are now
    so polluted with industrial and urban wastes that the water is no longer
    safe to drink. Samples show high levels of cyanide, cadmium, lead, and
    pesticides. “Ludhiana City’s groundwater is just short of poison,” laments
    a senior official at India’s Central Ground Water Board.
</p>
<p>
    Like Ludhiana’s residents, more than a third of the planet’s people live
    and work in densely settled cities, which occupy just 2 percent of the
    Earth’s land area. With the labor force thus concentrated, factories and
    other centers of employment also group together around the same urban
    areas. Aquifers in these areas are beginning to mirror the increasing
    density and diversity of the human activity above them. Whereas the
    pollutants emanating from hog farms or copper mines may be quite
    predictable, the waste streams flowing into the water under cities contain
    a witch’s brew of contaminants.
</p>
<p>
    Ironically, a major factor in such contamination is that in most places
    people have learned to dispose of waste—to remove it from sight and
    smell—so effectively that it is easy to forget that the Earth is a closed
    ecological system in which nothing permanently disappears. The methods
    normally used to conceal garbage and other waste—landfills, septic tanks,
    and sewers—become the major conduits of chemical pollution of groundwater.
    In the United States, businesses drain almost 2 million kilograms [4.4
    million lbs] of assorted chemicals into septic systems each year,
    contaminating the drinking water of 1.3 million people. In many parts of
    the developing world, factories still dump their liquid effluents onto the
    ground and wait for it to disappear.
</p>
<p>
    In the Bolivian city of Santa Cruz, for example, a shallow aquifer that is
    the city’s main water source has had to soak up the brew of sulfates,
    nitrates, and chlorides dumped over it.And even protected landfills can be
    a potent source of aquifer pollution: the EPA found that a quarter of the
    landfills in the U.S.
</p>
<p>
    state of Maine, for example, had contaminated groundwater.
</p>
<p>
    In industrial countries, waste that is too hazardous to landfill is
    routinely buried in underground tanks. But as these caskets age, like
    gasoline tanks, they eventually spring leaks. In California’s Silicon
    Valley, where electronics industries store assorted waste solvents in
    underground tanks, local groundwater authorities found that 85 percent of
    the tanks they inspected had leaks. Silicon Valley now has more Superfund
    sites—most of them affecting groundwater—than any other area its size in
    the country. And 60 percent of the United States’ liquid hazardous waste—34
    billion liters of solvents, heavy metals, and radioactive materials—is
    directly injected into the ground. Although the effluents are injected
    below the deepest source of drinking water, some of these wastes have
    entered aquifers used for water supplies in parts of Florida, Texas, Ohio,
</p>
<p>
    and Oklahoma.
</p>
<p>
    Shenyang, China, and Jaipur, India, are among the scores of cities in the
    developing world that have had to seek out alternate supplies of water
    because their groundwater has become unusable. Santa Cruz has also
    struggled to find clean water. But as it has sunk deeper wells in pursuit
    of pure supplies, the effluent has traveled deeper into the aquifer to
    replace the water pumped out of it. In places where alternate supplies
    aren’t easily available, utilities will have to resort to increasingly
    elaborate filtration set-ups to make the water safe for drinking. In
    heavily contaminated areas, hundreds of different filters may be necessary.
    At present, utilities in the U.S. Midwest spend $400 million each year to
    treat water for just one chemical—atrazine, the most commonly detected
    pesticide in U.S. groundwater. When chemicals are found in unpredictable
    mixtures, rather than discretely, providing safe water may become even more
    expensive.
</p>
<p align="left">
    <strong>ONE BODY, MANY WOUNDS</strong>
</p>
<p>
    The various incidents of aquifer pollution described may seem isolated. A
    group of wells in northern China have nitrate problems; another lot in the
    United Kingdom are laced with benzene.
</p>
<p>
    In each place it might seem that the problem is local and can be contained.
    But put them together, and you begin to see a bigger picture emerging.
    Perhaps most worrisome is that we’ve discovered as much damage as we have,
    despite the very limited monitoring and testing of underground water. And
    because of the time-lags involved—and given our high levels of chemical use
    and waste generation in recent decades—what’s still to come may bring even
    more surprises.
</p>
<p>
    Some of the greatest shocks may be felt in places where chemical use and
    disposal has climbed in the last few decades, and where the most basic
    measures to shield groundwater have not been taken. In India, for example,
    the Central Pollution Control Board (CPCB) surveyed 22 major industrial
    zones and found that groundwater in every one of them was unfit for
    drinking. When asked about these findings, CPCB chairman D. K. Biswas
    remarked, “The result is frightening, and it is my belief that we will get
    more shocks in the future.”
</p>
<p>
    Jack Barbash, an environmental chemist at the U.S. Geological Survey,
    points out that we may not need to wait for expensive tests to alert us to
    what to expect in our groundwater. “If you want to know what you’re likely
    to find in aquifers near Shanghai or Calcutta, just look at what’s used
    above ground,” he says. “If you’ve been applying DDT to a field for 20
    United States years, for example, that’s one of the chemicals you’re likely
    to find in the underlying groundwater.” The full consequences of today’s
    chemical-dependent and waste-producing economies may not become apparent
    for another generation, but Barbash and other scientists are beginning to
    get a sense of just how serious those consequences are likely to be if
    present consumption and disposal practices continue.
</p>
<p align="left">
    <strong>CHANGING COURSE</strong>
</p>
<p>
    Farmers in California’s San Joaquin Valley began tapping the area’s
    seemingly boundless groundwater store in the latenineteenth century. By
    1912, the aquifer was so depleted that the water table had fallen by as
    much as 400 feet [122 meters] in some places. But the farmers continued to
    tap the resource to keep up with demand for their produce. Over time, the
    dehydration of the aquifer caused its clay soil to shrink, and the ground
    began to sink—or as geologists put it, to “subside.” In some parts of the
    valley, the ground has subsided as much as 29 feet [9 meters]— cracking
    foundations, canals, and aqueducts.
</p>
<p>
    When the San Joaquin farmers could no longer pump enough groundwater to
    meet their irrigation demands, they began to bring in water from the
    northern part of the state via the California Aqueduct. The imported water
    seeped into the compacted aquifer, which was not able to hold all of the
    incoming flow. The water table then rose to an abnormally high level,
    dissolving salts and minerals in soils that had not been previously
    submerged. The salty groundwater, welling up from below, began to poison
    crop roots. In response, the farmers installed drains under irrigated
    fields—designed to capture the excess water and divert it to rivers and
    reservoirs in the valley so that it wouldn’t evaporate and leave its salts
    in the soil.
</p>
<p>
    But the farmers didn’t realize that the rocks and soils of the region
    contained substantial amounts of the mineral selenium, which is toxic at
    high doses. Some of the selenium leached into the drainage water, which was
    routed to the region’s wetlands. It wasn’t until the mid-1980s that the
    aftermath of this solution became apparent: ecologists noticed that
    thousands of waterfowl in the nearby Kesterson Reservoir were dying of
    selenium poisoning.
</p>
<p>
    Hydrological systems are not easy to outmaneuver, and the San Joaquin
    farmers’ experience serves as a kind of cautionary tale. Each of their
    stopgap solutions temporarily took care of an immediate obstacle, but led
    to a longer-term problem more severe than the original one.“Human
    understanding has lagged one step behind the inflexible realities governing
    the aquifer system,” observes USGS hydrologist Frank Chapelle.
</p>
<p>
    Around the world, human responses to aquifer pollution thus far have
    essentially reenacted the San Joaquin Valley farmers’ well-meaning but
    inadequate approach. In many places, various authorities and industries
    have fought back the contamination leak by leak, or chemical by
    chemical—only to find that the individual fixes simply don’t add up. As we
    line landfills to reduce leakage, for instance, tons of pesticide may be
    running off nearby farms and into aquifers. As we mend holes in underground
    gas tanks, acid from mines may be seeping into groundwater.
</p>
<p>
    Clearly, it’s essential to control the damage we’ve already inflicted, and
    to protect communities and ecosystems from the poisoned fallout. But given
    what we already know—that damage done to aquifers is mostly irreversible,
    that it can take years before groundwater pollution reveals itself, that
    chemicals react synergistically, and often in unanticipated ways—it’s now
    clear that a patchwork response isn’t going to be effective. Given how much
    damage this pollution inflicts on public health, the environment, and the
    economy once it gets into the water, it’s critical that emphasis be shifted
    from filtering out toxins to not using them in the first place. Andrew
    Skinner, who heads the International Association of Hydrogeologists, puts
    it this way: “Prevention is the only credible strategy.”
</p>
<p>
    To do this requires looking not just at individual factories, gas stations,
    cornfields, and dry cleaning plants, but at the whole social, industrial,
    and agricultural systems of which these businesses are a part.
</p>
<p>
    The ecological untenability of these systems is what’s really poisoning the
    world’s water. It is the predominant system of high-input agriculture, for
    example, that not only shrinks biodiversity with its vast monocultures, but
    also overwhelms the land—and the underlying water—with its massive
    applications of agricultural chemicals. It’s the system of car-dominated,
    geographically expanding cities that not only generates unsustainable
    amounts of climate-disrupting greenhouse gases and acid rain-causing air
    pollutants, but also overwhelms aquifers and soils with petrochemicals,
    heavy metals, and sewage. An adequate response will require a thorough
    overhaul of each of these systems.
</p>
<p>
    Begin with industrial agriculture. Farm runoff is a leading cause of
    groundwater pollution in many parts of Europe, the United States, China,
    and India. Lessening its impact calls for adopting practices that sharply
    reduce this runoff—or, better still, that require far smaller inputs to
    begin with. In most places, current practices are excessively wasteful. In
    Colombia, for example, growers spray flowers with as much as 6,000 liters
    [1,585 gallons] of pesticide per hectare [a hectare is about 2.5 acres]. In
    Brazil, orchards get almost 10,000 liters [2,642 gallons] per hectare.
</p>
<p>
