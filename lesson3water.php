    <p>
        The Mississippi River occupies a mythic place in the American
        imagination, in part because it is so huge. At any given moment, on
        average,about 2,100 billion liters of water are flowing across the Big
        Muddy’s broad bottom.If you were to dive about 35 feet down and lie on
        that bottom, you might feel a sense of awe that the whole river was on
        top of you. But in one very important sense, you’d be completely
        wrong.At any point in time,only 1 percent of the water in the
        Mississippi River system is in the part of the river that flows
        downstream to the Gulf of Mexico.The other 99 percent lies beneath the
        bottom,locked in massive strata of rock and sand.
    </p>
    <p>
        This is a distinction of enormous consequence. The availability of
        clean water has come to be recognized as perhaps the most critical of
        all human security issues facing the world in the next
        quarter-century—and what is happening to water buried under the bottoms
        of rivers, or under our feet, is vastly different from what happens to
        the “surface” water of rivers, lakes, and streams. New research finds
        that contrary to popular belief, it is groundwater that is most
        dangerously threatened. Moreover, the Mississippi is not unique in its
        ratio of surface to underground water; worldwide, 97 percent of the
        planet’s liquid freshwater is stored in aquifers.
    </p>
</div>
<br clear="all"/>
<p>
    In the early centuries of civilization, surface water was the only source
    we needed to know about. Human population was less than a tenth of one
    percent the size it is now; settlements were on river banks; and the water
    was relatively clean. We still think of surface water as being the main
    resource. So it’s easy to think that the problem of contamination is mainly
    one of surface water: it is polluted rivers and streams that threaten
    health in times of flood, and that have made waterborne diseases a major
    killer of humankind. But in the past century, as population has almost
    quadrupled and rivers have become more depleted and polluted, our
    dependence on pumping groundwater has soared—and as it has, we’ve made a
    terrible discovery. Contrary to the popular impression that at least the
    waters from our springs and wells are pure, we’re uncovering a pattern of
    pervasive pollution there too. And in these sources, unlike rivers, the
    pollution is generally irreversible.
</p>
<p>
    This is largely the work of another hidden factor: the rate of groundwater
    renewal is very slow in comparison with that of surface water. It’s true
    that some aquifers recharge fairly quickly, but the average recycling time
    for groundwater is 1,400 years, as opposed to only 20 days for river water.
    So when we pump out groundwater, we’re effectively removing it from
    aquifers for generations to come. It may evaporate and return to the
    atmosphere quickly enough, but the resulting rainfall (most of which falls
    back into the oceans) may take centuries to recharge the aquifers once
    they’ve been depleted. And because water in aquifers moves through the
    Earth with glacial slowness, its pollutants continue to accumulate.
</p>
<p>
    Unlike rivers, which flush themselves into the oceans, aquifers become
    sinks for pollutants, decade after decade—thus further diminishing the
    amount of clean water they can yield for human use.
</p>
<p>
    Perhaps the largest misconception being exploded by the spreading water
    crisis is the assumption that the ground we stand on—and what lies beneath
    it—is solid, unchanging, and inert. Just as the advent of climate change
    has awakened us to the fact that the air over our heads is an arena of
    enormous forces in the midst of titanic shifts, the water crisis has
    revealed that, slow-moving though it may be, groundwater is part of a
    system of powerful hydrological interactions—between earth, surface water,
    sky, and sea—that we ignore at our peril. A few years ago, reflecting on
    how human activity is beginning to affect climate, Columbia University
    scientist Wallace Broecker warned, “The climate system is an angry beast
    and we are poking it with sticks.” A similar statement might now be made
    about the system under our feet. If we continue to drill holes into
    it—expecting it to swallow our waste and yield freshwater in return—we may
    be toying with an outcome no one could wish.
</p>
