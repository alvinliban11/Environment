            developing economic incentives for local conservation. For example,
            in 1996, the International Monetary Fund (IMF) and the World
            Wildlife Fund (WWF) established a program to relieve poor countries
            of debt. More than 40 countries have benefited by agreeing to
            direct some of their savings toward environmental programs in the
            “Debtfor-Nature” swap programs.<sup>19</sup>
        </p>
