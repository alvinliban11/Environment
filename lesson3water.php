    <p align="left">
        <strong>THE NATURE OF THE PROBLEM</strong>
    </p>
    <p>
        The images of the <em>Exxon Valdez </em>oil spill in Prince William
        Sound, Alaska, in 1989, and the sight of trash washing up with the
        seaweed on our favorite beaches are all too familiar.
    </p>
    <p>
        What we are less aware of,however,is the amount of pollution that
        travels daily from each of our lawns, vehicle tailpipes, driveways, and
        the fields where our food is produced into our coastal waters. A recent
        [2002] study by the National Research Council found that the same
        amount of oil released in the <em>Exxon Valdez </em>spill—10.9 million
        gallons—washes off our coastal lands and into the surrounding waters
        every eight months. The Mississippi River, which drains nearly 40
        percent of the continental United States, carries an estimated 1.5
        million metric tons of nitrogen into the Gulf of Mexico each year.
        Overall, the amount of nitrogen released into coastal waters along the
        Atlantic seaboard and the Gulf of Mexico from anthropogenic, or
        human-induced sources, has increased about fivefold since the
        preindustrial era.
    </p>
    <p>
        The consequences of this polluted runoff are most acute along the
        coasts, where more than 13,000 beaches were closed
    </p>
</div>
<br clear="all"/>
<p>
    or under pollution advisories in 2001. Two-thirds of our estuaries and bays
    are either moderately or severely degraded from eutrophication. However,
    pollution’s reach extends far beyond our major cities. Scientists report
    that killer whales have higher PCB levels in their blubber than any animal
    on the planet and that fish species that live their entire lives far out in
    the Pacific are too contaminated with mercury to be safe to eat.
</p>
<p>
    These are the signs of a silent crisis in our oceans.
</p>
<p>
    Fortunately, we have set a good precedent for addressing water pollution.
    In response to public outcry over such environmental calamities as the
    burning of the Cuyahoga River in Ohio, Congress passed the Clean Water Act
    (CWA) in 1972. The law requires the U.S. Environmental Protection Agency
    (EPA) to establish national technology standards and science-based criteria
    for water quality protection. The states then control identifiable sources
    of pollution by issuing pollution discharge permits based on these
    technology and water quality requirements.
</p>
<p>
    Efforts resulting from the provisions of the Clean Water Act have succeeded
    in removing the worst pollution from the rivers and lakes that surround
    us.Some coastal waters,such as those off Los Angeles and San Diego, have
    dramatically improved. There, inputs of many pollutants have been reduced
    by 90 percent or more over a 25-year period, leading to the recovery of
    kelp beds, fish communities, and certain seabird populations.
</p>
<p>
    But in the 30 years since the Clean Water Act was passed, as scientific
    knowledge and experience has improved, the focus of our concern has
    shifted. Although controlling point sources remains critical, the subtler
    problem of nonpoint sources has moved to the fore. In our oceans, now, we
    are experiencing a crisis as great as a burning river. It is a crisis we
    must address through changes in both policy and commitment.
</p>
<p>
    Today, nonpoint sources present the greatest pollution threat to our oceans
    and coasts. Every acre of farmland and stretch of road in a watershed is a
    nonpoint source. Every treated lawn in America contributes toxins and
    nutrients to our coasts. Nonpoint pollutants include excess fertilizers and
    pesticides used in farming, oil and grease from paved surfaces, bacteria
    and nutrients from livestock manure, and acidic or toxic drainage from
    abandoned mines.
</p>
<p>
    The current legal framework is ill equipped to address this threat. Rather
    than confronting individual cases, the situation requires that we apply new
    thinking about the connection between the land and the sea, and the role
    watersheds play in providing habitat and reducing pollution.
</p>
<p>
    One of the major nonpoint pollutants is nitrogen, a nutrient that
    encourages plant growth. Although nitrogen is essential to life, in excess
    it can significantly damage and alter ecosystems. In fact, scientists now
    believe that nutrients are the primary pollution threat to living marine
    resources. Most nitrogen in the oceans arrives from nonpoint sources,
    including storm runoff from roads and agricultural fields, and airborne
    nitrogen emitted from power plants and car tailpipes.
</p>
<p>
    We have also learned that marine species accumulate toxic substances. From
    single-celled marine life to top ocean predators, including humans, toxic
    substance levels in body tissue increase as predators consume contaminated
    prey. In addition, new forms of pollution are emerging. Non-native species,
    introduced by accident or design, have proliferated to stress entire
    ecosystems, crowding out native species, altering habitat, and in some
    instances, introducing disease. And human-generated sound in the oceans is
    affecting marine life in ways we are just beginning to understand.
</p>
<p>
    Finally, we have not fully dispensed with the problem of point source
    pollution. Legal loopholes and poor enforcement allow significant point
    sources of pollution to go unregulated. These include cruise ships,
    ballast-water discharge from ships, and concentrated animal feeding
    operations. Animal feeding operations alone produce more than three times
    the amount of waste that people do—about 500 million tons of manure every
    year.
</p>
<p>
    Through witness testimony from around the country, commissioned papers, and
    its own research, the [Pew Oceans]
</p>
<p>
    Commission investigated five types of pollution—nutrients, toxic
    substances, cruise ship discharges, invasive species, and anthropogenic
    sound. It reviewed the current state of our laws and changes necessary to
    control new and overlooked sources
</p>
<p>
    of pollution.
</p>
<p align="left">
    <strong>WHEN NUTRIENTS POLLUTE</strong>
</p>
<p>
    The immediate cause of the 1991 event that killed one million menhaden in
North Carolina’s Neuse River was a single-celled creature called<em>Pfiestera piscicida</em>. Known as the killer alga,    <em>P. piscicida </em>can emit a strong neurotoxin when in the presence of
    schools of fish. It feasts on the dead and dying fish, reproduces, and then
settles back into the sediment. Scientists have found that    <em>P. piscicida </em>thrives in coastal waters that are enriched with
    nutrients such as phosphorous and nitrogen.
</p>
<p>
    The Neuse River outbreak was linked by analyses of the event to nutrients
    flowing from manure lagoons and other agricultural sources in the
    watershed.
</p>
<p>
    We are degrading the environment along our coasts. Nutrient pollution has
    been linked to harmful algal blooms, such as the <em>Pfiestera </em>
    outbreak. It has also been linked to dead zones, such as the area in the
    Gulf of Mexico that appears annually and has reached the size of
    Massachusetts (more than 8,000 square miles [20,720 km<sup>2</sup>]). In
    addition, this pollution results in the loss of seagrass and kelp beds,
    destruction of coral reefs, and lowered biodiversity in estuaries and
    coastal habitats. The incidence of harmful algal blooms along the United
    States coastlines increased from 200 in the decade of the 1970s to 700 in
    the 1990s, and now includes almost every coastal state in the U.S. One
    bloom off the coast of Florida was implicated in the deaths of more than
    150 manatees.
</p>
<p>
    The continued loss of wetlands is further evidence of this trend in
    degradation. Wetlands serve a critical function as natural filters that
    remove nutrients before they can reach the sea, but they are being lost at
    the rate of approximately 60,000 acres per year. If current practices of
    nutrient input and habitat destruction continue, nitrogen inputs to U.S.
    coastal waters in 2030 may be 30 percent higher than at present.
</p>
<p>
    When too many nutrients—particularly nitrogen—enter the marine environment,
    the result is eutrophication—the overenrichment of the water that
    stimulates extraordinary growth of phytoplankton and attached algae.
    Phytoplankton blooms can be so dense they block the light needed by corals
    and by submerged vegetation such as seagrasses. Severe light deprivation
    will kill the plants and cause corals to expel the algae they host, which
    leads to coral bleaching.
</p>
<p>
    After the phytoplankton die and sink to the ocean floor, bacteria decompose
    them. Decomposition pulls oxygen from the water, leaving the remaining
    plants and animals oxygenstarved. Areas with little oxygen, called hypoxic,
    are unable to support fish and shrimp populations, and the stress of
    hypoxia can make them more vulnerable to invasive species, disease, and
    mortality events. In addition to the well-known hypoxic dead zone at the
    mouth of the Mississippi River, hypoxic zones have developed in 39
    estuaries around the U.S. coast.
</p>
<p>
    Of the myriad sources of nutrient pollution, agriculture is the most
    significant. Nitrogen in fertilizer is easily dissolved in and transported
    by water. Animal wastes are also nitrogen rich, and are generally applied
    to farmland, where the nitrogen can be washed into water bodies by
    rainstorms. Aggravating this problem, tile drainage systems constructed to
    collect and shuttle excess water from fields—particularly common in the
    corn and soybean fields of the Midwest—provide an expressway for nitrogen
    flowing into waterways.
</p>
<p>
    Until recently, atmospheric deposition—the settling of airborne pollutants
    on the land and water—has been an overlooked source of nitrogen pollution
    in coastal waters. It is now clear that it is widespread and quantitatively
    important in some regions. Most atmospheric deposition of nitrogen
    originates as nitrogen oxide emissions from power plants and automobiles,
    and ammonia gas released from animal wastes.
</p>
<p>
    In addition to nonpoint sources, there are major point sources of
    nutrients, particularly concentrated animal feeding operations (CAFOs).
    Most animal wastes from CAFOs are stored in open lagoons, which can be
    larger than five and a half football fields and contain 20 to 45 million
    gallons of wastewater. If not properly managed, lagoons can leach nutrients
    and other substances into waterways and overflow during rainstorms. The
    liquid effluent, rich in nitrogen and phosphorous, is sprayed onto
    agricultural fields as fertilizer, often at many times the amount needed
    for crop growth. On a day-to-day basis, the over-application of animal
    waste to land, which fouls waterways with runoff, is a significant
    environmental problem.
</p>
<p>
    Although they are regulated under the CWA [Clean Water Act], CAFOs have
    largely avoided pollution restrictions because of exemptions in outdated
    regulations and the states’ failure to enforce permitting requirements. Of
    the approximately 15,500 operations that meet EPA’s definition triggering
    regulation, less than 30 percent have permits, reducing the government’s
    and the public’s ability to monitor and control CAFO-related pollution. EPA
    recently revised its CAFO regulations, which now expressly require all
    CAFOs over a certain size to obtain a point source discharge permit. EPA’s
    new regulations require CAFOs to develop a nutrient management plan by
    2006, but EPA has not set enforceable standards for these plans, which will
    be written by the operators and not subject to government or public review.
    In exchange for developing and implementing a nutrient management plan,
    CAFOs are shielded from liability for pollution that is discharged off the
    facility’s land application area.
</p>
<p>
    Regardless of its source, nitrogen has become one of the most pervasive and
    harmful pollutants in coastal waters. A revitalized pollution policy must
    reflect this understanding.
</p>
<p align="left">
    <strong>TOXIC WATERS</strong>
</p>
<p>
    When the <em>Exxon Valdez </em>ran aground in Alaska and spilled its oil
    cargo in March 1989, scientists, managers, and hundreds of volunteers
    rushed to rescue thousands of seabirds and sea otters. They picked the
    birds off soiled beaches and attempted to clean their plumage before the
    birds lost their ability to float and to stay warm. In the end, some 30,000
    seabirds perished as well as 1,000 or more sea otters, and untold numbers
    of fish.
</p>
<p>
    Congress has since passed the Oil Pollution Act to reduce the risk of
    similar tanker accidents.
</p>
<p>
    New evidence strongly suggests that components of crude oil, called
    polycyclic aromatic hydrocarbons (PAHs), persist in the marine environment
    for years and are toxic to marine life at concentrations in the low
    parts-per-billion range. Chronic exposure to PAHs can affect development,
    increase susceptibility to disease, and jeopardize normal reproductive
    cycles in many
</p>
<p>
    marine species.
</p>
<p>
    PAHs represent just one class of toxic substances that threaten the health
    of marine species and of humans who depend upon them for food. The
    Commission focused on three toxic substances of particular concern: PAHs,
    PCBs (polycholorinated biphenyls), and heavy metals like mercury. These
    substances are both pervasive and persistent. They are decomposed very
    slowly, if at all, by bacteria, and do not leave the marine environment
    quickly or completely. Although now banned in domestic manufacture of
    electrical transformers, plastics, paints, and other materials, PCBs are
    still present in many imported materials and at many industrial and
    military sites. Mercury levels are on the rise in some regions. Nearly 80
    percent of the mercury in the marine environment arrives as air emissions
    from coal-fired power plants and other combustion sources, some of them
    overseas.
</p>
<p>
    Landfills, urban runoff, ocean dumpsites, ocean vessels, and the burning of
    fossil fuels are just a few of the pathways that bring toxic substances to
    the oceans.
</p>
<p>
    Toxic compounds enter marine food chains either directly from the water or
    from concentrated deposits in sediments. Organisms accumulate toxic
    substances in their tissues, where they may be passed up the food
    chain.Some of these compounds are concentrated at each step in the chain.
    The ocean’s top predatory fish and marine mammals therefore often have the
    highest concentrations of toxic compounds in their bodies. Killer whales,
    walruses, and tuna are among those most contaminated.
</p>
<p>
    Accumulated toxic substances disrupt hormone cycles, cause birth defects,
    suppress the immune system, and cause disorders resulting in cancer,
    tumors, and genetic abnormalities. In some instances, accumulated toxic
    substances can even cause death in marine animals.
</p>
<p>
    The contamination of certain commercial species may pose particular
problems for humans. Recent studies sponsored by    <em>The Mobile Register </em>indicated that the presence of methylmercury
    (the bio-available form of mercury, and the form most prevalent in fish) in
    several species of fish in the Gulf of Mexico, including ling, amberjack,
    and redfish, may be so great that Food and Drug Administration standards
    would prohibit selling them to the public. In 2001, of the 2,618 fish
    advisories issued in U.S. waters, almost 75 percent were for mercury
    contamination. In Alaska and other polar regions, the evidence of
    correlation between increased toxic loads and declining health in humans
    and animals alike is mounting.
</p>
<p>
    The Arctic and Antarctic are hard hit by certain persistent toxins,
    especially heavy metals and organochlorines, which include PCBs, due to the
    peculiar mechanisms by which these compounds are preferentially transported
    to the polar regions. Airborne toxins are repeatedly deposited and
    volatilized as they are swept by atmospheric circulation from their points
    of origin toward the polar regions. This process is known as the
    grasshopper effect because the substances “hop” from their sources to their
    ultimate repositories in the polar marine environment.
</p>
<p>
    Not enough is being done to address the dangers that toxic substances pose
    to marine species and to humans. There are no water quality standards for
    PAHs under the CWA, no ambient air quality standards for mercury under the
    Clean Air Act (CAA), no systematic monitoring of toxins levels in most
    species consumed by humans, and there is insufficient effort to clean up
    toxic contaminants in sensitive marine environments. These policy
    shortcomings should be addressed without delay.
</p>
<p align="left">
    <strong>CRUISE SHIPS</strong>
</p>
<p>
    Cruise ships can offer spectacular views and unparalleled wildlife
    experiences. For many Americans, cruises provide their only exposure to the
    oceans and marine wildlife, and the popularity of this activity is
    increasing. In recent years the cruise ship industry has grown at an
    average annual rate of eight percent, and expansion continues. In 2001, the
    North American cruise industry set a record when it carried 8.4 million
    passengers. In San Francisco Bay, a new cruise terminal is expected to more
    than double the number of ship visits per year. Cruise ships make frequent
    stops in Florida, the Caribbean, along the West Coast, Maine, and Alaska.
</p>
<p>
    While taking a cruise can provide an invaluable experience for passengers,
    cruise ships can pose a particular risk to the very environments they seek
    to explore. With as many as 5,000 people onboard, a cruise ship is akin to
    a floating city, where people go about many of the same activities as they
    do at home: showering, cleaning, cooking. In addition, cruise ships offer
    such amenities as photo developing, hairdressing, and dry cleaning. The
    waste from these activities, however, is not regulated like waste produced
    from cities.
</p>
<p>
    In one week, a typical cruise ship generates 210,000 gallons of black water
    (sewage), 1,000,000 gallons of gray water (shower, sink, dishwashing
    water), 37,000 gallons of oily bilge water, more than eight tons of solid
    waste, millions of gallons of ballast water containing potential invasive
    species, and toxic wastes from dry cleaning and photo processing
    laboratories. This effluent,when discharged untreated—as too often happens—
    delivers human pathogens, nutrients, and hazardous substances directly to
    the marine environment. The wastewater pollution from these ships is
    compounded by air pollution from burning trash and fuel emissions that
    enter the marine environment via atmospheric deposition.
</p>
<p>
    Despite the fact that cruise ships discharge waste from a single source,
    they are exempted from regulation under the CWA point source permitting
    system.
</p>
<p>
    The CWA allows the discharge of untreated black water anywhere beyond three
    miles from shore, and does not require any treatment of gray or ballast
    water. Only in Alaskan waters are cruise ships required to meet federal
    effluent standards; treat gray water discharges; and monitor, record, and
    report discharges to state and federal authorities. In addition, the CWA
    authorizes the U.S. Coast Guard to inspect the discharge logs and pollution
    control equipment aboard ships. However, Coast Guard officers are not
    required to test discharges for
</p>
<p>
    compliance.
</p>
<p>
    The CWA and the Act to Prevent Pollution from Ships together regulate bilge
    water, which must be run through an oilwater separator before it is
    discharged. The National Invasive Species Act encourages all oceangoing
    vessels to exchange ballast water but does not require them to do so. The
    air emissions from ships are covered under the CAA amendments of 1990, but
    the EPA has yet to impose regulations.
</p>
<p>
    In short, the legal regime that covers cruise ships is complex but not
    comprehensive. Unless we take greater steps to control discharges and
    reduce pollution, we will continue to harm the very places we love to
    visit.
</p>
