<p align="left">
    <strong>“HEAVEN IS UNDER OUR FEET”</strong>
</p>
<p>
    Throughout human history, people have feared that the skies would be the
    source of great destruction. During the Cold War, industrial nations feared
    nuclear attack from above, and spent vast amounts of their wealth to avert
    it. Now some of that fear has shifted to the threats of atmospheric climate
    change: of increasing ultraviolet radiation through the ozone hole, and the
    rising intensity of global warming-driven hurricanes and typhoons. Yet, all
    the while, as the worldwide pollution of aquifers now reveals, we’ve been
    slowly poisoning ourselves from beneath. What lies under terra firma may,
    in fact, be of as much concern as what happens in the firmament above.
</p>
<p>
    The ancient Greeks created an elaborate mythology about the Underworld, or
    Hades, which they described as a dismal, lifeless place completely lacking
    the abundant fertility of the world above. Science and human experience
    have taught us differently.
</p>
<p>
    Hydrologists now know that healthy aquifers are essential to the life above
    ground—that they play a vital role not just in providing water to drink,
    but in replenishing rivers and wetlands and, through their ultimate effects
    on rainfall and climate, in nurturing the life of the land and air as
    well.But ironically,our neglectful actions now threaten to make the Greek
    myth a reality after all.To avert that threat now will require taking to
    heart what the hydrologists have found. As Henry David Thoreau observed a
    century-and-a-half ago, “Heaven is under our feet, as well as over our
    heads.”
</p>
