<p align="left">
    <strong>MESSING WITH EUTROPHICATION</strong>
</p>
<p>
    Under natural conditions, eutrophication can be a centuries-long aging
    process for some lakes. When human caused (scientists call this “cultural”
    eutrophication), it can happen in a few years. In many cases, cultural
    eutrophication can be reversed by greatly reducing the amount of phosphorus
    entering the water.
</p>
<p>
    Phosphorus is just one of many nutrients that plants need to survive and
    grow. However, it is particularly important to lake systems because it is
    the limiting nutrient. In other words, even when plants have all other
    nutrients in sufficient quantities, they will often lack phosphorus. When
    phosphorus is added to lake water, plants may suddenly grow very rapidly
    because all the necessary nutrients are now present. But the excessive
    growth of one organism may mean the death of another. When thick blooms of
    algal growth block sunlight from reaching the plants below, the decay of
    dead algae uses up the available oxygen in the water, suffocating fish and
    sometimes causing whole populations of species to be lost. Eutrophication
    not only makes a lake look and smell putrid; it also substantially changes
    the way the aquatic ecosystem works. It can change the plant community, the
    food web, and the chemistry of a lake beyond recognition.
</p>
<p>
    Although scientists have been studying eutrophication since the turn of the
    twentieth century, cultural eutrophication was not recognized as a
    widespread international problem until the 1950s and ’60s, when it became a
    matter of growing concern in both North America’s Great Lakes region and in
    much of Europe. “Lakes became green and smelly, devoid of fish, unfit as
    sources of drinking water and unimaginable as places of recreation,” writes
    John Emsley in
    <em>
        The Thirteenth Element: A Sordid Tale of Murder, Fire, and Phosphorus
    </em>
    . Scientists did not always know that phosphorus was the culprit in
    situations of cultural eutrophication. But in the late 1960s, a research
    team headed by David Schindler, a professor of Ecology at the University of
    Alberta in Edmonton, Canada, conducted a revealing experiment on a
    lake—known as “Lake 227”—in northwestern Ontario. The team divided the lake
    into two halves. They enriched one half with nitrogen and carbon, and the
    other half with phosphorus and carbon. The phosphate-enriched basin rapidly
    became eutrophic, while the other basin remained unchanged.
</p>
<p>
    Phosphorus became widely recognized as the source of fresh water
    eutrophication problems, and efforts were begun to reduce phosphorus inputs
    to aquatic ecosystems. In most of the developed world, sewage was diverted
    around lakes, and most “point” sources (direct outflows of phosphorus-rich
    effluents from specific farms or processing plants) were cut off.
</p>
<p>
    By the 1990s, phosphates had been removed from most detergents, as well.Yet
    eutrophication persisted, in part because lakes are efficient recyclers of
    phosphorus. Under conditions of low oxygen at the lake bottom, phosphorus
    lying in the sediment reenters the water column and is once again available
    to be used by algae. The problem also remained because scientists had
    overlooked another source of phosphorus entering lakes— the non-point
    source runoff from surrounding lands—which proves much more difficult to
    control. Currently, non-point runoff from uplands is the main source of
    phosphorus to most aquatic systems in the developed world.
</p>
<p>
    Worldwide, eutrophication in lakes, rivers, and estuaries appears to be
    increasing. In the Gulf of Mexico, there is now a large hypoxic zone, or
    “dead” zone, where the low oxygen content of the water has led to massive
    die-offs in ocean species. The cause has been traced to nutrient runoff
    from the grain-growing states of the midwestern United States, carried to
    the Gulf by the Mississippi River. In New Zealand, an increase in dairy
    farming and fertilizer use has worsened nutrient pollution in hundreds of
    shallow lakes and streams. By 1994, the most recent date for which we have
    worldwide data, significant eutrophication problems were being reported in
    54 percent of all lakes and reservoirs in Southeast Asia, 53 percent of
    those in Europe, 48 percent in North America, 41 percent in South America,
    and 28 percent in Africa.
</p>
<p>
    The growing recognition that eutrophication is more widespread than we
    might have initially imagined led us to look for a larger-scale perspective
    on the issue and its causes. We knew that the immediate cause is human
    impact on phosphorus cycling: increased phosphorus in soils and increase
    erosion causes increased erosion to lakes. However, we wanted to understand
    the eutrophication problem from a global perspective because we believed
    that this perspective might lead to more effective long-term policies to
    reduce phosphorus load to aquatic ecosystems. Studying phosphorus increase
    in soils, as opposed to waiting until fish begin dying off in lakes, can be
    an effective preventive measure. So far, the systematic assessment of
    phosphorus in upland soils has not become a standard of the eutrophication
    management process.
</p>
<p align="left">
    <strong>MANAGEMENT: THE GLOBAL PICTURE</strong>
</p>
<p>
    Despite widespread controlling of point source pollution, treatment of
    sewage, and elimination of phosphates from most soaps and detergents,
    eutrophication continues to worsen as a result of human activity worldwide.
    What to do?
</p>
<p>
    The phosphorus in agricultural soils could take decades to draw down by
    reducing use of fertilizers. During that time, changes in farm practices,
    urban expansion, or climate change could accelerate erosion and—despite the
    lower input of phosphorus to soil—increase the rate at which phosphorus
    moves from the soil into aquatic ecosystems. By the time authorities see
    enough impairment of lakes to want to take action upstream, the fate of the
    lakes downstream may already have been sealed. By the same logic, however,
    the long time lags associated with soil phosphorus buildup also mean that
    action now can prevent expensive and persistent eutrophication problems in
    the future.
</p>
<p>
    Efforts to control phosphorus runoff have increased dramatically in recent
    years. Still, most policies and regulations have approached such runoff as
    a problem of the particular lake, river reach, or estuary, rather than as
    part of a larger pattern. These efforts have generally involved reducing
    nearby fertilizer and manure use, limiting erosion, or removing algae from
    the water directly. In Lake Mendota, the city regularly runs a floating
    lawn mower–like machine that strips algae and other aquatic plants from the
    water.
</p>
<p>
    To implement the long-term solution by reducing the phosphorus stored in
    upland soil is more difficult. Because upland runoff originates from more
    dispersed sources, it is very difficult to pinpoint the landowners who are
    responsible. One consequence is that in the United States, programs are
    usually voluntary rather than mandatory. Effectiveness is limited,
