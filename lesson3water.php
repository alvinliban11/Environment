        <strong>
            THE IMPORTANCE OF FOREST PROTECTED AREAS TO DRINKING WATER
        </strong>
    </p>
    <p align="left">
        <strong>Introduction: What Do City Dwellers Need?</strong>
    </p>
    <p>
        In the past 100 years the world population tripled, but water use for
        human purposes multiplied sixfold! Water is, in theory, a
        quintessentially renewable resource. Most of the world’s surface is
        covered in water and over much of the world it falls, unbidden and with
        great regularity, from the skies. Yet, the carelessness and profligacy
        with which water resources have been used, the speed of human
        population growth and the increasing per capita demands for water
        together mean that provision of adequate, safe supplies of water is now
        a major source of concern, expense and even international tension. At
        the World Summit on Sustainable Development in Johannesburg in 2002,
        over 80 per cent of the participating decision-makers identified water
        as a key issue to be addressed by Heads of State from countries
        throughout the world.
    </p>
    <p>
        Overall, the greatest human requirement for freshwater resources is for
        crop irrigation, particularly in places where farming takes place in
        arid regions and in the great rice paddy fields of Asia. Municipal
        water—the focus of the current study—accounts for less than a tenth of
        human water use. But the need for clean drinking water is of critical
        importance to the growing proportion of the world’s population that
        live in cities. Wherever a breakdown in water supply occurs, because of
        disasters like earthquakes, floods, wars or civil unrest, immediate and
        acute problems occur and reliance on contaminated water results in the
        rapid spread of diseases like cholera and infant diarrhoea.
    </p>
    <p>
        Unfortunately, for many people there is no need for a disaster to make
        them dependent on unclean drinking water. Today, around half of the
        world’s population lives in towns and cities, and of this urban
        population one third, an estimated one billion people, live without
        clean water or adequate sanitation, despite these services widely being
        regarded as basic prerequisites of a decent life. These one billion
        extreme havenots are unevenly distributed around the world. Regionally,
        it has been estimated that 700 million people in urban Asia, or half
        the urban population, do not have adequate water supplies; nor do 150
        million people in Africa, again about 50 per cent of the city dwellers;
        with a further 120 million people, about 30 per cent of the urban
        population, lacking clean water in Latin America and the Caribbean.
        Many people die each year as a direct result. Annually, 2.2 million
        deaths, four per cent of all fatalities worldwide, can be attributed to
        inadequate supplies of clean water and sanitation.
    </p>
    <p>
        These problems are likely to increase in the future as the current
        rapid processes of population growth and urbanisation continue. The
        average size of the world’s 100 largest cities grew from around 0.2
        million in 1800 to 6.2 million in 2000. In 1900, there were estimated
        to be just 43 cities worldwide with a population of over half a
        million, by 1990 this figure had risen to around 800 cities
        worldwide—of which some 270 had more than one million and 14 had over
        10 million. These trends are likely to continue for some time. Most
        current estimates suggest that the world’s population will grow by two
        billion people over the next 30 years and another billion in the
        following 20 years. Virtually, all of these increases will be in
        developing countries, the bulk of which will occur in urban areas. In
        India, for example, World Bank forecasts are that demand for water in
        the urban and industrial sectors is likely to increase by 135 percent
        over the next 40 years.
    </p>
    <p>
        In many arid countries, there is already an acute supply shortage.
        World water withdrawals rose sixfold over the last century. It has been
        estimated that humanity now uses 54 per cent of accessible runoff, a
        figure that could rise to 70 per cent by 2005. For several countries,
        current reliance on nonrenewable (or only very slowly renewable)
        groundwater sources masks a problem that could rapidly become more
        acute as these are exhausted.Because of population growth,the average
        annual per capita availability of renewable water resources is
        projected to fall from 6,600 cubic metres [8,633 cubic yards] today to
        4,800 cubic metres [6,278 cubic yards] in 2025. In 1998, 28 countries
        experienced water stress or scarcity (defined when available water is
        lower than 1,000 cubic meters [1,308 cubic yards] per person per year).
        By 2025, this number is predicted to rise to 56. As the number of
        people in urban areas grows, so does the demand for water, food and for
        irrigation in agricultural areas close to the city adding further
        pressures on water resources.
    </p>
    <p>
        The demand for water, along with increasing pressures on water from
        pollution, urbanisation and overexploitation of aquatic resources, is
        also creating a biodiversity crisis in
    </p>
    <p>
        freshwaters.
    </p>
    <p>
        Although future supply problems are expected, with a few notable
        exceptions the current shortfall in clean water for city dwellers is
        seldom to do with a real lack of supply but more related to poor
        distribution, inadequate treatment and to some extent also poor
        education and a lack of understanding about the problems. For example,
        up to 50 per cent of the urban water in many African cities is being
        wasted through leakage, theft or is otherwise unaccounted for.
        (Conversely Melbourne, after a seven year period of extreme drought, is
        still supplying its citizens with some of the best quality drinking
        water in the world.) Efforts are being made to address these problems.
        Over the past 20 years for instance more than 2.4 billion people have
        gained access to water supply and 600 million to sanitation. The United
        Nations Millennium Summit in 2000 agreed to reduce halve the 1.1
        billion people who do not have access to safe water by 2015, as part of
        its Millennium Development Goals.
    </p>
    <p>
        Cities therefore face immediate problems of access to clean water and
        sanitation and mounting problems of supply. In recent years, increasing
        interest has been taken in the opportunities for maintaining urban
        water supplies (and perhaps even more importantly water quality)
        through management of natural resources.Unfortunately,the links often
        come into focus when something goes wrong—most commonly when resource
        management upstream has downstream impacts in terms of changes in water
        supply, increased flooding and reduced water quality. The majority of
        the world’s population live downstream of forested watersheds and
        therefore are susceptible to the costs of watershed degradation.At the
        same time, 28 percent of the world’s forest areas are in mountains, and
        mountains are the source of some 60 to 80 percent of the world’s
        freshwater resources. Hence the importance of this report.
    </p>
    <p>
        The protected areas and protected forests identified below all play a
        role in providing drinking water for the world’s biggest cities. More
        often than not this water also helps feed the people, through
        irrigation of crops, provide electricity through hydro-electric plants,
        and has a recreation, aesthetic and even religious function. Of course
        protected areas are just one tool in a range of watershed conservation
        models, that can have costs of their own, but it is hoped that by
        highlighting their role this report will add to the growing literature
        extolling the benefits of long-term protection to some of the world’s
        most important resource areas.
    </p>
