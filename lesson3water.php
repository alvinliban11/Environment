            based on monitoring that detected bacteria levels exceeding
            beachwater quality standards. Six percent were precautionary due to
            rain that was known to carry pollution into coastal waters, 4
            percent of beach closings and advisories were issued in response to
            a known pollution event (without relying solely on monitoring
            results), and 2 percent were due to other sources, such as dredging
            and algal blooms. The percentages may add up to more than 100
            percent because more than one cause may have contributed to a
            closing or advisory. For example, some closings/advisories begin as
