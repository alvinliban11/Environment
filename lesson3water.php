    farms and copper mines. To save the water in time requires the same
    fundamental restructuring of the global economy as does the stabilizing of
    the climate and biosphere as a whole—the rapid transition from a
    resource-depleting, oil- and coal-fueled, high-input industrial and
    agricultural economy to one that is based on renewable energy, compact
    cities, and a very light human footprint.
