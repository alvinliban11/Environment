    <p align="left">
        <strong>
            Where Rivers Are Born: The Scientific Imperative for Defending
            Small Streams and Wetlands
        </strong>
    </p>
    <p align="left">
        from the American Rivers and Sierra Club
    </p>
    <p>
        Our nation’s network of rivers, lakes and streams originates from
        amyriad of small streams and wetlands,many so small they do not appear
        on any map. Yet these headwater streams and wetlands exert critical
        influences on the character and quality of downstream waters. The
        natural processes that occur in such headwater systems benefit humans
        by mitigating flooding,maintaining water quality and quantity,
        recycling nutrients, and providing habitat for plants and animals. This
        paper summarizes the scientific basis for understanding that the health
        and productivity of rivers and lakes depends upon intact small streams
        and wetlands.
    </p>
    <p>
        Historically, federal agencies have interpreted the protections of the
        Clean Water Act to cover all the waters of the United States, including
        small streams and wetlands. Despite this, many of these ecosystems have
        been destroyed by agriculture, mining, development and other human
        activities. The extent to which small streams and wetlands should
        remain under the protection of the Clean Water Act is currently (2003)
        under consideration in federal agencies and Congress. Extensive
        scientific studies document the significance of these small systems and
        form the basis for this paper. Further references are provided at the
        end of the document.
    </p>
    <p>
        We know from local/regional studies that small, or headwater, streams
        make up at least 80 percent of the nation’s stream network. However,
        scientists’ abilities to extend these local and regional studies to
        provide a national perspective is hindered by the absence of a
        comprehensive database that catalogs the full extent of streams in the
        United States. The topographic maps most commonly used to trace stream
        networks do not show most of the nation’s headwater streams and
        wetlands. Thus, such maps do not provide detailed enough information to
        serve as a basis for stream protection and management. Scientists often
        refer to the benefits humans receive from the
    </p>
</div>
<br clear="all"/>
<p>
    natural functioning of ecosystems as ecosystem services. The special
    physical and biological characteristics of intact small streams and
    wetlands provide natural flood control, recharge groundwater, trap
    sediments and pollution from fertilizers, recycle nutrients, create and
    maintain biological diversity, and sustain the biological productivity of
    downstream rivers, lakes and estuaries. These ecosystem services are
    provided by seasonal as well as perennial streams and wetlands. Even when
    such systems have no visible overland connections to the stream network,
    small streams and wetlands are usually linked to the larger network through
    groundwater. Small streams and wetlands offer an enormous array of habitats
    for plant, animal and microbial life. Such small freshwater systems provide
    shelter, food, protection from predators, spawning sites and nursery areas,
    and travel corridors through the landscape. Many species depend on small
    streams and wetlands at some point in their life history. For example,
    headwater streams are vital for maintaining many of America’s fish species,
    including trout and salmon. Both perennial and seasonal streams and
    wetlands provide valuable habitat. Headwater streams and wetlands also
    provide a rich resource base that contributes to the productivity of both
    local food webs and those farther downstream. However, the unique and
    diverse biota of headwater systems is increasingly imperiled. Human-induced
    changes to such waters, including filling streams and wetlands, water
    pollution, and the introduction of exotic species, can diminish the
    biological diversity of such small freshwater systems, thereby also
    affecting downstream rivers and streams.
</p>
<p>
    Because small streams and wetlands are the source of the nation’s fresh
    waters, changes that degrade these headwater systems affect streams, lakes,
    and rivers downstream. Landuse changes in the vicinity of small streams and
    wetlands can impair the natural functions of such headwater systems.
    Changes in surrounding vegetation, development that paves and hardens soil
    surfaces, and the total elimination of some small streams reduces the
    amount of rainwater, runoff and snowmelt the stream network can absorb
    before flooding. The increased volume of water in small streams scours
    stream channels, changing them in a way that promotes further flooding.
    Such altered channels have bigger and more frequent floods. The altered
    channels are also less effective at recharging groundwater, trapping
    sediment, and recycling nutrients. As a result, downstream lakes and rivers
    have poorer water quality, less reliable water flows, and less diverse
    aquatic life. Algal blooms and fish kills can become more common, causing
    problems for commercial and sport fisheries. Recreational uses may be
    compromised. In addition, the excess sediment can be costly, requiring
    additional dredging to clear navigational channels and harbors and
    increasing water filtration costs for municipalities and industry.
</p>
<p>
    The natural processes that occur in small streams and wetlands provide
    Americans with a host of benefits, including flood control, adequate
    highquality water, and habitat for a variety of plants and animals.
    Scientific research shows that healthy headwater systems are critical to
    the healthy functioning of downstream streams, rivers, lakes and estuaries.
    To provide the ecosystem services that sustain the health of our nation’s
    waters, the hydrological, geological, and biological characteristics of
    small streams and wetlands require protection. . . .
</p>
<p align="left">
    <strong>SMALL STREAMS AND WETLANDS </strong>
</p>
<p align="left">
    <strong>PROVIDE BENEFICIAL ECOSYSTEM SERVICES</strong>
</p>
<p>
    Natural processes that occur in small streams and wetlands provide humans
    with a host of benefits, including flood control, maintenance of water
    quantity and quality, and habitat for a variety of plants and animals. For
    headwater streams and wetlands to provide ecosystem services that sustain
    the health of our nation’s waters, the hydrological, geological and
    biological components of stream networks must be intact.
</p>
<p align="left">
    <strong>Small Streams and Wetlands Provide Natural Flood Control</strong>
</p>
<p>
    Floods are a natural part of every river. In times past, waters of the
    Mississippi River routinely overtopped its banks.
</p>
<p>
    Floodwaters carried the sediment and nutrients that made the Mississippi
    Delta’s soil particularly suitable for agriculture. But floods can also
    destroy farms, houses, roads and bridges.
</p>
<p>
    When small streams and wetlands are in their natural state, they absorb
    significant amounts of rainwater, runoff and snowmelt before flooding.
    However, when a landscape is altered, such as by a landslide or large
    forest fire or a housing development, the runoff can exceed the absorption
    capacity of small streams. Moreover, the power of additional water coursing
    through a channel can change the channel itself. Humans often alter both
    landscape and stream channels in ways that result in larger and more
    frequent floods downstream.
</p>
<p>
    A key feature of streams and rivers is their shape. Unlike a concrete
    drainage ditch, a natural streambed does not present a smooth surface for
    water flow. Natural streambeds are rough and bumpy in ways that slow the
    passage of water. Particularly in small narrow streams, friction produced
    by a stream’s gravel bed, rocks, and dams of leaf litter and twigs slows
    water as it moves downstream. Slower moving water is more likely to seep
    into a stream’s natural water storage system—its bed and banks—and to
    recharge groundwater. Slower moving water also has less power to erode
    stream banks and carry sediment and debris downstream.
</p>
<p>
    In watersheds that are not carefully protected against impacts of land
    development, stream channels often become enlarged and incised from
    increased runoff. Changed channels send water downstream more quickly,
    resulting in more flooding. For example, after forests and prairies in
    Wisconsin watersheds were converted to agricultural fields, the size of
    floods increased. This change in land use had altered two parts of the
    river systems’ equation: the amount of runoff and shape of the stream
    channel. Cultivation destroyed the soil’s natural air spaces that came from
    worm burrows and plant roots. The resulting collapse of the soil caused
    more rainfall to run off into streams instead of soaking into the ground.
    Additional surface runoff then altered the stream channels, thereby
    increasing their capacity to carry large volumes of water quickly
    downstream. These larger volumes flow downstream at much higher velocity,
    rather than soaking into the streambed.
</p>
<p>
    Urbanization has similar effects; paving previously-vegetated areas leads
    to greater storm runoff, which changes urban stream channels and ultimately
    sends water more quickly downstream. Covering the land with impermeable
    surfaces, such as roofs, roads, and parking lots, can increase by several
    times the amount of runoff from a rainstorm. If land uses change near
    headwater streams, effects are felt throughout the stream network. In an
    urban setting, runoff is channeled into storm sewers, which then rapidly
    discharge large volumes of water into nearby streams. The additional water
    causes the stream to pick up speed, because deeper water has less friction
    with the streambed. The faster the water moves, the less it can soak into
    the streambed and banks. Faster water also erodes channel banks and beds,
    changing the shape of a channel. The effect is magnified downstream,
    because larger rivers receive water from tens, sometimes hundreds, of small
    headwater basins. When such changes are made near headwater streams,
    downstream portions of the stream network experience bigger and more
    frequent flooding.
</p>
<p>
    As regions become more urbanized, humans intentionally alter many natural
    stream channels by replacing them with storm sewers and other artificial
    conduits. When larger, smoother conduits are substituted for narrow,
    rough-bottomed natural stream channels, flood frequency increases
    downstream. For example, three decades of growth in storm sewers and paved
    surfaces around Watts Branch Creek, Maryland more than tripled the number
    of floods and increased average annual flood size by 23 percent.
</p>
<p align="left">
    <strong>Small Streams and Wetlands Maintain Water Supplies</strong>
</p>
<p>
    Headwater systems play a crucial role in ensuring a continual flow of water
    to downstream freshwater ecosystems. Water in streams and rivers comes from
    several sources: water held in the soil, runoff from precipitation, and
    groundwater. Water moves between the soil, streams and groundwater.
    Wetlands, even those without any obvious surface connection to streams, are
    also involved in such exchanges by storing and slowly releasing water into
    streams and groundwater, where it later resurfaces at springs. Because of
    these interactions, groundwater can contribute a significant portion of
    surface flow in streams and rivers; conversely, surface waters can also
    recharge groundwater. If connections between soil, water, surface waters,
    and groundwater are disrupted, streams, rivers, and wells can run dry.
    Two-thirds of Americans obtain their drinking water from a water system
    that uses surface water. The remaining one-third of the population relies
    on groundwater sources. The quality and amount of water in both of these
    sources respond to changes in headwater streams.
</p>
<p>
    USGS estimates that, on average, from 40 to 50 percent of water in streams
    and larger rivers comes from groundwater. In drier regions or during dry
    seasons, as much as 95 percent of a stream’s flow may come from
    groundwater. Thus, the recharge process that occurs in unaltered headwater
    streams and wetlands both moderates downstream flooding in times of high
    water and maintains stream flow during dry seasons.
</p>
<p>
    Headwater streams and wetlands have a particularly important role to play
    in recharge. These smallest upstream components of a river network have the
    largest surface area of soil in contact with available water, thereby
    providing the greatest opportunity for recharge of groundwater. Moreover,
    water level in headwater streams is often higher than the water table,
    allowing water to flow through the channel bed and banks into soil and
    groundwater. Such situations occur when water levels are high, such as
    during spring snowmelt or rainy seasons. During dry times, the situation in
    some reaches of the stream network, particularly those downstream, may
    reverse, with water flowing from the soil and groundwater through the
    channel banks and bed into the stream. This exchange of water from the soil
    and groundwater into the stream maintains stream flow. However, if land-use
    changes increase the amount of precipitation that runs off into a stream
    rather than soaking into the ground, the recharge process gets
    short-circuited. This increased volume of stream water flows rapidly
    downstream rather than infiltrating into soil and groundwater. The
    consequence is less overall groundwater recharge, which often results in
    less water in streams during drier seasons.
</p>
<p>
    Therefore, alteration of small streams and wetlands disrupts the quantity
    and availability of water in a stream and river system. Protecting
    headwater streams and wetlands is important for maintaining water levels
    needed to support everything from fish to recreational boating to
    commercial ship traffic.
</p>
<p align="left">
    <strong>Small Streams and Wetlands Trap Excess Sediment</strong>
</p>
<p>
    Headwater systems retain sediment. Like the flow of water, movement of
    sediment occurs throughout a river network. Thus, how a watershed is
    managed and what kinds of land uses occur there have substantial impact on
    the amount of sediment delivered to larger rivers downstream. Increased
    sediment raises water purification costs for municipal and industrial
    users, requires extensive dredging to maintain navigational channels, and
    degrades aquatic habitats. Intact headwater streams and wetlands can
    modulate the amount of sediment transported to downstream ecosystems.
</p>
<p>
    Runoff from rain, snowmelt and receding floodwaters can wash soil, leaves
    and twigs into streams, where the various materials get broken up into
    smaller particles or settle out. If natural vegetation and soil cover are
    disturbed by events and activities such as fires, farming or construction,
    runoff increases, washing more materials into streams. At the same time,
    the increased velocity and volume of water in a stream cause erosion within
    the streambed and banks themselves, contributing additional sediment to the
    stream system. Moreover, the faster, fuller stream can carry more and
    larger chunks of sediment further downstream.
</p>
<p>
    One study found that land disturbances such as urban construction can, at
    minimum, double the amount of sediment entering headwater streams from a
    watershed. A Pennsylvania study showed how, as a 160-acre [65-hectare]
    headwater watershed became more urbanized, channel erosion of a quartermile
    [0.4-km] stretch of stream generated 50,000 additional cubic feet [1,416
    cubic meters] of sediment in one year—enough to fill 25 moderate-sized
    living rooms. In a nonurban watershed of the same size, it would take five
    years to generate the same amount of sediment. Such studies demonstrate
    that landscape changes such as urbanization or agriculture, particularly
    without careful protection of headwater streams and their riparian zones,
    may cause many times more sediment to travel downstream.
</p>
<p>
    <strong>Excess Sediment in Downstream Ecosystems Costs Money </strong>
    Keeping excess sediment out of downstream rivers and lakes is one ecosystem
    service intact small streams and wetlands provide. Once sediment moves
    further downstream, it becomes an expensive problem. Too much sediment can
    fill up reservoirs and navigation channels, damage commercial and sport
    fisheries, eliminate recreation spots, harm aquatic habitats and their
    associated plants and animals, and increase water filtration costs.
</p>
<p>
    Additional sediment damages aquatic ecosystems. Sediment suspended in the
    water makes it murkier; as a result, underwater plants no longer receive
    enough light to grow. Fish that depend on visual signals to mate may be
    less likely to spawn in murky water, thereby reducing fish populations.
    High levels of sediment suspended in water can even cause fish kills. Even
    as it settles to the bottom, sediment continues to cause problems because
    it fills the holes between gravel and stones that some animals call home,
    smothers small organisms that form the basis of many food webs, and can
    also smother fish eggs.
</p>
<p>
    Getting rid of sediment is expensive. For example, keeping Baltimore Harbor
    navigable costs $10 to $11.5 million annually to dredge and dispose of
    sediment the Patapsco River deposits in the harbor.
</p>
<p align="left">
    <strong>Small Streams and Wetlands Retain Sediment</strong>
</p>
<p>
    Headwater streams and wetlands typically trap and retain much of the
    sediment that washes into them. The faster the water travels, the larger
    the particles it can carry. So, natural obstructions in small streams,
    rocks, downed logs, or even just a bumpy stream bottom slow water and cause
    sediment to settle out of the water column. Wetlands, whether or not they
    have a surface connection to a nearby stream, are often areas where runoff
    slows and stops,dropping any debris the water may be carrying. Because
    headwater streams represent 75 percent or more of total stream length in a
    stream network, such streams and their associated wetlands retain a
    substantial amount of sediment, preventing it from flowing into larger
    rivers downstream.
</p>
<p>
    Even ephemeral streams can retain significant amounts of sediment. Such
    small headwater streams expand and contract in response to heavy rains.
    During expansion, a stream flows over what was a dry or damp streambed.
    Most of the water at the leading edge of a growing stream, called the
    “trickle front,” soaks into the streambed and does not carry sediment
    downstream. In a small watershed near Corvallis, Oregon, researchers found
    that 60 to 80 percent of sediment generated from forest roads traveled less
    than 250 feet [76 meters] downstream before settling out in stream pools.
    Headwater streams can store sediment for long periods of time: research in
    Oregon’s Rock Creek basin found that headwater streams could retain
    sediment for 114 years.
</p>
<p align="left">
    <strong>
        Natural Cleansing Ability of Small Streams and Wetlands Protects Water
        Quality
    </strong>
</p>
<p>
    Materials that wash into streams include everything from soil, leaves and
    dead insects to runoff from agricultural fields and animal pastures. One of
    the key ecosystem services that stream networks provide is the filtering
    and processing of such materials. Healthy aquatic ecosystems can transform
    natural materials like animal dung and chemicals such as fertilizers into
    less harmful substances. Small streams and their associated wetlands play a
    key role in both storing and modifying potential pollutants, ranging from
    chemical fertilizers to rotting salmon carcasses, in ways that maintain
    downstream water quality.
</p>
<p>
    <strong>Excess Nutrients Cause Problems in Rivers and Lakes </strong>
    Inorganic nitrogen and phosphorus, the main chemicals in agricultural
    fertilizers, are essential nutrients not just for plants, but for all
    living organisms. However, in excess or in the wrong proportions, these
    chemicals can harm natural systems and humans.
</p>
<p>
    In freshwater ecosystems, eutrophication, the enriching of waters by excess
    nitrogen and phosphorus, reduces water quality in streams, lakes, estuaries
    and other downstream water bodies. One obvious result is the excessive
    growth of algae. More algae clouds previously clear streams, such as those
    favored by trout. In addition to reducing visibility, algal blooms reduce
    the amount of oxygen dissolved in the water, sometimes to a degree that
    causes fish kills. Fish are not the only organisms harmed: some of the
    algae species that grow in eutrophic waters generate tastes and odors or
    are toxic, a clear problem for stream systems that supply drinking water
    for municipalities. In addition, increased nitrogen can injure people and
    animals. Excess nitrogen in the form called nitrate in drinking water has
    been linked to “blue baby disease” (methemoglobinemia) in infants and also
    has toxic effects on livestock.
</p>
<p align="left">
    <strong>HEADWATER STREAMS TRANSFORM AND STORE EXCESS NUTRIENTS</strong>
</p>
<p>
    Headwater streams and associated wetlands both retain and transform excess
    nutrients, thereby preventing them from traveling downstream. Physical,
    chemical and biological processes in headwater streams interact to provide
    this ecosystem service.
</p>
<p>
    Compared with larger streams and rivers, small streams, especially shallow
    ones, have more water in physical contact with a stream channel. Therefore,
    the average distance traveled by a particle before it is removed from the
    water column is shorter in headwater streams than in larger ones. A study
    of headwater streams in the southern Appalachian Mountains found that both
    phosphorus and the nitrogen-containing compound ammonium traveled less than
    65 feet [20 meters] downstream before being removed from the water.
</p>
<p>
    In headwater streams and wetlands, more water is in direct contact with the
    streambed, where most processing takes place. Bacteria, fungi and other
    microorganisms living on the bottom of a stream consume inorganic nitrogen
    and phosphorus and convert them into less harmful, more biologically
    beneficial compounds. A mathematical model based on research in 14
    headwater streams throughout the U.S. shows that 64 percent of inorganic
    nitrogen entering a small stream is retained or transformed within 1,000
    yards [914 meters].
</p>
<p>
    Channel shape also plays a role in transforming excess nutrients. Studies
    in Pennsylvania have shown that when the forest surrounding headwaters is
    replaced by meadows or lawns, increased sunlight promotes growth of grasses
    along stream banks. The grasses trap sediments, create sod, and narrow the
    stream channel to one-third of the original width. Such narrowing reduces
    the amount of streambed available for microorganisms that process
    nutrients. As a result, nitrogen and phosphorus travel downstream five to
    ten times farther, increasing risks of eutrophication.
</p>
<p>
    Streams do not have to flow yearround to make significant contributions to
    water quality. Fertilizers and other pollutants enter stream systems during
    storms and other times of high runoff, the same times that ephemeral and
    intermittent streams are most likely to have water and process nutrients.
    Federal, state and local programs spend considerable sums of money to
    reduce non-point source inputs of nutrients because they are a major threat
    to water quality. One principal federal program, the EPA’s 319 cost-share
    program, awarded more than $1.3 billion between 1990 and 2001 to states and
    territories for projects to control non-point pollution. Failure to
    maintain nutrient removal capacity of ephemeral and intermittent streams
    and wetlands would undermine these efforts.
</p>
<p>
    Wetlands also remove nutrients from surface waters. Several studies of
    riparian wetlands have found that those associated with the smallest
    streams to be most effective in removing nutrients from surface waters. For
    example, headwater wetlands comprise 45 percent of all wetlands able to
    improve water quality in four Vermont watersheds. Another study found that
    wetlands associated with first-order streams are responsible for 90 percent
    of wetland phosphorus removal in eight northeastern watersheds. Such
    studies demonstrate that riparian wetlands, especially those associated
    with small streams, protect water quality.
</p>
<p>
    As land is developed, headwater streams are often filled or channeled into
    pipes or paved waterways, resulting in fewer and shorter streams. For
    example, as the Rock Creek watershed in Maryland was urbanized, more than
    half of the stream channel network was eliminated. In even more dramatic
    fashion, mining operations in the mountains of central Appalachia have
    removed mountain tops and filled valleys, wiping out entire headwater
    stream networks. From 1986 to 1998, more than 900 miles [1,448 km] of
    streams in central Appalachia were buried, more than half of them in West
    Virginia.
</p>
<p>
    If headwater streams and wetlands are degraded or filled, more fertilizer
    applied to farm fields or lawns reaches larger downstream rivers. These
    larger rivers process excess nutrients from fertilizer much more slowly
    than smaller streams. Losing the nutrient retention capacity of headwater
    streams would cause downstream water bodies to contain higher
    concentrations of nitrogen and phosphorus. A likely consequence of
    additional nutrients would be the further contamination and eutrophication
    of downstream rivers, lakes, estuaries and such waters as the Gulf of
    Mexico.
</p>
<p align="left">
    <strong>
        Natural Recycling in Headwater Systems Sustains Downstream Ecosystems
    </strong>
</p>
<p>
    Recycling organic carbon contained in the bodies of dead plants and animals
    is a crucial ecosystem service. Ecological processes that transform
    inorganic carbon into organic carbon and recycle organic carbon are the
    basis for every food web on the planet. In freshwater ecosystems, much of
    the recycling happens in small streams and wetlands, where microorganisms
    transform everything from leaf litter and downed logs to dead salamanders
    into food for other organisms in the aquatic food web, including mayflies,
    frogs and salmon.
</p>
<p>
    Like nitrogen and phosphorus, carbon is essential to life but can be
    harmful to freshwater ecosystems if it is present in excess or in the wrong
    chemical form. If all organic material received by headwater streams and
    wetlands went directly downstream, the glut of decomposing material could
    deplete oxygen in downstream rivers, thereby damaging and even killing fish
    and other aquatic life. The ability of headwater streams to transform
    organic matter into more usable forms helps maintain healthy downstream
    ecosystems.
</p>
<p align="left">
    <strong>
        HEADWATER STREAM SYSTEMS STORE AND TRANSFORM EXCESS ORGANIC MATTER
    </strong>
</p>
<p>
    Intact headwater systems both store and process organic matter in ways that
    modulate the release of carbon to downstream lakes and rivers. Headwater
    systems receive large amounts of organic matter, which can be retained and
    transformed into more palatable forms through decomposition processes. This
    organic matter is anything of biological origin that falls into, washes
    into or dies in a stream. Plant parts, such as leaves, twigs, stems and
    larger bits of woody debris, are the most common of these items. Another
    source of organic material is dead stream organisms, such as bits of dead
    algae and bacteria or bodies of insects and even larger animals. Waste
    products of plants and animals also add organic carbon to water. Water
    leaches dissolved organic carbon from organic materials in a stream and
    watershed like tea from a tea bag.
</p>
<p>
    Much of the organic matter that enters headwater systems remains there
    instead of continuing downstream. One reason is that the material often
    enters headwater streams as large pieces, such as leaves and woody debris,
    that are not easily carried downstream. In addition, debris dams that
    accumulate in headwater streams block the passage of materials. One study
    found four times more organic matter on the bottoms of headwater streams in
    forested watersheds than on the bottoms of larger streams. Another reason
    material stays in headwater streams is that food webs in small streams and
    wetlands process organic matter efficiently. Several studies have found
    that headwater streams are far more efficient at transforming organic
    matter than larger streams.
</p>
<p>
    For example, one study showed that, for a given length of stream, a
    headwater stream had an eight-fold higher processing efficiency than a
    fourth-order channel downstream. Microorganisms in headwater stream systems
    use material such as leaf litter and other decomposing material for food
    and, in turn, become food for other organisms. For example, fungi that grow
    on leaf litter become nutritious food for invertebrates that make their
    homes on the bottom of a stream, including mayflies, stoneflies and caddis
    flies. These animals provide food for larger animals, including birds such
    as flycatchers and fish such as trout.
</p>
<p align="left">
    <strong>Headwater Systems Supply Food for Downstream Ecosystems</strong>
</p>
<p>
    The organic carbon released by headwater streams provides key food
    resources for downstream ecosystems. Headwater ecosystems control the form,
    quality and timing of carbon supply downstream. Although organic matter
    often enters headwaters in large amounts, such as when leaves fall in
    autumn or storm runoff carries debris into the stream, those leaves and
    debris are processed more slowly. As a result, carbon is supplied to
    downstream food webs more evenly over a longer period of time. Forms of
    carbon delivered range from dissolved organic carbon that feeds
    microorganisms to the drifting insects such as mayflies and midges that
    make ideal fish food. Such insects are the preferred food of fish such as
    trout, char and salmon. One study estimated that fishless headwater streams
    in Alaska export enough drifting insects and other invertebrates to support
    approximately half of the fish production in downstream waters.
</p>
<p>
    Processed organic matter from headwater streams fuels aquatic food webs
    from the smallest streams to the ocean. Only about half of all first-order
    streams drain into second-order streams; the other half feed directly into
    larger streams or directly into estuaries and oceans, thus delivering their
    carbon directly to these larger ecosystems. The health and productivity of
    downstream ecosystems depends on processed organic carbon—ranging from
    dissolved organic carbon to particles of fungus, and leaf litter to
    mayflies and stoneflies—delivered by upstream headwater systems.
</p>
