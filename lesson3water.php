        <strong>
            It’s Your Drinking Water: Get to Know It and Protect It
        </strong>
    </p>
    <p align="left">
        from the U.S. Environmental Protection Agency
    </p>
    <p>
        There is no such thing as naturally pure water. In nature, all water
        contains some impurities. As water flows in streams, sits in lakes, and
        filters through layers of soil and rock in the ground, it dissolves or
        absorbs the substances that it touches. Some of these substances are
        harmless. In fact, some people prefer mineral water precisely because
        minerals give it an appealing taste. However, at certain levels,
        minerals, just like man-made chemicals, are considered contaminants
        that can make water unpalatable or even unsafe.
    </p>
    <p>
        Some contaminants come from erosion of natural rock formations. Other
        contaminants are substances discharged from factories, applied to
        farmlands, or used by consumers in their homes and yards. Sources of
        contaminants might be in your neighborhood or might be many miles away.
        Your local water quality report tells which contaminants are in your
        drinking water, the levels at which they were found, and the actual or
        likely source of each contaminant.
    </p>
    <p>
        Some ground water systems have established wellhead protection programs
        to prevent substances from contaminating their wells. Similarly, some
        surface water systems protect the watershed around their reservoir to
        prevent contamination. Right now, states and water suppliers are
        working systematically to assess every source of drinking water and to
        identify potential sources of contaminants.This process will help
        communities to protect their drinking water supplies from
        contamination, and a summary of the results will be in future water
        quality reports.
    </p>
    <p align="left">
        <strong>WHERE DOES DRINKING WATER COME FROM?</strong>
    </p>
    <p>
        A clean, constant supply of drinking water is essential to every
        community. People in large cities frequently drink water that comes
        from surface water sources, such as lakes, rivers, and reservoirs.
        Sometimes these sources are close to the community. Other times,
        drinking water suppliers get their water from sources many miles away.
        In either case, when you think about where your drinking water comes
        from, it’s important to consider not just the part of the river or lake
        that you can see, but the entire watershed. The watershed is the land
        area over which water flows into the river, lake, or reservoir.
    </p>
    <p>
        In rural areas, people are more likely to drink ground water that was
        pumped from a well. These wells tap into aquifers— the natural
        reservoirs under the earth’s surface—that may be only a few miles wide,
        or may span the borders of many states. As with surface water, it is
        important to remember that activities many miles away from you may
        affect the quality of
    </p>
    <p>
        ground water.
    </p>
    <p>
        Your annual drinking water quality report will tell you where your
        water supplier gets your water.
    </p>
    <p align="left">
        <strong>HOW IS DRINKING WATER TREATED?</strong>
    </p>
    <p>
        When a water supplier takes untreated water from a river or reservoir,
        the water often contains dirt and tiny pieces of leaves and other
        organic matter, as well as trace amounts of certain contaminants. When
        it gets to the treatment plant, water suppliers often add chemicals
        called coagulants to the water. These act on the water as it flows very
        slowly through tanks so that the dirt and other contaminants form
        clumps that settle to the bottom. Usually, this water then flows
        through a filter for removal of the smallest contaminants like viruses
        and <em>Giardia.</em>
    </p>
    <p>
        Most ground water is naturally filtered as it passes through layers of
        the earth into underground reservoirs known as aquifers. Water that
        suppliers pump from wells generally contains less organic material than
        surface water and may not need to go through any or all of the
        treatments described in the previous paragraph. The quality of the
        water will depend on local conditions.
    </p>
    <p>
        The most common drinking water treatment, considered by many to be one
        of the most important scientific advances of the 20<sup>th </sup>
        century, is disinfection. Most water suppliers add chlorine or another
        disinfectant to kill bacteria and other germs.
    </p>
    <p>
        Water suppliers use other treatments as needed, according to the
        quality of their source water. For example, systems whose water is
        contaminated with organic chemicals can treat their water with
        activated carbon, which adsorbs or attracts the chemicals dissolved in
        the water.
    </p>
    <p align="left">
        <strong>WHAT IF I HAVE SPECIAL HEALTH NEEDS?</strong>
    </p>
    <p>
        People who have HIV/AIDS, are undergoing chemotherapy, take steroids,
        or for another reason have a weakened immune system may be more
susceptible to microbial contaminants, including        <em>Cryptosporidium</em>, in drinking water. If you or someone you know
        fall into one of these categories, talk to your health care provider to
        find out if you need to take special precautions, such as boiling your
        water.
    </p>
    <p>
        Young children are particularly susceptible to the effects of high
        levels of certain contaminants, including nitrate and lead. To avoid
        exposure to lead, use water from the cold tap for making baby formula,
        drinking, and cooking, and let the water run for a minute or more if
        the water hasn’t been turned on for six or more hours. If your water
        supplier alerts you that your water does not meet EPA’s standard for
        nitrates and you have children less than six months old, consult your
        health care provider.You may want to find an alternate source of water
        that contains lower levels of nitrates for your child.
    </p>
    <p align="left">
        <strong>WHAT ARE THE HEALTH EFFECTS OF </strong>
    </p>
    <p align="left">
        <strong>CONTAMINANTS IN DRINKING WATER?</strong>
    </p>
    <p>
        EPA has set standards for more than 80 contaminants that may occur in
        drinking water and pose a risk to human health. EPA sets these
        standards to protect the health of everybody, including vulnerable
        groups like children.
    </p>
    <p>
        The contaminants fall into two groups according to the health effects
        that they cause.Your local water supplier will alert you through the
        local media, direct mail, or other means if there is a potential acute
        or chronic health effect from compounds in the drinking water.You may
        want to contact them for additional information specific to your area.
    </p>
    <p>
        Acute effects occur within hours or days of the time that a person
        consumes a contaminant. People can suffer acute health effects from
        almost any contaminant if they are exposed to extraordinarily high
        levels (as in the case of a spill). In drinking water, microbes, such
        as bacteria and viruses, are the contaminants with the greatest chance
        of reaching levels high enough to cause acute health effects. Most
        people’s bodies can fight off these microbial contaminants the way they
        fight off germs, and these acute contaminants typically don’t have
        permanent effects. Nonetheless, when high enough levels occur, they can
        make people ill, and can be dangerous or deadly for a person whose
        immune system is already weak due to HIV/AIDS, chemotherapy, steroid
        use, or another reason.
    </p>
    <p>
        Chronic effects occur after people consume a contaminant at levels over
        EPA’s safety standards for many years. The drinking water contaminants
        that can have chronic effects are chemicals (such as disinfection
        by-products, solvents, and pesticides), radionuclides (such as radium),
        and minerals (such as arsenic). Examples of these chronic effects
        include cancer, liver or kidney problems, or reproductive difficulties.
    </p>
    <p align="left">
        <strong>WHO IS RESPONSIBLE FOR DRINKING WATER QUALITY?</strong>
    </p>
    <p>
        The Safe Drinking Water Act gives the Environmental Protection Agency
        (EPA) the responsibility for setting national drinking water standards
        that protect the health of the 250 million people who get their water
        from public water systems. Other people get their water from private
        wells which are not subject to federal regulations. Since 1974, EPA has
        set national standards for over 80 contaminants that may occur in
        drinking water.
    </p>
    <p>
        While EPA and state governments set and enforce standards, local
        governments and private water suppliers have direct responsibility for
        the quality of the water that flows to your tap.
    </p>
    <p>
        Water systems test and treat their water, maintain the distribution
        systems that deliver water to consumers, and report on their water
        quality to the state. States and EPA provide technical assistance to
        water suppliers and can take legal action against systems that fail to
        provide water that meets state and EPA standards.
    </p>
    <p align="left">
        <strong>WHAT IS A VIOLATION OF A DRINKING WATER STANDARD?</strong>
    </p>
    <p>
        Drinking water suppliers are required to monitor and test their water
        many times, for many things, before sending it to consumers. These
        tests determine whether and how the water needs to be treated, as well
        as the effectiveness of the treatment process. If a water system
        consistently sends to consumers water that contains a contaminant at a
        level higher than EPA or state health standards or if the system fails
        to monitor for a contaminant, the system is violating regulations, and
        is subject to fines and other penalties.
    </p>
    <p>
        When a water system violates a drinking water regulation, it must
        notify the people who drink its water about the violation, what it
        means, and how they should respond. In cases where the water presents
        an immediate health threat, such as when people need to boil water
        before drinking it, the system must use television, radio, and
        newspapers to get the word out as quickly as possible. Other notices
        may be sent by mail, or delivered with the water bill. Each water
        suppliers’ annual water quality report must include a summary of all
        the violations that occurred during the previous year.
    </p>
    <p align="left">
        <strong>HOW CAN I HELP PROTECT DRINKING WATER?</strong>
    </p>
    <p>
        Using the new information that is now available about drinking water,
        citizens can both be aware of the challenges of keeping drinking water
        safe and take an active role in protecting drinking water. There are
        lots of ways that individuals can get involved. Some people will help
        clean up the watershed that is the source of their community’s water.
        Other people might get involved in wellhead protection activities to
        prevent the contamination of the ground water source that provides
        water to their community. These people will be able to make use of the
        information that states and water systems are gathering as they assess
        their sources of water.
    </p>
    <p>
        Other people will want to attend public meetings to ensure that the
        community’s need for safe drinking water is considered in making
        decisions about land use. You may wish to participate as your state and
        water system make funding decisions. And all consumers can do their
        part to conserve water and to dispose properly of household chemicals.
    </p>
    <p align="left">
        <strong>Is Your Drinking Water Safe?</strong>
    </p>
    <p align="left">
        by Rene Ebersole
    </p>
    <p>
        It’s clear, smells fresh, tastes good. But is it safe to drink? Most of
        us have considered this question, whether sipping tap water while
        traveling or making lemonade at home. Truth is American water supplies
        are some of the cleanest in the world. Still, it sure doesn’t hurt to
        be cautious.
    </p>
    <p>
        Case in point: The nation’s capital, where thousands of residents
        discovered last winter [2003–2004] that their tap water was tainted
        with dangerous levels of lead, a metal associated with behavioral
        problems, brain damage and lowered IQ in children, and strokes, cancer
        and elevated blood pressure in adults.
    </p>
    <p>
        More than two-thirds of the 6,000 Washington, D.C., households that had
        their water tested for lead were found to exceed the Environmental
        Protection Agency’s (EPA) hazard level of 15 parts per billion
        (ppb)—157 homes had levels above 300 ppb. When the test results hit the
        news in January [2004], city officials scrambled to root out the source
        of the contamination and ramped up efforts to replace lead service
        pipes that transmit water underground to customers’ homes.
    </p>
    <p>
        At the same time, residents stormed department stores, looking for home
        water filtration systems that would protect their families from the
        invisible toxin.
    </p>
    <p>
        The District of Columbia is home to one of the roughly three dozen
        water systems in the country with lead levels above the federal safety
        standard, according to data collected by the EPA. (Most of the others
        are in smaller communities.) But lead isn’t the only contaminant in the
        nation’s public water supplies.
    </p>
    <p>
        Some studies estimate that as many as seven million Americans become
        sick from contaminated tap water each year. Often to blame are aging
        pipes that break, leach contaminants into water and breed bacteria, as
well as old-fashioned treatment facilities that can fail to remove 21        <sup>st </sup>century contaminants. Exacerbating the problem are
        environmental threats—stormwater runoff, agricultural pesticides and
        fertilizers, industrial pollution, hazardous waste and oil and chemical
        spills.
    </p>
    <p>
        “Most Americans take it for granted that their tap water is pure and
        their water infrastructure is safe,” says Erik Olson, senior attorney
        with the Natural Resources Defense Council (NRDC). “They shouldn’t.”
    </p>
    <p>
        A recent NRDC report authored by Olson found the quality of drinking
        water in 19 major U.S. cities might pose health risks to some
        residents, especially those with compromised immune systems. Of all the
        cities studied, five had apparent or confirmed violations of
        enforceable tap water laws. The cities with the poorest water quality
        were Albuquerque, Boston, Fresno, Phoenix and San Francisco.
    </p>
    <p>
        Water utilities are required by law to provide consumers with
        information about where municipal water comes from, whether it exceeds
        the allowable limits of the 80 possible contaminants regulated by the
        EPA and the health risks to which they may be exposed by drinking tap
        water.“If a system fails to monitor for a contaminant, it is subject to
        fines and penalties,” says EPA spokesperson Cathy Milbourn. Utilities
        are required to mail these “consumer confidence reports” by July each
        year.
    </p>
    <p>
        Can the utilities’ reports be trusted? According to NRDC, sometimes
        yes, sometimes no. Olson and his colleagues carefully evaluated the
        utility reports of the 19 cities. They determined that some, such as
        Chicago, Denver and Detroit, were doing a “good job.” Others, including
        Atlanta, Boston, Houston, Newark, Phoenix, Seattle and Washington,
        D.C., published information that was “incomplete or misleading.”
    </p>
    <p>
        Studies show that it will take from $230 to $500 billion— more than
        $300 million in Washington, D.C., alone—to restore and upgrade the
        nation’s aging and outdated water systems. Some experts criticize the
        Bush administration for providing insufficient funding for such an
        important endeavor, and instead attempting to weaken the bedrock
        environmental laws that protect the nation’s drinking water supplies.
    </p>
    <p>
        At press time [July 2004], Congress was considering the
        administration’s proposal to revoke Clean Water Act protections for
        more than 20 million acres [8.1 million hectares] of wetlands that
        naturally filter pollutants and provide clean drinking water to
        American towns and cities. The administration did, however, cancel
        plans last winter to issue a new rule limiting the types of waters
        protected under the Clean Water Act.
    </p>
    <p>
        “Clean drinking water has been one of the major public health triumphs
        of the past 100 years,” says David Ozonoff, a professor at Boston
        University’s School of Public Health and an expert on waterborne
        illnesses. “We’ve figured out how to build very efficient water
        delivery systems. But these systems can either provide safe drinking
        water, or deliver poisons and harmful organisms into every home, school
        and workplace. One misstep can lead to disaster, so we must vigorously
        protect our watersheds and use the best technology to purify our tap
        water.”
    </p>
