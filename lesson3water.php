        <p>
            The following is a description of the different sources of
            beachwater pollution.
        </p>
        <p align="left">
            <strong>COMBINED SEWER OVERFLOWS</strong>
        </p>
        <p>
            Combined sewers are pipes designed to carry both raw sewage from
            residences and industrial sites and stormwater runoff from streets
            to sewage treatment plants.When it rains—even as little as
            one-quarter of an inch [just over half a centimeter]—the volume of
            the combined wastewater becomes too great for the treatment plant
            to handle. In such circumstances, the flow is then diverted to
            outfall points that discharge pollutants—including raw sewage;
            floatables such as garbage,syringes,and tampon applicators; toxic
            industrial waste; and contaminated stormwater—into the nearest
            stream or coastal waterway. These untreated discharges can be as
            potent as direct sewer emissions.
        </p>
        <p>
            Combined sewer overflows (CSOs) are one of the major causes of
            pathogen contamination in marine and Great Lakes waters near urban
            areas. Although they are most prevalent in urban areas,CSOs occur
            throughout coastal and Great Lakes states. Combined sewer overflows
            also contaminate shellfish waters and recreational beaches.
            Combined sewers serve 40 million people nationwide. Although an EPA
            policy that aims to reduce these overflows has been in effect since
            1994, virtually all combined sewer systems continue to overflow
            when it rains. The EPA 1994 Combined Sewer Overflow Control policy
            required all systems to implement nine minimum system controls by
            1997. According to the EPA’s 2001 Report to Congress, progress has
            been slow. Only 32 percent of CSO communities are implementing the
            nine minimum system controls, despite the January 1997 deadline.
            Only 19 percent have finished their plans for controlling CSOs, and
            fewer than 10 percent have finished implementing CSO controls. The
            EPA estimates that 1,260 billion gallons of raw sewage from CSO
            discharges continue to flow into our surface waters every year.
        </p>
        <p align="left">
            <strong>SANITARY SEWER OVERFLOWS AND </strong>
        </p>
        <p align="left">
            <strong>SEWER-LINE BREAK DISCHARGES</strong>
        </p>
        <p>
            Many sanitary sewers—those designed to carry only human and
            industrial waste from buildings to sewage-treatment plants—pose a
            threat to bathing-beach safety. Human-waste sewage lines have
            breaches, obstructions such as tree roots or grease clogs, cracks,
            stormwater drain cross-connections, and open manholes that permit
            infiltration by groundwater and inflows of stormwater. These
            sanitary sewers can become overloaded, especially when it rains,
            and can overflow and discharge raw sewage from manholes, manhole
            bypasses, pump-station bypasses, and treatment-plant bypasses. In
            addition, sanitary-sewer lines are often old and, in many cities,
            inadequately maintained. They can break and spill sewage directly
            onto streets or into waterways. The EPA has estimated 40,000
            sanitary sewer overflows (SSO) annually and about 400,000 basement
            backups. These overflows often discharge sewage, untreated,
            directly into coastal waterways or their tributaries, causing
            between 1.8 and 3.5 million people to get gastroenteritis each year
            from swimming in raw sewage from SSOs. In January 2001, the EPA
            proposed SSO regulations that would require improved capacity,
            operation, and maintenance and require that systems notify the
            public when these overflows occur. Unfortunately, instead of
            finalizing rules to prevent SSOs, the Bush Administration has held
            up these proposed rules.
        </p>
        <p>
            To make matters worse, the EPA has recently proposed to allow the
            “blending” of minimally treated sewage with secondarily treated
            effluent during rains. In effect this policy would allow the
            discharge of nearly raw sewage so long as it is sufficiently
            diluted. Analyses of pathogen data from blended sewage discharges
            gathered by the Milwaukee Public Health Department conclude that
            people swimming at the sewage discharge point in Lake Michigan
            would have had a 50 percent chance of contracting giardiasis after
            this blended sewage had been released, representing a thousandfold
            increase in the risk of getting sick as a result of exposure to
            pathogens while swimming.
        </p>
        <p align="left">
            <strong>SEWAGE TREATMENT PLANT MALFUNCTIONS</strong>
        </p>
        <p>
            Sewage plants in coastal areas tend to serve densely populated,
            rapidly growing urban areas. When too many homes and businesses are
            hooked up to a sewage treatment plant, the plant cannot treat the
            sewage adequately. The National Oceanic and Atmospheric
            Administration (NOAA) estimates that between 1990 and 2010 the
            coastal population will grow from 112 million to more than 127
            million—an increase of almost 13 percent. Plants that exceed their
            capacity or the capacity of their collection systems are prone to
            more frequent episodes of bypasses and inadequate treatment.
            Moreover, sewage treatment plants can, and often do, malfunction as
            the result of human error, breakage of old equipment, or unusual
            conditions in the raw sewage. At such times, raw or partially
            treated sewage may be discharged into coastal waterways and their
            tributaries. Sewage plants are increasingly discharging
            inadequately treated sewage in high-flow conditions. Instead of
            providing full secondary treatment for sewage, treated and
            untreated sewage are mixed, increasing the pathogen load to
            receiving waters.
        </p>
        <p align="left">
            <strong>URBAN STORMWATER RUNOFF</strong>
        </p>
        <p>
            Stormwater starts as rain or snowmelt. As it washes over roads,
            rooftops, parking lots, construction sites, and industrial or
            commercial sites, it becomes contaminated with oil and grease,
            heavy metals, pesticides, litter, and pollutants from vehicle
            exhaust. On its way to stormdrains, it also often picks up fecal
            matter from dogs, cats, pigeons, other urban animals, and even
            humans. Stormdrains may empty into separate storm sewer systems
            that carry only stormwater and discharge directly into waterways,
            or they may become a part of combined sewer systems, which, as
            described above, usually overflow when it rains. Moreover, human
            waste may find its way into storm sewer systems from adjacent
            sewage pipes that leak or from businesses or residences that are
            illegally hooked up. Illicit discharges may also include such
            substances as oil from cars, paint, and grease from restaurants. In
            Los Angeles County, for instance, the sewer system is separate from
            the stormdrain system, yet Santa Monica Bay stormdrains sometimes
            discharge runoff containing human enteric viruses, indicating the
            presence of human wastes. About a quarter of our nation’s polluted
            estuaries and lakes are fouled by urban stormwater, and it is a
            significant source of bathingbeach pollution in many regions.
            Almost every coastal or Great Lakes state has beaches with
            stormdrains nearby.
        </p>
        <p>
            Urban runoff can cause swimmers to become ill. A recent study of
            the health effects of urban runoff compared illness rates for
            surfers in California’s urban North Orange County and rural Santa
            Cruz County during the winters of 1998 and 1999. The study
            determined that surfers at the North Orange County beaches, which
            were more polluted by urban runoff, saw modestly elevated
            incidences of illness in 1999, and twice as many illnesses in 1998
            (likely because 1998 was a wet year associated with El Niño). The
            EPA regulations require cities and industrial and construction
            sites to obtain permits, develop stormwater management plans, and
            implement best management practices; however, only limited progress
            has been made to date. Vigorous implementation and enforcement and
            ambitious pollutant reduction goals are necessary to make this
            program successful. Unfortunately, despite the magnitude of
            stormwater pollution, the EPA recently declined to set baseline
            technology standards for new construction and
        </p>
        <p>
            development.
        </p>
        <p align="left">
            <strong>POLLUTED RUNOFF FROM NONURBAN AREAS</strong>
        </p>
        <p>
            In nonurban and suburban areas, rainwater often flows directly over
            farms, roads, golf courses, and lawns into waterways.
        </p>
        <p>
            Farm runoff may contain high concentrations of pathogenic animal
            waste, fertilizers, and pesticides. Suburban lawn runoff often
            contains significant amounts of animal waste, fertilizer, and other
            chemicals. This uncontrolled runoff can foul beaches in less
            densely populated areas. Animal waste can also contain pathogens
            usually not found in human waste, such as <em>E. coli </em>0157:H7.
            Animal waste from large feedlots, often spread too heavily on
            fields, runs off and has been linked to outbreaks of a toxic
            microorganism, <em>Pfiesteria piscicida</em>, in the Chesapeake Bay
            region and North Carolina, causing numerous waterway closings.
        </p>
        <p align="left">
            <strong>SEPTIC SYSTEMS</strong>
        </p>
        <p>
            Dwellings built near the coast may be equipped with underground
            septic systems, which, if not sited, built, and maintained
            properly, can leach wastewater into coastal recreational waters.
            Homeowners often do not adequately maintain their septic systems,
            and there is no federal regulatory program to control waste from
            septic systems. Local governments and states rarely inspect septic
            systems sufficiently to prevent such failures. Bathing beaches can
            be contaminated by fecal matter from malfunctioning or overloaded
            septic systems. Runoff can also carry bacteria from failing septic
            systems far from the shore into streams that empty into bays near
            beaches. The EPA estimates that 25 percent of all U.S. dwellings
            use some kind of septic tank or on-site waste disposal system.
        </p>
        <p align="left">
            <strong>BOATING WASTES</strong>
        </p>
        <p>
            Improperly handled boating wastes can pose a health and aesthetic
            threat to bathing beaches. Elevated concentrations of fecal
            coliform have been found in areas with high boating density.
            Federal law requires boats with onboard toilets either to treat the
            waste (through chemical treatment) before discharging it or to hold
            the waste and later pump it out for treatment at a sewage-treatment
            plant. Many areas lack sufficient pump-out facilities, and
            compliance with the law appears to be poor in many areas.
        </p>
        <p align="left">
            <strong>OIL POLLUTION</strong>
        </p>
        <p>
            Oil reaches the ocean as a result of the production,
            transportation, and use of oil, and even from natural seeps. Oil
            spilled during tanker accidents, pipeline breaks, and refinery
            accidents can foul beaches. Many oils evaporate quickly, creating
            unsafe fumes. Other oils form globules that can float for days and
            wash onto beaches for weeks after a spill. In addition to the
            catastrophic oil spills, there are small oil spills every time it
            rains. Over the course of a year, urban runoff from a city of 5
            million [people] can contain as much oil and grease as a large
            tanker spill. Normal ship operations also frequently cause small
            oil spills. A recent report by the National Research Council states
            that nearly 85 percent of the 29 million gallons of petroleum that
            enter North American ocean waters each year as a result of human
            activities comes from land-based runoff, polluted rivers,
            airplanes, and small boats and jet skis. The report estimated that
            two-stroke outboard motors release between 0.6 and 2.5 million
            gallons of oil and gasoline into U.S. coastal waters each year. Oil
            runoff from cars and trucks is increasing in coastal areas where
            the population is increasing and roads and parking lots are
            expanding to accommodate
        </p>
        <p>
            this growth.
        </p>
        <p align="left">
            <strong>WATERFOWL</strong>
        </p>
        <p>
            Several municipalities listed waterfowl as the cause of a beach
            closing or advisory. Large or excessive populations of waterfowl at
            a beach or in suburban areas draining near a beach can occur during
            migration season when other potential waterfowl habitats are
            unavailable for some reason, such as filling of wetlands, or at
            other times as a result of altered ecological conditions (for
            example, when Canada geese that were previously migratory become
            resident). The fecal matter from these waterfowl can sometimes
            overload the normal capacity of a beach to absorb natural wastes,
            degrading water quality.
        </p>
    </div>
