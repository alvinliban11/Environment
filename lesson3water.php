    As it becomes clearer to decisionmakers that the most serious threats to
    human security are no longer those of military attack but of pervasive
    environmental and social decline,experts worry about the difficulty of
    mustering sufficient political will to bring about the kinds of
    systemic—and therefore revolutionary—changes in human life necessary to
    turn the tide in time. In confronting the now heavily documented assaults
    of climate change and biodiversity loss, leaders seem on one hand paralyzed
    by how bleak the big picture appears to be—and on the other hand too easily
    drawn into denial or delay by the seeming lack of immediate consequences of
    such delay. But protecting aquifers may provide a more immediate incentive
    for change, if only because it simply may not be possible to live with
