        <p>
            <em>
                As water pollution threatens our health and environment, we
                need to implement an expanding array of techniques for its
                assessment, prevention, and remediation.
            </em>
        </p>
        <p>
            In the 1890s, entrepreneur William Love sought to establish a model
            industrial community in the La Salle district of Niagara Falls, New
            York. The plan included building a canal that tapped water from the
            Niagara River for a navigable waterway and a hydroelectric power
            plant. Although work on the canal was begun, a nationwide economic
            depression and other factors forced abandonment of the project.
        </p>
        <p>
            By 1920, the land adjacent to the canal was sold and used as a
            landfill for municipal and industrial wastes. Later purchased by
            Hooker Chemicals and Plastics Corp.,the landfill became a dumping
            ground for nearly 21,000 tons of mixed chemical wastes before being
            closed and covered over in the early 1950s.Shortly thereafter, the
            property was acquired by the Niagara Falls Board of Education, and
            schools and residences were built on and around the site.
        </p>
        <p>
            In the ensuing decades, groundwater levels in the area rose, parts
            of the landfill subsided, large metal drums of waste were
            uncovered, and toxic chemicals oozed out. All this led to the
            contamination of surface waters,oily residues in residential
            basements, corrosion of sump pumps,and noxious odors.Residents
            began to question if these problems were at the root of an apparent
            prevalence of birth defects and miscarriages in the neighborhood.
        </p>
        <p>
            Eventually, in 1978, the area was declared unsafe by the New York
            State Department of Health, and President Jimmy Carter approved
            emergency federal assistance. The school located on the landfill
            site was closed and nearby houses were condemned. State and federal
            agencies worked together to relocate hundreds of residents and
            contain or destroy the chemical wastes.
        </p>
        <p>
            That was the bitter story of Love Canal. Although not the worst
            environmental disaster in U.S. history, it illustrates the tragic
            consequences of water pollution.
        </p>
    </div>
    <p align="left">
        <strong>WATER QUALITY STANDARDS</strong>
    </p>
    <p>
        In addition to toxic chemical wastes, water pollutants occur in many
        other forms, including pathogenic microbes (harmful bacteria and
        viruses), excess fertilizers (containing compounds of phosphorus and
        nitrogen), and trash floating on streams, lakes, and beaches. Water
        pollution can also take the form of sediment eroded from stream banks,
        large blooms of algae, low levels of dissolved oxygen, or abnormally
        high temperatures (from the discharge of coolant water at power
        plants).
    </p>
    <p>
        The United States has seen a growing concern about water pollution
        since the middle of the twentieth century, as the public recognized
        that pollutants were adversely affecting human health and rendering
        lakes unswimmable, streams unfishable, and rivers flammable. In
        response, in 1972, Congress passed the Federal Water Pollution Control
        Act Amendments, later modified and referred to as the Clean Water Act.
        Its purpose was to “restore and maintain the chemical, physical, and
        biological integrity of the nation’s waters.”
    </p>
    <p>
        The Clean Water Act set the ambitious national goal of completely
        eliminating the discharge of pollutants into navigable waters by 1985,
        as well as the interim goal of making water clean enough to sustain
        fish and wildlife, while being safe for swimming and boating. To
        achieve these goals, certain standards for water quality were
        established.
    </p>
    <p>
        The “designated uses” of every body of water subject to the act must
        first be identified. Is it a source for drinking water? Is it used for
        recreation, such as swimming? Does it supply agriculture or industry?
        Is it a significant habitat for fish and other aquatic life?
        Thereafter, the water must be tested for pollutants. If it fails to
        meet the minimum standards for its designated uses, then steps must be
        taken to limit pollutants entering it, so that it becomes suitable for
        those uses.
    </p>
    <p>
        On the global level, the fundamental importance of clean water has come
        into the spotlight. In November 2002, the UN Committee on Economic,
        Cultural and Social Rights declared access to clean water a human
        right. Moreover, the United Nations has designated 2003 to be the
        International Year of Freshwater, with the aim of encouraging
        sustainable use of freshwater and integrated water resources
        management.
    </p>
    <p align="left">
        <strong>HERE, THERE, AND EVERYWHERE</strong>
    </p>
    <p>
        Implementing the Clean Water Act requires clarifying the sources of
        pollutants. They are divided into two groups: “point sources” and
        “nonpoint sources.” Point sources correspond to discrete, identifiable
        locations from which pollutants are emitted. They include factories,
        wastewater treatment plants, landfills, and underground storage tanks.
        Water pollution that originates at point sources is usually what is
        associated with headline-grabbing stories such as those about Love
        Canal.
    </p>
    <p>
        Nonpoint sources of pollution are diffuse and therefore harder to
        control. For instance, rain washes oil, grease, and solid pollutants
        from streets and parking lots into storm drains that carry them into
        bays and rivers. Likewise, irrigation and rainwater leach fertilizers,
        herbicides, and insecticides from farms and lawns and into streams and
        lakes.
    </p>
    <p>
        The direct discharge of wastes from point sources into lakes, rivers,
        and streams is regulated by a permit program known as the National
        Pollutant Discharge Elimination System (NPDES). This program,
        established through the Clean Water Act, is administered by the
        Environmental Protection Agency (EPA) and authorized states. By
        regulating the wastes discharged, NPDES has helped reduce point-source
        pollution dramatically. On the other hand, water pollution in the
        United States is now mainly from nonpoint sources, as reported by the
        EPA.
    </p>
    <p>
        In 1991, the U.S. Geological Survey (USGS, part of the Department of
        the Interior) began a systematic, long-term program to monitor
        watersheds. The National Water-Quality Assessment Program (NAWQA),
        established to help manage surface and groundwater supplies, has
        involved the collection and analysis of water quality data in over 50
        major river basins and aquifer systems in nearly all 50 states.
    </p>
    <p>
        The program has encompassed three principal categories of
        investigation: (1) the current conditions of surface water and
        groundwater; (2) changes in those conditions over time; and (3) major
        factors—such as climate, geography, and land use— that affect water
        quality. For each of these categories, the water and sediment have been
        tested for such pollutants as pesticides, plant nutrients, volatile
        organic compounds, and heavy metals.
    </p>
    <p>
        The NAWQA findings were disturbing. Water quality is most affected in
        watersheds with highest population density and urban development. In
        agricultural areas, 95 percent of tested streams and 60 percent of
        shallow wells contained herbicides, insecticides, or both. In urban
        areas, 99 percent of tested streams and 50 percent of shallow wells had
        herbicides, especially those used on lawns and golf courses.
        Insecticides were found more frequently in urban streams than in
        agricultural ones.
    </p>
    <p>
        The study also found large amounts of plant nutrients in water
        supplies. For instance, 80 percent of agricultural streams and 70
        percent of urban streams were found to contain phosphorus at
        concentrations that exceeded EPA guidelines.
    </p>
    <p>
        Moreover, in agricultural areas, one out of five well-water samples had
        nitrate concentrations higher than EPA standards for drinking water.
        Nitrate contamination can result from nitrogen fertilizers or material
        from defective septic systems leaching into the groundwater, or it may
        reflect defects in the wells.
    </p>
    <p align="left">
        <strong>EFFECTS OF POLLUTION</strong>
    </p>
    <p>
        According to the UN World Water Assessment Programme, about 2.3 billion
        people suffer from diseases associated with polluted water, and more
        than 5 million people die from these illnesses each year. Dysentery,
        typhoid, cholera, and hepatitis A are some of the ailments that result
        from ingesting water contaminated with harmful microbes. Other
        illnesses—such as malaria, filariasis, yellow fever, and sleeping
        sickness—are transmitted by vector organisms (such as mosquitoes and
        tsetse flies) that breed in or live near stagnant, unclean water.
    </p>
    <p>
        A number of chemical contaminants—including DDT, dioxins,
        polychlorinated biphenyls (PCBs), and heavy metals— are associated with
        conditions ranging from skin rashes to various cancers and birth
        defects. Excess nitrate in an infant’s drinking water can lead to the
        “blue baby syndrome” (methemoglobinemia)—a condition in which the
        child’s digestive system cannot process the nitrate, diminishing the
        blood’s ability to carry adequate concentrations of oxygen.
    </p>
    <p>
        Besides affecting human health, water pollution has adverse effects on
        ecosystems. For instance, while moderate amounts of nutrients in
        surface water are generally not problematic, large quantities of
        phosphorus and nitrogen compounds can lead to excessive growth of algae
        and other nuisance species. Known as <em>eutrophication, </em>this
        phenomenon reduces the penetration of sunlight through the water; when
        the plants die and decompose, the body of water is left with odors, bad
        taste, and reduced levels of dissolved oxygen.
    </p>
    <p>
        Low levels of dissolved oxygen can kill fish and shellfish. In
        addition, aquatic weeds can interfere with recreational activities
        (such as boating and swimming) and can clog intake by industry and
        municipal systems.
    </p>
    <p>
        Some pollutants settle to the bottom of streams, lakes, and harbors,
        where they may remain for many years. For instance, although DDT and
        PCBs were banned years ago, they are still found in sediments in many
        urban and rural streams. They occur at levels harmful to wildlife at
        more than two-thirds of the urban sites tested.
    </p>
    <p align="left">
        <strong>PREVENTION AND REMEDIATION</strong>
    </p>
    <p>
        As the old saying goes, an ounce of prevention is worth a pound of
        cure. This is especially true when it comes to controlling water
        pollution. Several important steps taken since the passage of the Clean
        Water Act have made surface waters today cleaner in many ways than they
        were 30 years ago.
    </p>
    <p>
        For example, industrial wastes are mandated to be neutralized or broken
        down before being discharged to streams, lakes, and harbors. Moreover,
        the U.S. government has banned the production and use of certain
        dangerous pollutants such as DDT and PCBs.
    </p>
    <p>
        In addition, two major changes have been introduced in the handling of
        sewage. First, smaller, less efficient sewage treatment plants are
        being replaced with modern, regional plants that include biological
        treatment, in which microorganisms are used to break down organic
        matter in the sewage. The newer plants are releasing much cleaner
        discharges into the receiving bodies of water (rivers, lakes, and
        ocean).
    </p>
    <p>
        Second, many jurisdictions throughout the United States are building
        separate sewer lines for storm water and sanitary wastes. These
        upgrades are needed because excess water in the older, “combined” sewer
        systems would simply bypass the treatment process, and untreated sewage
        would be discharged directly into receiving bodies of water.
    </p>
    <p>
        To minimize pollutants from nonpoint sources, the EPA is requiring all
        municipalities to address the problem of runoff from roads and parking
        lots. At the same time, the use of fertilizers and pesticides needs to
        be reduced. Toward this end, county extension agents are educating
        farmers and homeowners about their proper application and the
        availability of nutrient testing.
    </p>
    <p>
        To curtail the use of expensive and potentially harmful pesticides, the
        approach known as <em>integrated pest management </em>can be
        implemented. It involves the identification of specific pest problems
        and the use of nontoxic chemicals and chemicalfree alternatives
        whenever possible. For instance, aphids can be held in check by ladybug
        beetles and caterpillars can be controlled by applying neem oil to the
        leaves on which they feed.
    </p>
    <p>
        Moreover, new urban development projects in many areas are required to
        implement storm-water management practices. They include such features
        as oil and grease traps in storm drains; swales to slow down runoff,
        allowing it to infiltrate back into groundwater; “wet” detention basins
        (essentially artificial ponds) that allow solids to settle out of
        runoff; and artificial wetlands that help break down contaminants in
        runoff. While such additions may be costly, they significantly improve
        water quality. They are of course much more expensive to install after
        those areas have been developed.
    </p>
    <p>
        Once a waterway is polluted, cleanup is often expensive and time
        consuming. For instance, to increase the concentration of dissolved
        oxygen in a lake that has undergone eutrophication, fountains and
        aerators may be necessary. Specially designed boats may be needed to
        harvest nuisance weeds.
    </p>
    <p>
        At times, it is costly just to identify the source of a problem. For
        example, if a body of water contains high levels of coliform bacteria,
        expensive DNA testing may be needed to determine whether the bacteria
        came from leakage of human sewage, pet waste, or the feces of waterfowl
        or other wildlife.
    </p>
    <p>
        Contaminated sediments are sometimes difficult to treat. Available
        techniques range from dredging the sediments to “capping” them in
        place, to limit their potential exposure. Given that they act as
        reservoirs of pollutants, it is often best to remove the sediments and
        burn off the contaminants. Alternatively, the extracted sediments may
        be placed in confined disposal areas that prevent the pollutants from
        leaching back into groundwater. Dredging, however, may create
        additional problems by releasing pollutants back into the water column
        when the sediment is stirred up.
    </p>
    <p align="left">
        <strong>THE FUTURE OF CLEAN WATER</strong>
    </p>
    <p>
        The EPA reports that as a result of the Clean Water Act, millions of
        tons of sewage and industrial waste are being treated before they are
        discharged into U.S. coastal waters. In addition, the majority of lakes
        and rivers now meet mandated water quality goals.
    </p>
    <p>
        Yet the future of federal regulation under the Clean Water Act is
unclear. In 2001, a Supreme Court decision (<em>Solid Waste Agency of Northern Cook County </em>v.        <em>United States Army Corps of Engineers, et al.</em>) brought into
        question the power of federal agencies to regulate activities affecting
        water quality in smaller, nonnavigable bodies of water. This and
        related court decisions have set the stage for the EPA and other
        federal agencies to redefine which bodies of water can be protected
        from unregulated dumping and discharges under the Clean Water Act. As a
        result, individual states may soon be faced with much greater
        responsibility for the protection of water resources.
    </p>
    <p>
        Worldwide, more than one billion people presently lack access to clean
        water sources, and over two billion live without basic sanitation
        facilities. A large proportion of those who die from water-related
        diseases are infants. We would hope that by raising awareness of these
        issues on an international level, the newly recognized right to clean
        water will become a reality for a much larger percentage of the world’s
        population.
    </p>
    <p align="left">
        <strong>TRAGEDY AT MINAMATA BAY</strong>
    </p>
    <p>
        The Chisso chemical factory, located on the Japanese island of Kyushu,
        is believed to have discharged between 70 and 150 tons of methylmercury
        (an organic form of mercury) into Minamata Bay between 1932 and 1968.
        The factory, a dominant presence in the region, used the chemical to
        manufacture acetic acid and vinyl chloride.
    </p>
    <p>
        Methylmercury is easily absorbed upon ingestion, causing widespread
        damage to the central nervous system. Symptoms include numbing and
        unsteadiness of extremities, failure of muscular coordination, and
        impairment of speech, hearing, and vision. Exposure to high levels of
        the substance can be fatal. In addition, the effects are magnified for
        infants exposed to methylmercury through their mothers, both before
        birth and while nursing.
    </p>
    <p>
        In the 1960s and ’70s, it was revealed that thousands of Minamata Bay
        residents had been exposed to methylmercury. The chemical had been
        taken up from the bay’s waters by its fish and then made its way into
        the birds, cats, and people who ate the fish. Consequently,
        methylmercury poisoning came to be called Minamata disease.
    </p>
    <p>
        Remediation, which took as long as 14 years, involved removing the
        mercury-filled sediments and containing them on reclaimed land in
        Minamata Bay. Fish in the bay had such high levels of methylmercury
        that they had to be prevented from leaving the bay by a huge net, which
        was in place from 1974 to 1997.
    </p>
    <p>
        Mercury poisoning has recently appeared in the Amazon basin, where
        deforestation has led to uncontrolled runoff of natural accumulations
        of mercury from the soil into rivers and streams. In the United States,
        testing has revealed that predator fish such as bass and walleye in
        certain lakes and rivers contain enough mercury to justify warnings
        against consuming them in large amounts.
    </p>
