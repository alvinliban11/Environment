        <p align="left">
            Foreword
        </p>
        <p align="left">
            by David Seideman, Editor-in-Chief, <em>Audubon </em>Magazine
        </p>
        <p>
            For anyone contemplating the Earth’s fate, there’s probably no more
            instructive case study than the Florida Everglades. When European
            explorers first arrived there in the mid-1800s, they discovered a
            lush, tropical wilderness with dense sawgrass, marshes, mangrove
            forests, lakes, and tree islands. By the early 20<sup>th </sup>
            century, developers and politicians had begun building a series of
            canals and dikes to siphon off the region’s water. They succeeded
            in creating an agricultural and real estate boom, and to some
            degree, they offset floods and droughts. But the ecological cost
            was exorbitant. Today, half of the Everglades’ wetlands have been
            lost, its water is polluted by runoff from farms, and much of its
            wildlife, including Florida panthers and many wading birds such as
            wood storks, are hanging on by a thread.
        </p>
        <p>
            Yet there has been a renewed sense of hope in the Everglades since
            2001, when the state of Florida and the federal government approved
            a comprehensive $7.8 billion restoration plan, the biggest recovery
            of its kind in history. During the next four decades, ecologists
            and engineers will work to undo years of ecological damage by
            redirecting water back into the Everglades’ dried-up marshes. “The
            Everglades are a test,” says Joe Podger, an environmentalist.“If we
            pass, we get to keep the planet.”
        </p>
        <p>
            In fact, as this comprehensive series on environmental issues
            shows, humankind faces a host of tests that will determine whether
            we get to keep the planet. The world’s crises—air and water
            pollution, the extinction of species, and climate change— are
            worsening by the day. The solutions—and there are many practical
            ones—all demand an extreme sense of urgency. E. O. Wilson, the
            noted Harvard zoologist, contends that “the world environment is
            changing so fast that there is a window of opportunity that will
            close in as little time as the next two or three decades.” While
            Wilson’s main concern is the rapid loss of biodiversity, he could
            have just as easily been discussing climate change or wetlands
            destruction.
        </p>
        <p>
            The Earth is suffering the most massive extinction of species since
            the die-off of dinosaurs 65 million years ago. “If
        </p>
        <p align="center">
            VIII
        </p>
    </div>
    <div>
        <p>
            we continue at the current rate of deforestation and destruction of
            major ecosystems like rain forests and coral reefs, where most of
            the biodiversity is concentrated,” Wilson says,“we will surely lose
            more than half of all the species of plants and animals on Earth by
            the end of the 21<sup>st </sup>century.”
        </p>
        <p>
            Many conservationists still mourn the loss of the passenger pigeon,
            which, as recently as the late 1800s, flew in miles-long flocks so
            dense they blocked the sun, turning noontime into nighttime. By
            1914, target shooters and market hunters had reduced the species to
            a single individual, Martha, who lived at the Cincinnati Zoo until,
            as Peter Matthiessen wrote in <em>Wildlife in America, </em>“she
            blinked for the last time.” Despite U.S. laws in place to avert
            other species from going the way of the passenger pigeon, the
            latest news is still alarming. In its 2004 State of the Birds
            report, Audubon noted that 70% of grassland bird species and 36% of
            shrubland bird species are suffering significant declines. Like the
            proverbial canary in the coalmine, birds serve as indicators,
            sounding the alarm about impending threats to environmental and
            human health.
        </p>
        <p>
            Besides being an unmitigated moral tragedy, the disappearance of
            species has profound practical implications. Ninety percent of the
            world’s food production now comes from about a dozen species of
            plants and eight species of livestock. Geneticists rely on wild
            populations to replenish varieties of domestic corn, wheat, and
            other crops, and to boost yields and resistance to disease. “Nature
            is a natural pharmacopoeia, and new drugs and medicines are being
            discovered in the wild all the time,” wrote Niles Eldredge of the
            American Museum of Natural History, a noted author on the subject
            of extinction. “Aspirin comes from the bark of willow trees.
            Penicillin comes from a mold, a type of fungus.” Furthermore,
            having a wide array of plants and animals improves a region’s
            capacity to cleanse water, enrich soil, maintain stable climates,
            and produce the oxygen we breathe.
        </p>
        <p>
            Today, the quality of the air we breathe and the water we drink
            does not augur well for our future health and well-being. Many
            people assume that the passage of the Clean Air Act in 1970
        </p>
        <p align="left">
            X Foreword
        </p>
        <p>
            ushered in a new age.But the American Lung Association reports that
            159 million Americans—55% of the population—are exposed to
            “unhealthy levels of air pollution.” Meanwhile, the American Heart
            Association warns of a direct link between exposure to air
            pollution and heart disease and strokes.While it’s true that U.S.
            waters are cleaner than they were three decades ago, data from the
            Environmental Protection Agency (EPA) shows that almost half of
            U.S. coastal waters fail to meet water-quality standards because
            they cannot support fishing or swimming. Each year, contaminated
            tap water makes as many as 7 million Americans sick. The chief
            cause is “non-point pollution,” runoff that includes fertilizers
            and pesticides from farms and backyards as well as oil and chemical
            spills. On a global level, more than a billion people lack access
            to clean water; according to the United Nations, five times that
            number die each year from malaria and other illnesses associated
            with unsafe water.
        </p>
        <p>
            Of all the Earth’s critical environmental problems, one trumps the
            rest: climate change. Carol Browner, the EPA’s chief from 1993
            through 2001 (the longest term in the agency’s history), calls
            climate change “the greatest environmental health problem the world
            has ever seen.” Industry and people are spewing carbon dioxide from
            smokestacks and the tailpipes of their cars into the
            atmosphere,where a buildup of gases,acting like the glass in a
            greenhouse, traps the sun’s heat. The 1990s was the warmest decade
            in more than a century, and 1998 saw the highest global
            temperatures ever.In an article about global climate change in the
            December 2003 issue of <em>Audubon</em>, David Malakoff wrote,
            “Among the possible consequences: rising sea levels that cause
            coastal communities to sink beneath the waves like a modern
            Atlantis, crop failures of biblical proportions, and once-rare
            killer storms that start to appear with alarming regularity.”
        </p>
        <p>
            Yet for all the doom and gloom, scientists and environmentalists
            hold out hope. When Russia recently ratified the Kyoto Protocol, it
            meant that virtually all of the world’s industrialized nations—the
            United States, which has refused to sign, is a notable
            exception—have committed to cutting greenhouse gases. As Kyoto and
            other international agreements go into effect, a market is
            developing for cap-and-trade systems for carbon dioxide. In this
            country, two dozen big corporations, including British Petroleum,
            are cutting emissions. At least 28 American states have adopted
            their own policies. California, for example,has passed global
            warming legislation aimed at curbing emissions from new cars.
            Governor Arnold Schwarzenegger has also backed regulations
            requiring automakers to slash the amount of greenhouse gases they
            cause by up to 30% by 2016, setting a precedent for other states.
        </p>
        <p>
            As Washington pushes a business-friendly agenda, states are filling
            in the policy vacuum in other areas, as well. California and New
            York are developing laws to preserve wetlands, which filter
            pollutants, prevent floods, and provide habitat for endangered
            wildlife.
        </p>
        <p>
            By taking matters into their own hands, states and foreign
            countries will ultimately force Washington’s. What industry
            especially abhors is a crazy quilt of varying rules.After all,it
            makes little sense for a company to invest a billion dollars in a
            power plant only to find out later that it has to spend even more
            to comply with another state’s stricter emissions standards. Ford
            chairman and chief executive William Ford has lashed out at the
            states’“patchwork” approach because he and “other manufacturers
            will have a hard time responding.”Further, he wrote in a letter to
            his company’s top managers, “the prospect of 50 different
            requirements in 50 different states would be nothing short of
            chaos.” The type of fears Ford expresses are precisely the reason
            federal laws protecting clean air and water came into being.
        </p>
        <p>
            Governments must take the lead, but ecologically conscious
            consumers wield enormous influence, too. Over the past four
            decades, the annual use of pesticides has more than doubled, from
            215 million pounds to 511 million pounds. Each year, these poisons
            cause $10 billion worth of damage to the environment and kill 72
            million birds. The good news is that the demand for organic
            products is revolutionizing agriculture, in part by creating a
            market for natural alternatives for pest control. Some industry
            experts predict that by 2007 the organic industry will almost
            quadruple, to more than $30 billion.
        </p>
        <p align="left">
            XII Foreword
        </p>
        <p>
            E. O. Wilson touts “shade-grown” coffee as one of many “personal
            habitats that, if moderated only in this country, could contribute
            significantly to saving endangered species.” In the mountains of
            Mexico and Central America, coffee grown beneath a dense forest
            canopy rather than in cleared fields helps provide refuge for
            dozens of wintering North American migratory bird species, from
            western tanagers to Baltimore orioles.
        </p>
        <p>
            With conservation such a huge part of Americans’ daily routine,
            recycling has become as ingrained a civic duty as obeying traffic
            lights.Californians,for their part,have cut their energy
            consumption by 10% each year since the state’s 2001 energy crisis.
            “Poll after poll shows that about two-thirds of the American
            public—Democrat and Republican, urban and rural—consider
            environmental progress crucial,” writes Carl Pope, director of the
            Sierra Club, in his recent book, <em>Strategic Ignorance. </em>
            “Clean air, clean water, wilderness preservation— these are such
            bedrock values that many polling respondents find it hard to
            believe that any politician would oppose them.”
        </p>
        <p>
            Terrorism and the economy clearly dwarfed all other issues in the
            2004 presidential election. Even so, voters approved 120 out of 161
            state and local conservation funding measures nationwide, worth a
            total of $3.25 billion. Anti-environment votes in the U.S. Congress
            and proposals floated by the like-minded Bush administration should
            not obscure the salient fact that so far there have been no changes
            to the major environmental laws. The potential for political
            fallout is too great.
        </p>
        <p>
            The United States’ legacy of preserving its natural heritage is the
            envy of the world. Our national park system alone draws more than
            300 million visitors each year. Less well known is the 103-year-old
            national wildlife refuge system you’ll learn about in this series.
            Its unique mission is to safeguard the nation’s wild animals and
            plants on 540 refuges, protecting 700 species of birds and an equal
            number of other vertebrates; 282 of these species are either
            threatened or endangered. One of the many species particularly
            dependent on the invaluable habitat refuges afford is the bald
            eagle. Such safe havens, combined with the banning of the
            insecticide DDT and enforcement of the Endangered Species Act, have
            led to the bald eagle’s remarkable recovery, from a low of 500
            breeding pairs in 1963 to 7,600 today. In fact, this bird, the
            national symbol of the United States, is about be removed from the
            endangered species list and downgraded to a less threatened status
            under the CITES, the Convention on International Trade in
            Endangered Species.
        </p>
        <p>
            This vital treaty, upheld by the United States and 165 other
            participating nations (and detailed in this series),underscores the
            worldwide will to safeguard much of the Earth’s magnificent
            wildlife. Since going into effect in 1975, CITES has helped enact
            plans to save tigers, chimpanzees, and African elephants. These
            species and many others continue to face dire threats from
            everything from poaching to deforestation. At the same time,
            political progress is still being made.Organizations like the World
            Wildlife Fund work tirelessly to save these species from extinction
            because so many millions of people care. China, for example, the
            most populous nation on Earth, is so concerned about its giant
            pandas that it has implemented an ambitious captive breeding
            program. That program’s success, along with government measures
            prohibiting logging throughout the panda’s range, may actually
            enable the remaining population of 1,600 pandas to hold its own—and
            perhaps grow. “For the People’s Republic of China, pressure
            intensified as its internationally popular icon edged closer to
extinction,” wrote Gerry Ellis in a recent issue of            <em>National Wildlife. </em>“The giant panda was not only a poster
            child for endangered species, it was a symbol of our willingness to
            ensure nature’s place on Earth.”
        </p>
        <p>
            Whether people take a spiritual path to conservation or a pragmatic
            one, they ultimately arrive at the same destination. The sight of a
            bald eagle soaring across the horizon reassures us about nature’s
            resilience, even as the clean air and water we both need to survive
            becomes less of a certainty. “The conservation of our natural
            resources and their proper use constitute the fundamental problem
            which underlies almost every other problem of our national
            life,”President Theodore Roosevelt told Congress at the dawn of the
            conservation movement a century ago. His words ring truer today
            than ever.
        </p>
        <p align="left">
            Introduction: “Why Should We Care?”
        </p>
        <p>
            Our nation’s air and water are cleaner today than they were 30
            years ago. After a century of filling and destroying over half of
            our wetlands, we now protect many of them. But the Earth is getting
            warmer, habitats are being lost to development and logging, and
            humans are using more water than ever before. Increased use of
            water can leave rivers, lakes, and wetlands without enough water to
            support the native plant and animal life. Such changes are causing
            plants and animals to go extinct at an increased rate. It is no
            longer a question of losing just the dodo birds or the passenger
            pigeons, argues David Quammen, author of <em>Song of the Dodo</em>:
“Within a few decades, if present trends continue, we’ll be losing            <em>a lot </em>of everything.”<sup>1</sup>
        </p>
        <p>
            In the 1980s, E. O. Wilson, a Harvard biologist and Pulitzer
            Prize–winning author, helped bring the term <em>biodiversity </em>
            into public discussions about conservation. <em>Biodiversity</em>,
            short for “biological diversity,” refers to the levels of
            organization for living things. Living organisms are divided and
            categorized into ecosystems (such as rain forests or oceans), by
            species (such as mountain gorillas), and by genetics (the genes
            responsible for inherited traits).
        </p>
        <p>
            Wilson has predicted that if we continue to destroy habitats and
            pollute the Earth at the current rate, in 50 years, we could lose
30 to 50% of the planet’s species to extinction. In his 1992 book,<em>The Diversity of Life</em>, Wilson asks: “Why should we care?”            <sup>2 </sup>His long list of answers to this question includes:
            the potential loss of vast amounts of scientific information that
            would enable the development of new crops, products, and medicines
            and the potential loss of the vast economic and environmental
            benefits of healthy ecosystems. He argues that since we have only a
            vague idea (even with our advanced scientific methods) of how
            ecosystems really work, it would be “reckless” to suppose that
            destroying species indefinitely will not threaten us all in ways we
            may not even understand.
        </p>
        <p align="left">
            <strong>THE BOOKS IN THE SERIES</strong>
        </p>
        <p>
            In looking at environmental issues, it quickly becomes clear that,
            as naturalist John Muir once said, “When we try to pick
        </p>
        <p align="center">
            XIV
        </p>
    </div>
    <div>
        <p>
            out anything by itself, we find it hitched to everything else in
            the Universe.”<sup>3 </sup>For example, air pollution in one state
            or in one country can affect not only air quality in another place,
            but also land and water quality.Soil particles from degraded
            African lands can blow across the ocean and cause damage to far-off
            coral reefs.
        </p>
        <p>
            The six books in this series address a variety of environmental
            issues: conservation, wildlife protection, water pollution, air
            quality, climate change, and environmental policy. None of these
            can be viewed as a separate issue. Air quality impacts climate
            change,wildlife,and water quality.Conservation initiatives directly
            affect water and air quality, climate change, and wildlife
            protection. Endangered species are touched by each of these issues.
            And finally, environmental policy issues serve as important tools
            in addressing all the other environmental problems that face us.
        </p>
        <p>
            You can use the burning of coal as an example to look at how a
            single activity directly “hitches” to a variety of environmental
            issues. Humans have been burning coal as a fuel for hundreds of
            years. The mining of coal can leave the land stripped of
            vegetation, which erodes the soil. Soil erosion contributes to
            particulates in the air and water quality problems. Mining coal can
            also leave piles of acidic tailings that degrade habitats and
            pollute water. Burning any fossil fuel—coal, gas, or oil—releases
            large amounts of carbon dioxide into the atmosphere. Carbon dioxide
            is considered a major “greenhouse gas” that contributes to global
            warming—the gradual increase in the Earth’s temperature over time.
            In addition, coal burning adds sulfur dioxide to the air, which
            contributes to the formation of acid rain—precipitation that is
            abnormally acidic. This acid rain can kill forests and leave lakes
            too acidic to support life. Technology continues to present ways to
            minimize the pollution that results from extracting and burning
            fossil fuels. Clean air and climate change policies guide states
            and industries toward implementing various strategies and
            technologies for a cleaner coal industry.
        </p>
        <p>
            Each of the six books in this series—ENVIRONMENTAL ISSUES—
            introduces the significant points that relate to the specific topic
            and explains its relationship to other environmental concerns.
        </p>
        <p align="left">
            <strong>Book One: <em>Air Quality</em></strong>
        </p>
        <p>
            Problems of air pollution can be traced back to the time when
            humans first started to burn coal. <em>Air Quality </em>looks at
            today’s challenges in fighting to keep our air clean and safe. The
            book includes discussions of air pollution sources—car and truck
            emissions, diesel engines, and many industries. It also discusses
            their effects on our health and the environment.
        </p>
        <p>
            The Environmental Protection Agency (EPA) has reported that more
            than 150 million Americans live in areas that have unhealthy levels
            of some type of air pollution.<sup>4 </sup>Today, more than 20
            million Americans, over 6 million of whom are children, suffer from
asthma believed to be triggered by pollutants in the air.            <sup>5</sup>
        </p>
        <p>
            In 1970, Congress passed the Clean Air Act, putting in place an
            ambitious set of regulations to address air pollution concerns. The
            EPA has identified and set standards for six common air pollutants:
            ground-level ozone, nitrogen oxides, particulate matter, sulfur
            dioxide, carbon monoxide, and lead.
        </p>
        <p>
            The EPA has also been developing the Clean Air Rules of 2004,
            national standards aimed at improving the country’s air quality by
            specifically addressing the many sources of contaminants. However,
            many conservation organizations and even some states have concerns
            over what appears to be an attempt to weaken different sections of
            the 1990 version of the Clean Air Act. The government’s
            environmental protection efforts take on increasing importance
            because air pollution degrades land and water, contributes to
            global warming, and affects the health of plants and animals,
            including humans.
        </p>
        <p align="left">
            <strong>Book Two: <em>Climate Change</em></strong>
        </p>
        <p>
            Part of science is observing patterns, and scientists have observed
            a global rise in temperature. <em>Climate Change </em>discusses the
            sources and effects of global warming. Scientists attribute this
            accelerated change to human activities such as the burning of
            fossil fuels that emit greenhouse gases (GHG).<sup>6 </sup>Since
            the 1700s, we have been cutting down the trees that help remove
            carbon dioxide from the atmosphere, and have increased the amount
            of coal, gas, and oil we burn, all of which add carbon dioxide to
            the atmosphere. Science tells us that these human activities have
            caused greenhouse gases—carbon dioxide (CO<sub>2</sub>), methane
            (CH<sub>4</sub>), nitrous oxide (N<sub>2</sub>O),
            hydrofluorocarbons (HFCs), perfluorocarbons (PFCs), and sulfur
hexafluoride (SF<sub>6</sub>)—to accumulate in the atmosphere.            <sup>7</sup>
        </p>
        <p>
            If the warming patterns continue, scientists warn of more negative
            environmental changes. The effects of climate change, or global
            warming, can be seen all over the world. Thousands of scientists
            are predicting rising sea levels, disturbances in patterns of
            rainfall and regional weather, and changes in ranges and
            reproductive cycles of plants and animals. Climate change is
already having some effects on certain plant and animal species.            <sup>8</sup>
        </p>
        <p>
            Many countries and some American states are already working
            together and with industries to reduce the emissions of greenhouse
            gases. Climate change is an issue that clearly fits noted scientist
            Rene Dubois’s advice:“Think globally, act locally.”
        </p>
        <p align="left">
            <strong>Book Three: <em>Conservation</em></strong>
        </p>
        <p>
            <em>Conservation </em>
            considers the issues that affect our world’s vast array of living
            creatures and the land, water, and air they need to survive.
        </p>
        <p>
            One of the first people in the United States to put the political
            spotlight on conservation ideas was President Theodore Roosevelt.
            In the early 1900s, he formulated policies and created programs
            that addressed his belief that: “The nation behaves well if it
            treats the natural resources as assets which it must turn over to
the next generation increased, and not impaired, in value.”<sup>9 </sup>In the 1960s, biologist Rachel Carson’s book,            <em>Silent Spring</em>, brought conservation issues into the public
            eye. People began to see that polluted land, water, and air
            affected their health. The 1970s brought the creation of the United
            States Environmental Protection Agency (EPA) and passage of many
            federal and state rules and regulations to protect the quality of
            our environment and our health.
        </p>
        <p>
            Some 80 years after Theodore Roosevelt established the first
            National Wildlife Refuge in 1903, Harvard biologist
        </p>
        <p>
            E. O. Wilson brought public awareness of conservation issues to a
            new level. He warned:
        </p>
        <p>
            . . . the worst thing that will probably happen—in fact is already
            well underway—is not energy depletion, economic collapse,
            conventional war, or even the expansion of totalitarian
            governments.As terrible as these catastrophes would be for us, they
            can be repaired within a few generations. The one process now
            ongoing that will take million of years to correct is the loss of
            genetic species diversity by the destruction of natural habitats.
This is the folly our descendants are least likely to forgive us.            <sup>10</sup>
        </p>
        <p>
            To heed Wilson’s warning means we must strive to protect
            species-rich habitats, or “hotspots,” such as tropical rain forests
            and coral reefs. It means dealing with conservation concerns like
            soil erosion and pollution of fresh water and of the oceans. It
            means protecting sea and land habitats from the overexploitation of
            resources. And it means getting people involved on all levels—from
            national and international government agencies, to private
            conservation organizations, to the individual person who recycles
            or volunteers to listen for the sounds of frogs in the spring.
        </p>
        <p align="left">
            <strong>Book Four: <em>Environmental Policy</em></strong>
        </p>
        <p>
            One approach to solving environmental problems is to develop
            regulations and standards of safety. Just as there are rules for
            living in a community or for driving on a road, there are
            environmental regulations and policies that work toward protecting
            our health and our lands. <em>Environmental Policy </em>discusses
            the regulations and programs that have been crafted to address
            environmental issues at all levels—global, national, state, and
            local.
        </p>
        <p>
            Today, as our resources become increasingly limited, we witness
            heated debates about how to use our public lands and how to protect
            the quality of our air and water. Should we allow drilling in the
            Arctic National Wildlife Refuge? Should we protect more marine
            areas? Should we more closely regulate the emissions of vehicles,
            ships, and industries? These policy issues, and many more, continue
            to make news on a daily basis.
        </p>
        <p>
            In addition, environmental policy has taken a place on the
            international front. Hundreds of countries are working together in
            a variety of ways to address such issues as global warming, air
            pollution, water pollution and supply, land preservation, and the
            protection of endangered species. One question the United States
            continues to debate is whether to sign the 1997 Kyoto Protocol, the
            international agreement designed to decrease the emissions of
            greenhouse gases.
        </p>
        <p>
            Many of the policy tools for protecting our environment are already
            in place. It remains a question how they will be used— and whether
            they will be put into action in time to save our natural resources
            and ourselves.
        </p>
        <p align="left">
            <strong>Book Five: <em>Water Pollution</em></strong>
        </p>
        <p>
            Pollution can affect water everywhere. Pollution in lakes and
            rivers is easily seen. But water that is out of our plain view can
            also be polluted with substances such as toxic chemicals,
fertilizers, pesticides, oils, and gasoline.            <em>Water Pollution </em>considers issues of concern to our surface
            waters, our groundwater, and our oceans.
        </p>
        <p>
            In the early 1970s, about three-quarters of the water in the United
            States was considered unsafe for swimming and fishing. When Lake
            Erie was declared “dead” from pollution and a river feeding it
            actually caught on fire, people decided that the national
            government had to take a stronger role in protecting our resources.
            In 1972, Congress passed the Clean Water Act, a law whose objective
            “is to restore and maintain the chemical, physical, and biological
            integrity of the Nation’s waters.”<sup>11 </sup>Today, over 30
            years later, many lakes and rivers have been restored to health.
            Still, an estimated 40% of our waters are still unsafe to swim in
            or fish.
        </p>
        <p>
            Less than 1% of the available water on the planet is fresh water.
            As the world’s population grows, our demand for drinking and
            irrigation water increases. Therefore, the quantity of available
            water has become a major global issue. As Sandra Postel, a leading
            authority on international freshwater issues, says, “Water scarcity
is now the single biggest threat to global food production.”            <sup>12 </sup>Because there are many competing demands for water,
            including the needs of habitats, water pollution continues to
            become an even more serious problem each year.
        </p>
        <p align="left">
            <strong>Book Six: <em>Wildlife Protection</em></strong>
        </p>
        <p>
            For many years, the word <em>wildlife </em>meant only the animals
            that people hunted for food or for sport. It was not until 1986
            that the Oxford English Dictionary defined <em>wildlife </em>as
“the native fauna and flora of a particular region.” <sup>13 </sup>            <em>Wildlife Protection </em>looks at overexploitation—for example,
            overfishing or collecting plants and animals for illegal trade—and
            habitat loss. Habitat loss can be the result of development,
            logging, pollution, water diverted for human use, air pollution,
            and climate change.
        </p>
        <p>
            Also discussed are various approaches to wildlife protection. Since
            protection of wildlife is an issue of global concern, it is
            addressed here on international as well as on national and local
            levels. Topics include voluntary international organizations such
            as the International Whaling Commission and the CITES agreements on
            trade in endangered species. In the United States, the Endangered
            Species Act provides legal protection for more than 1,200 different
            plant and animal species. Another approach to wildlife protection
            includes developing partnerships among conservation organizations,
            governments, and local people to foster economic incentives to
            protect wildlife.
        </p>
        <p align="left">
            <strong>CONSERVATION IN THE UNITED STATES</strong>
        </p>
        <p>
            Those who first lived on this land, the Native American peoples,
            believed in general that land was held in common, not to be
            individually owned, fenced, or tamed. The white settlers from
            Europe had very different views of land. Some believed the New
            World was a Garden of Eden. It was a land of opportunity for them,
            but it was also a land to be controlled and subdued. Ideas on how
            to treat the land often followed those of European thinkers like
            John Locke, who believed that “Land that is left wholly to nature
            is called, as indeed it is, waste.” <sup>14</sup>
        </p>
        <p>
            The 1800s brought another way of approaching the land. Thinkers
            such as Ralph Waldo Emerson, John Muir, and Henry David Thoreau
            celebrated our human connection with nature. By the end of the
            1800s, some scientists and policymakers were noticing the damage
            humans have caused to the land. Leading public officials preached
            stewardship and wise use of our country’s resources. In 1873,
            Yellowstone National Park was set up. In 1903, the first National
            Wildlife Refuge was established.
        </p>
        <p>
            However, most of the government practices until the middle of the
            20<sup>th </sup>century favored unregulated development and use of
            the land’s resources. Forests were clear cut, rivers were dammed,
            wetlands were filled to create farmland, and factories were allowed
            to dump their untreated waste into rivers and lakes.
        </p>
        <p>
            In 1949, a forester and ecologist named Aldo Leopold revived the
            concept of preserving land for its own sake. But there was now a
            biological, or scientific, reason for conservation, not just a
            spiritual one. Leopold declared: “All ethics rest upon a single
            premise: that the individual is a member of a community of
            interdependent parts. . . . A thing is right when it tends to
            preserve the integrity and stability and beauty of the biotic
            community. It is wrong when it tends otherwise.” <sup>15</sup>
        </p>
        <p>
            The fiery vision of these conservationists helped shape a more
            far-reaching movement that began in the 1960s. Many credit Rachel
Carson’s eloquent and accessible writings, such as her 1962 book            <em>Silent Spring</em>, with bringing environmental issues into
            people’s everyday language. When the Cuyahoga River in Ohio caught
            fire in 1969 because it was so polluted, it captured the public
            attention. Conservation was no longer just about protecting land
            that many people would never even see, it was about protecting
            human health. The condition of the environment had become personal.
        </p>
        <p>
            In response to the public outcry about water and air pollution, the
            1970s saw the establishment of the EPA. Important legislation to
            protect the air and water was passed. National standards for a
            cleaner environment were set and programs were established to help
            achieve the ambitious goals. Conservation organizations grew from
            what had started as exclusive white men’s hunting clubs to interest
            groups with a broad membership base. People came together to demand
            changes that would afford more protection to the environment and to
            their health.
        </p>
        <p>
            Since the 1960s, some presidential administrations have sought to
            strengthen environmental protection and to protect more land and
            national treasures. For example, in 1980, President Jimmy Carter
            signed an act that doubled the amount of protected land in Alaska
            and renamed it the Arctic National Wildlife Refuge. Other
            administrations, like those of President Ronald Reagan, sought to
            dismantle many earlier environmental protection initiatives.
        </p>
        <p>
            The environmental movement, or environmentalism, is not one single,
            homogeneous cause. The agencies, individuals, and organizations
            that work toward protecting the environment vary as widely as the
            habitats and places they seek to protect. There are individuals who
            begin grass-roots efforts—people like Lois Marie Gibbs, a former
            resident of the polluted area of Love Canal, New York, who founded
            the Center for Health, Environment and Justice. There are
            conservation organizations, like The Nature Conservancy, the World
            Wildlife Fund (WWF), and Conservation International, that sponsor
            programs to preserve and protect habitats. There are groups that
            specialize in monitoring public policy and legislation—for example,
            the Natural Resources Defense Council and Environmental Defense. In
            addition, there are organizations like the Audubon Society and the
            National Wildlife Federation whose focus is on public education
            about environmental issues. Perhaps from this diversity, just like
            there exists in a healthy ecosystem, will come the strength and
            vision environmentalism needs to deal with the continuing issues of
            the 21<sup>st </sup>century.
        </p>
        <p align="left">
            <strong>INTERNATIONAL CONSERVATION EFFORTS</strong>
        </p>
        <p>
            In his book <em>Biodiversity</em>, E. O. Wilson cautions that
            biological diversity must be taken seriously as a global resource
            for three reasons. First, human population growth is accelerating
            the degrading of the environment, especially in tropical countries.
            Second, science continues to discover new uses for biological
            diversity—uses that can benefit human health and protect the
            environment. And third, much biodiversity is being lost through
            extinction, much of it in the tropics. As Wilson states, “We must
            hurry to acquire the knowledge on which a wise policy of
conservation and development can be based for centuries to come.”            <sup>16</sup>
        </p>
        <p>
            People organize themselves within boundaries and borders. But
            oceans, rivers, air, and wildlife do not follow such rules.
            Pollution or overfishing in one part of an ocean can easily degrade
            the quality of another country’s resources. If one country diverts
            a river, it can destroy another country’s wetlands or water
            resources. When Wilson cautions us that we must hurry to develop a
            wise conservation policy, he means a policy that will protect
            resources all over the world.
        </p>
        <p>
            To accomplish this will require countries to work together on
            critical global issues: preserving biodiversity, reducing global
            warming, decreasing air pollution, and protecting the oceans. There
            are many important international efforts already going on to
            protect the resources of our planet. Some efforts are regulatory,
            while others are being pursued by nongovernmental organizations or
            private conservation groups.
        </p>
        <p>
            Countries volunteering to cooperate to protect resources is not a
            new idea. In 1946, a group of countries established the
            International Whaling Commission (IWC). They recognized that
            unregulated whaling around the world had led to severe declines in
            the world’s whale populations. In 1986, the IWC declared a
            moratorium on whaling, which is still in effect, until the
            populations have recovered.<sup>17 </sup>Another example of
            international cooperation occurred in 1987 when various countries
            signed the Montreal Protocol to reduce the emissions of
            ozone-depleting gases. It has been a huge success, and perhaps has
            served as a model for other international efforts, like the 1997
            Kyoto Protocol, to limit emissions of greenhouse gases.
        </p>
        <p>
            Yet another example of international environmental cooperation is
            the CITES agreement (the Convention on International Trade in
            Endangered Species of Wild Fauna and Flora), a legally binding
            agreement to ensure that the international trade of plants and
            animals does not threaten the species’ survival. CITES went into
            force in 1975 after 80 countries agreed to the terms. Today, it has
            grown to include more than 160 countries. This make
        </p>
        <p>
            CITES among the largest conservation agreements in existence.
