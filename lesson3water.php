        <p align="left">
            <strong>Trashed: Across the Pacific Oceans, </strong>
        </p>
        <p align="left">
            <strong>Plastics, Plastics Everywhere</strong>
        </p>
        <p align="left">
            by Charles Moore
        </p>
        <p>
            It was on our way home,after finishing the Los Angeles-to-Hawaii
            sail race known as the Transpac, that my crew and I first caught
            sight of the trash,floating in one of the most remote regions of
all the oceans.I had entered my cutter-rigged research vessel,            <em>Alguita</em>, an aluminum-hulled catamaran, in the race to test
            a new mast. Although <em>Alguita </em>was built for research
            trawling, she was also a smart sailor, and she fit into the
            “cruising class”of boats that regularly enter the race. We did
            well, hitting a top speed of twenty knots under sail and winning a
            trophy for finishing in third place.
        </p>
        <p>
            Throughout the race our strategy, like that of every other boat in
            the race, had been mainly to avoid the North Pacific subtropical
            gyre—the great high-pressure system in the central Pacific Ocean
            that, most of the time, is centered just north of the racecourse
            and halfway between Hawaii and the mainland. But after our success
            with the race we were feeling mellow and unhurried, and our vessel
            was equipped with auxiliary twin diesels and carried an extra
            supply of fuel. So on the way back to our home port in Long Beach,
            California, we decided to take a shortcut through the gyre, which
            few seafarers ever cross. Fishermen shun it because its waters lack
            the nutrients to support an abundant catch. Sailors dodge it
            because it lacks the wind to propel their sailboats.
        </p>
        <p>
            I often struggle to find words that will communicate the vastness
            of the Pacific Ocean to people who have never been to sea. Day
            after day, <em>Alguita </em>was the only vehicle on a highway
            without landmarks, stretching from horizon to horizon. Yet as I
            gazed from the deck at the surface of what ought to have been a
            pristine ocean, I was confronted, as far as the eye could see, with
            the sight of plastic.
        </p>
        <p>
            It seemed unbelievable, but I never found a clear spot. In the week
            it took to cross the subtropical high, no matter what time of day I
            looked, plastic debris was floating everywhere: bottles, bottle
            caps, wrappers, fragments. Months later, after I discussed what I
            had seen with the oceanographer Curtis Ebbesmeyer, perhaps the
            world’s leading expert on flotsam, he began referring to the area
            as the “eastern garbage patch.” But “patch” doesn’t begin to convey
            the reality. Ebbesmeyer has estimated that the area, nearly covered
            with floating plastic debris, is roughly the size of Texas.
        </p>
        <p>
            My interest in marine debris did not begin with my crossing of the
            North Pacific subtropical gyre. Voyaging in the Pacific has been
            part of my life since earliest childhood. In fifty-odd years as a
            deckhand, stock tender, able seaman, and now captain, I became
            increasingly alarmed by the growth in plastic debris I was seeing.
            But the floating plastics in the gyre galvanized my interest.
        </p>
        <p>
            I did a quick calculation, estimating the debris at half a pound
            for every hundred square meters of sea surface. Multiplied by the
            circular area defined by our roughly thousand-mile course through
            the gyre, the weight of the debris was about 3 million tons,
            comparable to a year’s deposition at Puente Hills, Los Angeles’s
            largest landfill. I resolved to return someday to test my alarming
            estimate.
        </p>
        <p>
            Historically, the kind of drastic accumulation I encountered is a
            brand-new kind of despoilment. Trash has always been tossed into
            the seas, but it has been broken down in a fairly short time into
            carbon dioxide and water by marine microorganisms. Now, however, in
            the quest for lightweight but durable means of storing goods, we
            have created a class of products—plastics— that defeat even the
            most creative and voracious bacteria.
        </p>
        <p>
            Unlike many discarded materials, most plastics in common use do not
            biodegrade. Instead they “photodegrade,” a process whereby sunlight
            breaks them into progressively smaller pieces, all of which are
            still plastic polymers. In fact, the degradation eventually yields
            individual molecules of plastic, but these are still too tough for
            most anything—even such indiscriminate consumers as bacteria—to
            digest. And for the past fifty years or so, plastics that have made
            their way into the Pacific Ocean have been fragmenting and
            accumulating as a kind of swirling sewer in the North Pacific
            subtropical gyre.
        </p>
        <p>
            It surprised me that the debris problem in the gyre had not already
            been looked at more closely by the scientific community. In fact,
            only recently—starting in the early 1990s—has the scientific
            community begun to focus attention on the trash in the gyre. One of
            the first investigators to study the problem was W. James Ingraham
            Jr., an oceanographer at the National Oceanic and Atmospheric
            Administration (NOAA) in Seattle. Ingraham’s Ocean Surface Current
            Simulator (OSCURS) predicts that objects reaching this area might
            revolve around in it for sixteen years or more.
        </p>
        <p>
            A year after my sobering voyage, I asked Steven B. Weisberg,
            director of the Southern California Coastal Water Research Project
            and an expert in marine environmental monitoring, to help me make a
            more rigorous estimate of the extent of the debris in the
            subtropical gyre. Weisberg’s group had already published an article
            on the debris they had collected in fish trawls of the Southern
            California Bight, a region along the Pacific coast extending a
            hundred miles both north and south of Los Angeles. As I discussed
            the design plan for our survey with Weisberg’s statisticians, Molly
            K. Leecaster and Shelly L. Moore, it became apparent that we were
            facing a new problem. In the coastal ocean, bodies of water are
            naturally defined, in part, by the coasts they lie against. In the
            open ocean, however, bodies of water are bounded by atmospheric
            pressure systems and the currents those systems create. In other
            words, air, not land, defines the body of water. Because air
            pressure systems move, the body of water we wanted to survey would
            be moving as well. A random sample of a moving area such as the
            gyre would have to be done quite differently from the way
            Weisberg’s group had conducted their survey along the Pacific
            coast.
        </p>
        <p>
            The gyre we planned to survey is one of the largest ocean realms on
            Earth, and one of five major subtropical gyres on the planet. Each
            subtropical gyre is created by mountainous flows of air moving from
            the tropics toward the polar regions. The air in the North Pacific
            subtropical gyre is heated at the equator and rises high into the
            atmosphere because of its buoyancy in cooler, surrounding air
            masses. The rotation of the Earth on its axis moves the heated air
            mass westward as it rises, then eastward once it cools and descends
            at around 30 degrees north latitude, creating a huge,
            clockwise-rotating mass of air.
        </p>
        <p>
            The rotating air mass creates a high-pressure system throughout the
            region. Those high pressures depress the ocean surface, and the
            rotating air mass also drives a slow but oceanicscale surface
            current that moves with the air in a clockwise spiral. Winds near
            the center of the high are light or even calm, and so they do not
            mix the floating debris into the water column. This huge region,
            what I call a “gentle maelstrom,” has become an accumulator of
            debris from innumerable sources along the North Pacific rim, as
            well as from ships at sea.
        </p>
        <p>
            The subtropical gyres are also oceanic deserts—in fact, many of the
            world’s land-based deserts lie at nearly the same latitudes as the
            oceanic gyres. Like their terrestrial counterparts, the oceanic
            deserts are low in biomass. On land the low biomass is caused by
            the lack of moisture; in oceanic deserts the low biomass is a
            consequence of great ocean depths.
        </p>
        <p>
            In coastal areas and shallow seas, winds and waves constantly stir
            up and recycle nutrients, increasing the biomass of the food web.
            In the deep oceans, though, such forces have no effect; the bottom
            sequesters the nutrient-rich residue of millions of years of
            near-surface photosynthetic production, as well as the decomposed
            fragments of life in the sea, trapping them miles below the
            surface. Hence the major source of food for the web of life in deep
            ocean areas is photosynthesis.
        </p>
        <p>
            But even in the clear waters that prevail in the subtropical gyres,
            photosynthesis is confined to the top of the water column. Sunlight
            attenuates rapidly with depth, and by the time it has gone only
            about 5 percent of the way to the bottom, the light is too weak to
            fuel marine plants. The net effect is a vast area poor in
            resources, an effect that makes itself felt throughout the food
            web. Top predators such as tuna and other commercially viable fish
            don’t hang out in the gyres because the density of prey is so low.
            The human predator stays away too: the resources that have drawn
            entrepreneurs and scientists alike to various regions of the ocean
            are not present in the subtropical gyres.
        </p>
        <p>
            What does exist in the gyres is a great variety of filter-feeding
            organisms that prey on the ever-renewed crop of tiny plants, or
            phytoplankton. Each day the phytoplankton grow in the sunlit part
            of the water, and each night they are consumed by the filter
            feeders, a fantastic array of alien-looking animals called
            zooplankton. The zooplankton include chordate jellyfishes known as
            “salps,” which are among the fastest-growing multicellular
            organisms on the planet. By fashioning their bodies into pulsating
            tubes, the salps are able, each day, to filter half the water
            column they inhabit, drawing out the phytoplankton and smaller
            zooplankton for food. But salps are gelatinous creatures with a low
            biomass, and so there is no market for them, either. Hence the
            realm they dominate, one of the largest uniform habitats on the
            planet, remains unexploited and largely unexplored.
        </p>
        <p>
            Leecaster, Moore, and I came up with a plan to make a series of
            trawls with a surface plankton net, along paths within a circle
            with a 564-mile [908-km] radius. The area of the circle would then
be almost exactly 1 million square miles [2.6 million km            <sup>2</sup>]. Trawling would start when we estimated we were under
            the central pressure cell of the high-pressure system that creates
            the gyre.We would regard the starting point as the easternmost
            point along the circumference of the circle.Then we would proceed
            due west to the center of the circle, turn south, and sail back to
            the southernmost point on the circumference, alternating between
            trawling and cruising. We intended to obtain transect samples with
            random lengths and random spacing between trawls. To be
            conservative about our sampling technique, we decided that any
            debris we collected would count only as a sample of the debris
            within the area of the transected circle.
        </p>
        <p>
            In August 1998 I set out with a four-member volunteer crew from
            Point Conception, California heading northwest toward the
            subtropical gyre. Onboard <em>Alguita </em>was a manta trawl, an
            apparatus resembling a manta ray with wings and a broad mouth,
            which skimmed the ocean surface trailing a net with a fine mesh.
            Eight days out of port, the wind dropped below ten knots and we
            decided to practice our manta trawling technique, taking a sample
            at the edge of the subtropical gyre, about 800 miles [1,288 km]
            offshore. We pulled in the manta after trawling three and a half
            miles [5.6 km].
        </p>
        <p>
            What we saw amazed us. We were looking at a rich broth of minute
            sea creatures mixed with hundreds of colored plastic fragments—a
            plastic-plankton soup. The easy pickings energized all of us, and
            soon we began sampling in earnest. Because plankton move up and
            down in the water column each day, we needed to trawl nonstop, day
            and night, to get representative samples. When we encountered the
            light winds typical of the subtropical gyre, we deployed the manta
            outside the port wake, along with two other kinds of nets. Each net
            caught plenty of debris, but far and away the most productive trawl
            was the manta.
        </p>
        <p>
            There was plenty of larger debris in our path as well, which the
            crew members retrieved with an inflatable dingy. In the end, we
            took about a ton of this debris on board. The items included
        </p>
        <p>
            • a drum of hazardous chemicals;
        </p>
        <p>
            • an inflated volleyball, half covered in gooseneck barnacles;
        </p>
        <p>
            • a plastic coat hanger with a swivel hook;
        </p>
        <p>
            • a cathode-ray tube for a nineteen-inch TV;
        </p>
        <p>
            • an inflated truck tire mounted on a steel rim;
        </p>
        <p>
            • numerous plastic, and some glass, fishing floats;
        </p>
        <p>
            • a gallon bleach bottle that was so brittle it crumbled in our
            hands; and
        </p>
        <p>
            • a menacing medusa of tangled net lines and hawsers that we hung
            from the A-frame of our catamaran and named Polly P, for the
            polypropylene lines that made up its bulk.
        </p>
        <p>
            In 2001, in the <em>Marine Pollution Bulletin</em>, we published
            the results of our survey and the analysis we had made of the
            debris, reporting, among other things, that there are six pounds
            [2.7 kg] of plastic floating in the North Pacific subtropical gyre
            for every pound of naturally occurring zooplankton. Our readers
            were as shocked as we were when we saw the yield of our first
            trawl. Since then we have returned to the area twice to continue
            documenting the phenomenon. During the latest trip, in the summer
            of 2002, our photographers captured underwater images of jellyfish
            hopelessly entangled in frayed lines, and transparent filter
            feeding organisms with colored plastic fragments in their bellies.
        </p>
        <p>
            Entanglement and indigestion, however, are not the worst problems
            caused by the ubiquitous plastic pollution. Hideshige Takada, an
            environmental geochemist at Tokyo University, and his colleagues
            have discovered that floating plastic fragments accumulate
            hydrophobic—that is, non-water-soluble—toxic chemicals. Plastic
            polymers, it turns out, are sponges for DDT, PCBs, and other oily
            pollutants. The Japanese investigators found that plastic resin
            pellets concentrate such poisons to levels as high as a million
            times their concentrations in the water as free-floating
            substances.
        </p>
        <p>
            The potential scope of the problem is staggering. Every year some
            5.5 quadrillion (5.5 x 1015) plastic pellets—about 250 billion
            pounds of them—are produced worldwide for use in the manufacture of
            plastic products. When those pellets or products degrade, break
            into fragments, and disperse, the pieces may also become
            concentrators and transporters of toxic chemicals in the marine
            environment. Thus an astronomical number of vectors for some of the
            most toxic pollutants known are being released into an ecosystem
            dominated by the most efficient natural vacuum cleaners nature ever
            invented: the jellies and salps living in the ocean. After those
            organisms ingest the toxins, they are eaten in turn by fish, and so
            the poisons pass into the food web that leads, in some cases, to
            human beings. Farmers can grow pesticide-free organic produce, but
            can nature still produce a pollutant-free organic fish? After what
            I have seen firsthand in the Pacific, I have my doubts.
        </p>
        <p>
            Many people have seen photographs of seals trapped in nets or
            choked by plastic six-pack rings, or sea turtles feeding on plastic
            shopping bags, but the poster child for the consumption of pelagic
            plastic debris has to be the Laysan albatross. The plastic gadgets
            one typically finds in the stomach of the bird— whose range
            encompasses the remote, virtually uninhabited region around the
            northwest Hawaiian Islands—could stock the checkout counter at a
            convenience store. My analysis of the stomach contents of birds
            from two colonies of Laysan albatrosses that nest and feed in
            divergent areas of the North Pacific show differences in the types
            of plastic they eat. I believe those differences reveal something
            about the way plastic is transported and breaks down in the ocean.
        </p>
        <p>
            On Midway Island in the Hawaiian chain, a bolus, or mass of chewed
            food, coughed up by one bird included many identifiable objects. By
            contrast, a bird on Guadalupe Island, which lies 150 miles [241 km]
            off the coast of Baja California, produced a bolus containing only
            plastic fragments. The principal natural prey of both bird colonies
is squid,but as the ecologist Carl Safina notes in his book            <em>Eye of the Albatross</em>, the birds’ foraging style can be
            described as “better full than fussy.” Robert W. Henry III, a
            biologist at the University of California, Santa Cruz, and his
            colleagues have tracked both the Hawaiian and the Guadalupe
            populations of birds and found that the foraging areas of each
            colony in the Pacific are generally nonoverlapping and wide apart.
        </p>
        <p>
            One difference between the two areas is apparently the way debris
            flows into them. In Ingraham’s OSCURS model, debris from the coast
            of Japan reaches the foraging area of the Hawaiian birds within a
            year. Debris from the West Coast of the United States, however,
            sticks close to the coast until it bypasses the foraging area of
            the Guadalupe birds, then heads westward to Asia, not to return for
            six years or more. The lengthy passage seems to give the plastic
            debris time to break into fragments.
        </p>
        <p>
            The subtropical gyres of the world are part of the deep ocean
            realm, whose ability to absorb, hide, and recycle refuse has long
            been seen as limitless. That ecologically sound image, however, was
            born in an era devoid of petroleum-based plastic polymers. Yet the
            many benefits of modern society’s productivity have made nearly all
            of us hopelessly, and to a large degree rationally, addicted to
            plastic. Many, if not most, of the products we use daily contain or
            are contained by plastic. Plastic wraps, packaging, and even
            clothing defeat air and moisture and so defeat bacterial and
            oxidative decay. Plastic is ubiquitous precisely because it is so
            good at preventing nature from robbing us of our hard-earned goods
            through incessant decay.
        </p>
        <p>
            But the plastic polymers commonly used in consumer products, even
            as single molecules of plastic, are indigestible by any known
            organism. Even those single molecules must be further degraded by
            sunlight or slow oxidative breakdown before their constituents can
            be recycled into the building blocks of life. There is no data on
            how long such recycling takes in the ocean—some ecologists have
            made estimates of 500 years or more. Even more ominously, no one
            knows the ultimate consequences of the worldwide dispersion of
            plastic fragments that can concentrate the toxic chemicals already
            present in the world’s oceans.
        </p>
        <p>
            Ironically, the debris is re-entering the oceans whence it came;
            the ancient plankton that once floated on Earth’s primordial sea
            gave rise to the petroleum now being transformed into plastic
            polymers. That exhumed life, our “civilized plankton,” is, in
            effect, competing with its natural counterparts, as well as with
            those life-forms that directly or indirectly feed on them.
        </p>
        <p>
            And the scale of the phenomenon is astounding. I now believe
            plastic debris to be the most common surface feature of the world’s
            oceans.Because 40 percent of the oceans are classified as
            subtropical gyres, a fourth of the planet’s surface area has become
            an accumulator of floating plastic debris. What can be done with
            this new class of products made specifically to defeat natural
            recycling? How can the dictum “In ecosystems, everything is used”
            be made to work with plastic?
        </p>
