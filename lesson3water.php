        <p align="left">
            <strong>Oil in the Sea</strong>
        </p>
        <p align="left">
            by Nancy Rabalais
        </p>
        <p>
            “When it rains, it pours”—or so a motorist caught in a sudden storm
            might think while sliding into another vehicle. It is not merely
            the reduced visibility and the frenetic behavior of drivers in the
            rain that foster such mishaps; the streets also are slicker just
            after the rain begins to fall.Why? Because the oil and grease that
            are dripped, spewed, or otherwise inadvertently deposited by motor
            vehicles onto roadways are among the first materials to be lifted
            off by the rain, thereby literally lubricating the surface. Nor do
            matters end with making life miserable for motorists. The oil and
            grease washed off roads will most likely run into storm sewers and
            be discharged into the nearest body of water. From there, the oily
            materials often are carried to the sea, where they can cause a host
            of environmental problems.
        </p>
        <p>
            These events on a rainy day in the city illustrate an important but
            often overlooked route by which petroleum finds its way into
            coastal waters. Shutting down this and other routes presents a
            pressing challenge. True, the nation is doing a better job than
            ever of keeping oil out of the marine environment. But much work
            remains. We need to better understand the various pathways by which
            oil gets into the environment, how it behaves when it gets there,
            what effects it has on living organisms, and, perhaps most
            important, what steps can be taken to further reduce the amount of
            petroleum that enters the nation’s and the world’s oceans.
        </p>
        <p align="left">
            <strong>SOURCES AND PROBLEMS</strong>
        </p>
        <p>
            Approximately 75 million gallons of petroleum find their way into
North America’s oceans each year, according to            <em>Oil in the Sea III: Inputs, Fates, and Effects</em>, a report
            issued in 2002 by the National Research Council (NRC). About 62
            percent of the total—roughly 47 million gallons per year—derives
            naturally from out of the ocean floor. The rest comes from human
            activities.
        </p>
        <p>
            Contrary to common belief, the bulk of human-related inputs is not
            due to large-scale spills and accidents that occur during the
            transport of crude oil or petroleum products. Indeed, these types
            of releases account for only about 10 percent of the oil that
            reaches the sea as a result of human activity. The other 90 percent
            comes in the form of chronic low-level releases associated with the
            extraction and consumption of petroleum. Within this category, the
            biggest problem is nonpoint source pollution. Rivers and streams
            that receive runoff from a variety of land-based activities deliver
            roughly 16 million gallons of oil to North American coastal waters
            each year, more than half of the total anthropogenic load. The
            loads are most obvious in watersheds that drain heavily populated
            areas. Other sources of oil that turns up in the marine environment
            include jettisoned aircraft fuel, marine recreational vehicles, and
            operational discharges, such as cargo washings and releases from
            petroleum extraction.
        </p>
        <p>
            There is at least some good news. Less oil is now entering the
            oceans as compared to the levels found in a previous NRC report
            issued in 1985. Some of this change may be attributable to
            differences between methodologies used in the two reports, but some
            decreases are due to improved regulations regarding how oil is
            produced and shipped. Spills from vessels in North American waters
            from 1990 through 1999 were down by nearly two-thirds compared to
            the prior decade. There also has been a dramatic decline in the
            amount of oil released into the environment during exploration for
            and production of petroleum and natural gas. Still, the recent NRC
            report concludes that despite such progress, the damage from oil in
            the marine environment is considerably more pervasive and
            longer-term than was previously understood.
        </p>
        <p>
            Oil in the sea, whether from catastrophic spills or chronic
            releases, poses a range of environmental problems. Major spills
            receive considerable public attention because of the obvious
            attendant environmental damage, including oil-coated shorelines and
            dead or moribund wildlife, especially among seabirds and marine
            mammals. The largest oil spill in U.S. waters occurred on March 24,
            1989, when the tanker <em>Exxon Valdez</em>, en route from Valdez,
            Alaska, to Los Angeles, California, ran aground on Bligh Reef in
            Prince William Sound, Alaska. Within six hours of the grounding,
            the ship spilled approximately 10.9 million gallons of crude oil,
            which would eventually affect more than 1,100 miles [1,770 km] of
            coastline. Large numbers of animals were killed directly, including
            an estimated 900 bald eagles, 250,000 seabirds, 2,800 sea otters,
            and 300 harbor seals.
        </p>
        <p>
            Oil pollution also can have more subtle biological effects, caused
            by the toxicity of many of the compounds contained in petroleum or
            by the toxicity of compounds that form as the petroleum degrades
            over time. These effects may be of short duration and limited
            impact, or they may span long periods and affect entire populations
            or communities of organisms, depending on the timing and duration
            of the spill and the numbers and types of organisms exposed to the
            oil.
        </p>
        <p>
            Of course, oil spills need not be large to be hazardous to marine
            life. Even a small spill in an ecologically sensitive area can
            result in damage to individual organisms or entire populations.A
            spill’s influence also depends on the type and amount of toxins
            present in the petroleum product released. For instance, the fuel
            oil leaked when the tanker <em>Prestige </em>broke apart off the
            northwest coast of Spain in 2002 was initially more toxic than the
            crude oil spilled from the <em>Exxon Valdez</em>.
        </p>
        <p>
            One major problem with all spills, no matter their size or type, is
            that the oil can remain in the environment for a long time. Several
            lines of evidence point to continued exposure of marine organisms
            to oil spilled by the <em>Exxon Valdez</em>. Substantial subsurface
            oil beneath coarse beaches in the spill area was found in the
            summer of 2001. The oil was still toxic and appeared to be
            chemically unchanged since its release more than a decade earlier.
            Some researchers predict that oil beneath mussel beds in the region
            affected by the spill may not decline to background levels for at
            least another two decades. In another instance, scientists studying
            salt marshes in Massachusetts that had been covered by fuel oil
            spilled from the barge <em>Florida </em>30 years ago recently
            reported that oil is still present in sediments at depths of 6 to
            28 centimeters [2.4 to 11 inches]. Moreover, the concentrations of
            oil found in the sediments are similar to those observed shortly
            after the spill. The researchers, from the Woods Hole Oceanographic
            Institution, predict that the compounds may remain there
            indefinitely, while crabs and other intertidal organisms continue
            to burrow through the oil-contaminated layer.
        </p>
        <p align="left">
            <strong>PRESCRIBED REMEDIES</strong>
        </p>
        <p>
            Reducing the threat of oil in the oceans will require blocking the
            routes by which oil enters the environment. Focusing on inputs from
            spills and nonpoint sources, two of the major anthropogenic
            contributors, will show the range of actions needed.
        </p>
        <p align="left">
            <strong>Reducing Spills</strong>
        </p>
        <p>
            Worldwide, large spills resulting from tanker accidents are down
            considerably from the totals reported by the NRC in 1985—they have
            decreased to 17 million gallons annually from 140 million gallons
            annually. This gain was achieved even as the size of the global
            tanker fleet increased by 900 vessels, to a total of 7,270 in 1999.
            Progress was made through the implementation of numerous
            regulations and by technological advances in vessel construction,
            including the increased use of double-hull tankers, the use of new
            construction materials, and improvements in vessel design. Spills
            larger than 50,000 gallons now represent less than 1 percent of
            total spills by number but are responsible for more than 80 percent
            of the total spill volume. It is important to note, however, that
            more than half of all tanker spills now occur in North American
            waters. Although the number and size of spills in these waters have
            been reduced considerably during the past two decades, with total
            volume falling to 2.5 million gallons per year, they remain the
            dominant domestic source of oil input to the marine environment
            from petroleum transportation activities, as they are globally.
        </p>
        <p>
            Prevention, in the form of stricter regulations for tankers, has
            obviously not prevented all large spills, as the <em>Prestige </em>
            spill so dramatically illustrated. Irreparably damaged by a storm,
            the ship spilled nearly 3 million gallons of fuel oil, which spread
            over 125 miles [201 km] of coastline in one of Spain’s leading
            areas of commercial fishing and shellfishing. But out of this
            calamity may come improved policies. The spill has highlighted
            concerns about older, single-hull ships (the <em>Prestige </em>was
            26 years old) that are due to be phased out by 2015, and about what
            Europe should do to keep these ships safe and inspected in the
            meantime. Under a proposal made by the European Commission in
            December 2002, which the European Union (EU) is expected to adopt,
            single-hull oil tankers will not be allowed to carry heavy grades
            of oil in EU waters. Prohibited grades will include heavy fuel oil,
            heavy crude oil, waste oils, bitumen, and tar. Questions also have
            been raised about singlehull ships that are bypassing EU ports in
            order to avoid tough new EU-mandated inspection rules adopted in
            1999 after the <em>Erika </em>oil spill polluted 250 miles [402 km]
            of French shoreline. The EU regulations before the passage of the
            latest restrictions on transport in EU waters required port
            authorities to check at least 25 percent of all ships coming into
            dock, starting with older, single-hull vessels, with priority going
            to ships flying flags of convenience or registered in countries
            with lax safety, labor, or tax rules.
        </p>
        <p>
            The potential for a large tanker spill, however, is still
            significant, especially in regions without stringent safety
            procedures and maritime inspection practices. Furthermore, tanker
            traffic is expected to grow over the coming decades, as the centers
            of oil production continue to migrate toward the Middle East,
            Russia, and former Soviet states. U.S. agencies should expand their
            efforts to work with ship owners, domestically and internationally,
            through the International Maritime Organization, to enforce and
            build on the international regulatory standards that have
            contributed to the recent decline in oil spills and operational
            discharges.
        </p>
        <p>
            Tankers are not the only potential source of large spills. There
            also is concern about aging oil pipelines and other coastal
            facilities. The aging of the infrastructure in fields in the
            central and western Gulf of Mexico and in some areas of Alaska is
            especially disconcerting, because these facilities often lie near
            sensitive coastal areas. Many pipelines in coastal Louisiana that
            should be buried no longer are. Numerous wellheads and other
            facilities within the estuaries are being abandoned, as one company
            takes over facilities from another. As the resources become
            depleted, the cost of extraction exceeds the profit to be gained
            from sale of the product, and owners file bankruptcy and abandon
            holdings. Federal agencies should work with state environmental
            agencies and industry to evaluate the threat posed by aging
            pipelines and abandoned facilities, and to take steps to minimize
            the potential for spills.
        </p>
        <p align="left">
            <strong>Reducing Nonpoint Source Inputs</strong>
        </p>
        <p>
            Regulations developed under the Clean Water Act of 1972 have
            significantly reduced the number and amount of pollutants coming
            from the end of a pipe, and the Toxics Release Inventory tracks
            many of the pollutants that are released. But the more diffuse
            sources, such as urban runoff, atmospheric deposition, and
            watershed drainage, are not regulated or even monitored adequately.
            Unfortunately, nonpoint source runoff is difficult to measure and
            sparsely sampled; as a result, estimates have a high degree of
            uncertainty. Clearly, new federal, state, and local partnerships
            are needed to monitor runoff and to keep better track of how much
            petroleum and other pollutants industry and consumers are
            releasing.
        </p>
        <p>
            Such a call for increased monitoring will undoubtedly elicit groans
            from managers and other people responsible for water quality, yet
            few people would argue that existing efforts are adequate for the
            overall task. Even the better-funded federal efforts are
            insufficient. The National Stream Quality Accounting Network, the
            national network operated by the U.S. Geological Survey to monitor
            water quality in streams, has increasingly fewer stations,
            particularly along the coast, as budgets are tightened and
            tightened again. Additional funding is necessary to invigorate this
            program. Coastal stations that have been shut down need to be
            restored, and new stations along the coast and at critical inland
            locations need to be added. The network also must expand monitoring
            to include total hydrocarbons (instead of merely “oil and grease,”
            as is now the case), as well as a particular class of compounds
            called polynuclear aromatic hydrocarbons (PAHs). Growing evidence
            indicates that even at very low concentrations, PAHs carried in
            crude oil or refined products can have adverse effects on biota.
            This suggests that PAHs released from chronic sources may be of
            greater concern than previously recognized, and that in some
            instances the effects of petroleum spills may last longer than
            expected.
        </p>
        <p>
            There also is a great need for expanded basic research. The most
            significant unanswered questions remain those regarding the effects
            on ecosystems of long-term, chronic, low-level exposures resulting
            from petroleum discharges and spills caused by development
            activities. Federal agencies, especially the Environmental
            Protection Agency (EPA), the U.S. Geological Survey, and the
            National Oceanic and Atmospheric Administration, should work with
            academia and industry to develop and implement a major research
            effort to more fully understand and evaluate the risk posed to
            organisms and the marine environment by the chronic release of
            petroleum, especially the cumulative effects of chronic releases
            and multiple types of hydrocarbons.
        </p>
        <p>
            Apace with advances in monitoring and research, positive steps can
            be taken to implement proven methods for reducing nonpoint source
            discharges of oil into the environment. Remember, for example, the
            motorist brought low by slick pavement. The oil and grease that
            wash off highways during rain storms usually bypass sewage
            treatment plants in storm water overflow systems that pump the rain
            and any materials caught up in the flow directly to the closest
            body of water. In many urban settings, this source can be a
            significant contribution of petroleum to the ocean. As the
            population of coastal regions increases, urban runoff will become
            more polluted because of the expansion in the numbers of cars,
            asphalt-covered highways and parking lots, municipal wastewater
            loads, and the use and improper disposal of petroleum products.
            Collection and treatment of storm water overflows is necessary to
            control these inputs. Improved landscape management and urban
            management, increasing use of fuelefficient vehicles, and public
            education can all contribute to lessening petroleum runoff.
        </p>
        <p>
            The power of public education can be seen in the Chesapeake Bay
            region. In small but effective ways, people living within the bay’s
            watershed are reminded daily of their consumptive uses of
            pollutants that enter the water. They see license plates that
            proclaim “Save the Bay” and storm water drain covers that say
            “Don’t Dump. Drains to Chesapeake Bay.” Education is a first step
            for a better-informed public that will recognize the need for less
            consumption, less pollution, and better conservation of resources.
            This knowledge should but does not always lead to legislation and
            funding for reducing pollutant loads, including oil reaching the
            sea.
        </p>
        <p>
            Additional remedial actions should target the recreational
            watercraft that have grown so popular during the past two decades.
            Most of these craft, including jet skis and small boats with
            outboard motors, use two-stroke engines, which release up to 30
            percent of their fuel directly into the water. Collectively, these
            watercraft contribute almost 1 million gallons of petroleum each
            year into North American waters. The bulk of their input is in the
            form of gasoline, which is thought to evaporate rapidly from the
            water surface. However, little is known about the actual fate of
            the discharge, or about its biological effects while in its
            volatile phase, which is highly toxic. In 1990, heightened
            awareness of this problem led the EPA to begin regulating the
            “nonroad engine” population, under the authority of the Clean Air
            Act. Questions remain regarding the amount of petroleum residing in
            the water column or along the surface for biologically significant
            lengths of time. The EPA should continue its phase-out efforts
            directed at two-stroke engines, and it should expand research, in
            conjunction with other relevant federal agencies, on the fate and
            effects of discharges from these older, inefficient motors.
        </p>
        <p>
            To achieve maximal effectiveness, efforts to understand and
            minimize oil pollution should pay heed to worldwide needs. The
            United States and other developed countries have invested much in
            technologies to reduce the spillage of oil into the marine
            environment, as well as in the science that has increased knowledge
            of the effects of spilled oil, either acute or chronic. This
            knowledge should be transferred to people in other countries that
            are developing their petroleum reserves.It is imperative that the
            petroleum industry not simply comply with regulations in developing
            countries where they operate, but that they transfer the knowledge
            derived from extensive studies in U.S. waters to areas where their
            operations are expanding.
        </p>
        <p align="left">
            <strong>SHARED RESPONSIBILITIES</strong>
        </p>
        <p>
            It is tempting to blame the oil and shipping industries alone for
spills such as those from the <em>Exxon Valdez </em>and the            <em>Prestige</em>, but everyone who benefits from oil bears
            responsibility for the fraction that enters the sea. If companies
            have failed to build and buy double-hull tankers, it is in part
            because consumers do not wish to pay the increased fuel prices that
            would be needed to offset the extra cost. The push for improved
            methods of extracting, producing, and transporting oil must come
            from the general public, and this link reinforces the need for
            education.
        </p>
        <p>
            The price of oil and natural gas is a major force in the world
            economy. As recently as the late 1990s, the average price of a
            barrel of crude oil was less than the cost of a takeout dinner. Yet
            a fluctuation of 20 or 30 percent in the price can influence
            automotive sales, travel decisions, interest rates, stock market
            trends, and the gross national products of industrialized nations.
            Perceived or real decreases in the availability of oil led to long
            lines for gasoline in the early 1970s and to the development and
            sale of fuel-efficient vehicles. Many observers argue that the low
            oil prices in recent years have helped put a glut of gas-guzzling
            vehicles on the highway.As the prices of a barrel of oil and a
            gallon of gasoline continue to rise in the face of social unrest in
            South America and the unrelenting hostilities in the Middle East,
            the value of this limited commodity may become more apparent.
        </p>
        <p>
            The United States needs an energy policy that treats petroleum as a
            limited and treasured commodity that encourages conservation rather
            than waste, and that supports alternative energy development and
            use. The nation also should tighten controls on air and water
            pollution and should adequately fund environmental monitoring of
            water resources. Without these policy changes, U.S. citizens cannot
            expect wise and environmentally sound use of the nation’s or the
            world’s resources. And if U.S. citizens cannot reduce their overly
            consumptive use of petroleum to help in curbing its introduction to
            the diffuse but voluminous nonpoint source pollutant load, how can
            they expect citizens of developing countries to strive for less
            polluting and consumptive actions in the face of an improving
            economy derived from sales of petroleum?
        </p>
        <p>
            “We’re all in this together,” as the saying goes, including the
            motorist who crashed on that oil-slicked road. There is just one
            global economy, one global ecosystem, and one global source of
            nonrenewable petroleum reserves.
        </p>
