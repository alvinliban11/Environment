        <strong>Water Quality in the Nation’s </strong>
    </p>
    <p align="left">
        <strong>Streams and Aquifers</strong>
    </p>
    <p align="left">
        from the United States Geological Survey (USGS)
    </p>
    <p align="left">
        <strong>
            INFORMATION TO MANAGE, PROTECT, AND RESTORE WATER QUALITY
        </strong>
    </p>
    <p>
        The National Water-Quality Assessment (NAWQA) Program of the U.S.
        Geological Survey (USGS) assesses the quality of streams, ground water,
        and aquatic ecosystems in major river basins and aquifer systems across
        the Nation. These assessments characterize the ambient water
        resource—the source for more than 60 percent of the Nation’s drinking
        water and water for irrigation and industry.
    </p>
    <p>
        During its first decade (1991–2001), NAWQA completed assessments in 51
        study units, which provided baseline data and information on the
        occurrence of pesticides, nutrients, volatile organic compounds (VOCs),
        trace elements, and radon in water, and on the condition of aquatic
        habitats and fish, insect, and algal communities. Conditions are
        compared to selected benchmarks, such as for drinkingwater quality and
        the protection of aquatic organisms. Each assessment follows a
        nationally consistent study design and methodology, thereby providing
        information about local water-quality conditions as well as providing
        insight on where and when water quality varies regionally and
        nationally. This document contains findings of regional and national
        interest along with examples from the study units illustrating these
        findings.
    </p>
    <p>
        During its second decade (2002–2012), NAWQA plans to reassess 42 of the
        51 study units. These assessments will fill critical gaps in the
        characterization of water-quality conditions; determine trends at many
        of the monitoring sites; and build upon earlier assessments that link
        water-quality conditions and trends to natural and human factors. . . .
    </p>
    <p>
        The first round of NAWQA assessments indicates that while many of our
        Nation’s waters are suitable for most uses, contaminants from nonpoint
        and point sources continue to affect our streams and ground water in
        parts of every study unit. Findings from the 51 study units show that •
        contamination of streams and ground water is widespread in agricultural
        and urban areas, and is characterized by complex mixtures of nutrients,
        trace elements, pesticides, VOCs, and their chemical breakdown
        products, and
    </p>
    <p>
        • water quality and aquatic-ecosystem health are controlled by a
        combination of factors, including chemical use, land use,
        land-management practices, and natural features, such as geology,
        hydrology, soils,
    </p>
    <p>
        and climate.
    </p>
    <p>
        Local, state, tribal, and national stakeholders use NAWQA information
        to design and implement strategies for managing, protecting, and
        monitoring water resources in many different hydrologic and land-use
        settings across the Nation, such as to:
    </p>
    <p>
        • support development of regulations, standards, and guidelines that
        reflect actual contaminant occurrence, including contaminant mixtures,
        breakdown products, seasonal patterns, and variability among different
        settings;
    </p>
    <p>
        • identify key sources of nonpoint pollution in agricultural and urban
        areas;
    </p>
    <p>
        • prioritize geographic areas and basins in which water resources and
        aquatic ecosystems are most vulnerable to contamination and where
        improved treatment or management can have the greatest
    </p>
    <p>
        benefits;
    </p>
    <p>
        • improve strategies and protocols for monitoring, sampling, and
        analysis of all hydrologic components, including the atmosphere,
        surface water, ground water, and biological communities;
    </p>
    <p>
        • contribute to State assessments of beneficial uses and impaired
        waters (Total Maximum Daily Loads or TMDLs), strategies for
        source-water protection and management, pesticide and nutrient
        management plans, and fish-consumption advisories; and, • sustain the
        health of aquatic ecosystems through improved stream protection and
        restoration
    </p>
    <p>
        management.
    </p>
    <p>
        NAWQA studies indicate that contaminants are widespread, albeit often
        at low concentrations, in river basins and aquifer systems across a
        wide range of landscapes and land uses. In the mostly agricultural
        Lower Tennessee River Basin, for example, 52 different pesticides were
        detected in streams and rivers, and VOCs were detected in about 67
        percent of sampled springs and wells that tap underlying carbonate
        aquifers. Nationally, at least one pesticide was found in about 94
        percent of water samples and in 90 percent of fish samples from
        streams, and in about 55 percent of shallow wells sampled in
        agricultural and urban areas.
    </p>
    <p>
        The type and concentrations of contaminants that are found in urban and
        agricultural water resources are closely related to the chemicals that
        are used (such as fertilizers and pesticides) or that are released with
        waste products (such as sewage or manure). For example, phosphorus and
        many insecticides, such as diazinon, carbaryl, chlorpyrifos, and
        malathion, were detected more frequently and usually at higher
        concentrations in urban streams than in agricultural streams.
        Nationally, at least one pesticide guideline established to protect
        aquatic life was exceeded in nearly all (about 93 percent) of the urban
        streams sampled.
    </p>
    <p>
        Nitrogen and many herbicides—most commonly atrazine and its breakdown
        product deethylatrazine (DEA), metolachlor, alachlor, and
        cyanazine—generally were detected more frequently and usually at higher
        concentrations in streams and shallow ground water in agricultural
        areas than in urban areas. Occurrence is linked to use; these
        herbicides rank in the top five used for agriculture. Concentrations of
        nitrate exceeded the U.S. Environmental Protection Agency (USEPA)
        drinking-water standard of 10 milligrams per liter in samples collected
        from about 20 percent of shallow wells in agricultural areas (versus
        about 3 percent in urban areas).
    </p>
    <p>
        VOCs were detected frequently in shallow ground water beneath urban
        areas (in about 90 percent of monitoring wells sampled) and less
        frequently in shallow ground water beneath agricultural areas (in 20
        percent of monitoring wells). Some of the most common VOCs in urban
        areas, such as in the Delaware River Basin, were the solvents
        trichloroethene (TCE), tetrachloroethene (PCE), 1,1,1-trichloroethane
        (TCA), and trichloromethane (also known as chloroform), which is also a
        disinfection by-product of water treatment; and the gasolinerelated
        compounds benzene, toluene, xylene, and methyl tert-butyl ether (MTBE).
    </p>
    <p>
        New pesticides, VOCs, and other synthetic chemicals are introduced into
        the environment every year as products are approved for agricultural or
        urban use. USGS has expanded its laboratory methods to analyze for
        these “emerging”contaminants, testing new methods in areas where their
        use is the greatest.
    </p>
    <p align="left">
        <strong>
            WATER QUALITY AND ITS CONNECTION TO LAND USE— AGRICULTURAL AND
            URBAN SOURCES OF NONPOINT POLLUTION
        </strong>
    </p>
    <p align="left">
        <strong>Implications</strong>
    </p>
    <p>
        Reducing chemical use and improving disposal practices can help reduce
        contaminant concentrations in both urban and agricultural settings.
        More information about chemical use— data that are virtually
        unavailable in urban areas—is critical to linking contaminants to their
        sources, thus helping individuals, businesses, and industry as well as
        local, State, and Federal governments to improve water quality.
    </p>
    <p>
        <strong>INTERACTIONS AMONG WATER, AIR, AND AQUATIC ECOSYSTEMS </strong>
        Interactions among surface water, ground water, land, and the
        atmosphere govern the occurrence and movement of water and
        contaminants, and thus affect the health of aquatic ecosystems. NAWQA
        studies therefore include assessments of multiple components of the
        hydrologic system, including streams, rivers, and ground water within
        the study units; the atmosphere; and biological communities (primarily
        fish, aquatic invertebrates, and algae) and stream habitat.
    </p>
    <p align="left">
        <strong>INTERACTIONS BETWEEN SURFACE WATER AND GROUND WATER</strong>
    </p>
    <p>
        Ground-water discharge can influence the quality of streams and
        ultimately the receiving water. For example, ground water supplies
        about half of the water and nitrogen to streams in the Chesapeake Bay
        watershed and is therefore an important source of nitrogen to the
        Chesapeake Bay. Streams, in turn, can influence the quality of ground
        water. Contaminants generally are more prevalent and detected at higher
        concentrations in streams than in ground water. This is largely
        determined by chemical properties of contaminants and flow conditions—
        ground water typically is not vulnerable to contamination by compounds
        that attach to soils or that are unstable in water, and relatively long
        residence times along ground-water flow paths allow many chemicals to
        degrade, disperse, or be diluted before reaching a well.
    </p>
    <p>
        Interactions between surface water and ground water are affected by
        natural features such as soils, geology, and hydrology, and by human
        activities such as ground-water pumping. Exchanges of water and
        contaminants can be rapid in areas underlain by carbonate rocks or by
        permeable and welldrained soils and sediment, such as sand and gravel.
        Pumping ground water can accelerate exchanges between streams and
        ground water. In some areas, concentrations of contaminants can
        decrease during these exchanges, such as when chemicals are sorbed onto
        soil particles or are transformed by chemical or biological processes.
    </p>
    <p>
        Ground-water and surface-water exchanges are substantial in carbonate
        aquifers near San Antonio, Texas. Major streams lose water to the
        Edwards aquifer as they flow across the highly permeable, faulted and
        fractured carbonate rocks of the aquifer outcrop. Although streams that
        recharge the aquifer originate in and flow through mostly undeveloped
        rangeland, some of them also flow through urbanized northern San
        Antonio.
    </p>
    <p align="left">
        <strong>Implications</strong>
    </p>
    <p>
        Ground-water contributions to streams and rivers can be substantial.
        For this reason, ground-water flow and quality should be considered in
        making decisions for stream protection, such as in establishing Total
        Maximum Daily Loads (TMDLs). Surface-water recharge to ground water
        also is important, particularly where public-supply wells are located
        near streams, and should be considered in source-water management and
        well-head protection programs.
    </p>
