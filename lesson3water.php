    pesticides in the soil. City officials realized they would have to spend
    between $6 billion and $8 billion to build a water filtration plant to
    clean the water. Instead, they were able to purchase watershed land in the
    Catskills sufficient to naturally filter the water for only $1.5 billion.
    Quality of the water supply was increased and money was saved.
</p>
<p>
